<h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The expansion of Universal&#8217;s wildly popular 1950&#8217;s &amp; 1960&#8217;s themed Cabana Bay Beach Resort includes two new rounded white and blue eleven-story towers, each approximately 124,000 square feet, that adds an additional 400 hotel rooms to the south end of the resort property, with half of those rooms overlooking Universal&#8217;s new 28-acre water park, Volcano Bay.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The new towers, officially named &#8220;Beachside&#8221; and &#8220;Bayside,&#8221; were placed between Volcano Bay and the southernmost end of the existing resort. The architectural style of the new buildings are light, glassy and curved, versus the brightly colored boxy look of the earlier phases of the existing resort. Cabana Bay&#8217;s first phase of rooms on the opposite end of the property were motel-inspired, while the 400 room tower expansion represent an era when motels adapted a hotel look.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The interior of the tower rooms are very similar to their Cabana Bay predecessors, which feature bright aquas and oranges along with 50&#8217;s &amp; 60&#8217;s decor. A new feature that the expansion towers include that differ from the existing resort is the introduction of 20 two-bedroom suites, which are the first among Universal&#8217;s on-property hotels. On the waterpark side of the towers, both bedrooms in these units are volcano-view.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Universal selected Alticor Inc to complete this expansion project based on the complexity and aggressive schedule, combined with our experience working on several of their fast-paced projects in the past. Additionally, the towers&#8217; footprint sat less than 20 feet from active hotel, another active construction site for the new Volcano Bay water park, while compounded with parking garages being constructed on both flanks of the project site, leaving our team with very limited laydown and staging space throughout the entire duration of the project.</p>

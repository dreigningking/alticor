                    <h2>About the Project</h2>
<p>The new Marriott International Headquarters building stands 22 stories tall and provides approximately 785,000 gross SF for 3,500+ Marriott International associates. Featuring 486 offices, 2,346 workstations, 3,232 lockers and 210+ conference rooms (supporting 2-18+ people), the new headquarters building is designed to provide new opportunities for flexible, collaborative workspaces. The building also meets LEED Gold standards and offers state-of-the-art technology throughout. In addition, the building provides an abundance of modern amenities to Marriott associates including a childcare center, fitness center and cafeteria.</p>

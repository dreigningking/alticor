        <div class="grid-box1 ">
            <p><strong>Complex Support of Excavation Design</strong></p>
<p>The five-story parking garage connecting the headquarters building and hotel required an excavation of over 140,000 CY of soil and rock, adjacent to multiple structures that were to remain in place within feet of the excavation. Given the layout of the building and the close vicinity to other buildings, the excavation required a complex Support of Excavation (SOE) design.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p><strong>Implementation of Unconventional Support of Excavation Systems</strong></p>
<p>Due to the depth and complexity of the excavation and adjacent structures, unconventional SOE systems had to be implemented. Major elements of the SOE system included 170 soldier piles (70-feet-deep), 60,000 SF of lagging, 458 tiebacks drilled up to 80-feet-deep, 24 cross-lot braces (24-inch diameter x 80-feet long) and 18 rakers (14-24-inch diameter).</p>
<p>The vertical support along the perimeter of the site consisted of 170 soldier piles. With lagging and tiebacks installed among many of these soldier piles, this system created most of the outside form of the foundation wall. Due to the depth of the excavation, pile drilling had to occur through nearly 20 feet of hard granite bedrock. To more accurately schedule and track production, the drill operators logged drill durations to better understand and create more detailed rock profiles. Through these profiles and production estimates, the SOE contractor was able to more accurately plan labor and equipment requirements to meet the project schedule.</p>
<p>In the corner of the project where two restaurants were to remain, tiebacks were drilled beneath the buildings on the south side along with bracket piles to support the building’s foundation. Due to the extended tiebacks on the south side of the restaurants, additional tiebacks could not be drilled on the adjacent side of the buildings. In order to avoid potential contact between crossing tiebacks, the SOE contractor designed a substantial 80-foot-wide steel pipe strut bracing system. The strut system consisted of steel pipe braces connected to wales running parallel to the lagging. Each of the four levels of bracing consisted of six pipe braces and adjacent support beams. After the installation of each level and prior to proceeding with excavation, each of the pipes and wales had to be simultaneously and hydraulically jacked to distribute the load evenly along the supported adjacent walls.</p>
<p>Due to the depth of the adjacent building’s parking garage on the south side of the project, steel rakers had to be installed in lieu of tiebacks. Rakers varying from 30 to 80-feet-long at multiple elevations were angled down from the piles to a concrete heel block and jacked against embedded steel in the heel block. All cross-lot braces and rakers spanning through the future parking garage could not be taken out prior to the substructure being placed. Major elements of the SOE system were modeled into the new building construction BIM model to coordinate blockouts in the structure, thus avoiding major impacts. This coordination effort allowed for all shear walls, columns and footings to be installed prior to the removal of SOE steel.</p>

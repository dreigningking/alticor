                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The 227,000 SF Denver International Airport (DEN) Great Hall Phase 1 project entailed a renovation of DEN&#8217;s main terminal that included a modern ticketing pod check-in, self-bag drops, expanded curbside floorplate, new restrooms, new escalators and updated finishes.</p>
<p style="text-align: center;">For the new ticketing pod check-in, the renovation added a four-pod style ticketing area, new dynamic signage, 86 new self-bag drops and a baggage handling system. This new system streamlines the check-in process for passengers and airlines.</p>
<p style="text-align: center;">The expanded 21,000 SF curbside floorplate included new bathrooms for the east and west side of the terminal and new curtainwall and insulated metal panels on the relocated exterior skin. Sleek wood paneling ceilings and quartz tile flooring were installed throughout the main terminal to welcome passengers into the newly renovated Great Hall.</p>


                    <h2>About the Project</h2>
<p>Nestled into a hillside overlooking Stanford University and San Francisco Bay, the new SLAC Science and User Support Building (SUSB) provides much-needed space for scientists to collaborate and plan their work in a state-of-the-art, high performance building. As the gateway to the SLAC National Accelerator Laboratory Campus, the new Science and User Support Building serves as the welcome center for visitors, and also as a museum, with pieces of the Lab’s important history of scientific discoveries displayed throughout the lobby.</p>
<h3>CONSTRUCTION</h3>
<p>The design-build project includes a new four-story, 62,125-SF signature building; a 350-seat auditorium with a green roof; a large cafeteria with two patio areas; multiple conference rooms; a large training room; and several “collaboration” spaces throughout the four-story atrium. The first phase of the project involved the demolition of existing facilities including the original Panofsky Auditorium, visitor’s center and cafeteria.</p>
<h3>FACILITY SERVICES</h3>
<p style="margin: 0in; margin-bottom: .0001pt;"><span style="color: #333333;">Alticor Inc provided Transition to Stabilized Occupancy (TSO) and Facility Management (FM) services for Stanford Linear Accelerator Center.</span></p>
<p style="margin: 0in; margin-bottom: .0001pt;"><span style="color: #333333;">The Science and User Support Building (SUSB), part of the Stanford Linear Accelerator Center (SLAC), was built to provide office space and conference centers for Stanford management, as well as a student and faculty cafeteria. The Alticor Inc Services team was activated to make the building Day One Ready. The team did this by ensuring reliable operation of building systems, swift and consistent resolution to issues that arise post-construction, and the development of a maintenance and operations program that meets the needs of the facility as the building matures.</span></p>
<p>&nbsp;</p>
<p style="margin: 0in; margin-bottom: .0001pt;"><span style="color: #333333;">Alticor Inc provided comprehensive Transition to Stabilized Occupancy (TSO) services to the Stanford Linear Accelerator Center (SLAC) and provided Computerized Maintenance Management System (CMMS) data loading and maintenance, warranty management services, occupant and staff training on building systems and operations.</span></p>


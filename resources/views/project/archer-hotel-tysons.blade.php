                    <h2>About the Project</h2>
<p>The new, upscale Archer Hotel is located in the business district of Tysons Corner, Virginia. The hotel is the seventh location of the boutique Archer Hotel brand and is part of the planned eight million square foot Scotts Run development.</p>
<p>&nbsp;</p>
<p>The new, seven-story, 181,449 SF Archer Hotel includes 178 rooms, 8,795 SF of restaurant and retail space and 9,209 SF of amenity space. Amenities include private meeting space on the mezzanine level, event space on the first and seventh floors, an exercise room, a marketplace, an upscale restaurant, covered patios and a roof-top terrace and bar. The hotel also includes 64 parking spaces on two levels of below-grade parking totaling 35,036 SF.</p>
<p>&nbsp;</p>
<p>A unique feature of the hotel is its intricate façade which includes brick, curtainwall and metal panels. The façade required extensive coordination to properly execute the complex interface between several material types while maintaining weatherproofing integrity. Its unique composition includes five brick types, five metal panel types and three types of glass. The glass consists of curtainwall ribbons, storefront sections on the first floor and steel replica windows for meeting spaces on the perimeter of the building. Alticor Inc held early design review meetings with the owner, trade partners and a third-party consultant to thoroughly understand and carefully detail the relationship between all the varied material types. In addition, Alticor Inc and trade partners constructed mock-ups at the off-site staging area to identify risks, troubleshoot and determine the best sequence for installing the exterior skin.</p>
<p>&nbsp;</p>
<p>Another unique aspect of the hotel is the rooftop terrace and bar. The terrace features a structural timber frame roof which spans the width of the terrace and is composed of glulam beams from Minnesota. Due to site constraints and the lack of laydown area, the delivery of each beam was carefully scheduled and erection and installation was meticulously sequenced over the span of five weeks. In addition, the timber is supported by concrete and steel structural elements which remain exposed and part of the interior design. The interface between these three structural elements required careful coordination and attention to detail to ensure proper installation while preserving aesthetic quality.</p>


                    <h2>About the Project</h2>
<p>The Andrews Federal Center Bus Operations and Maintenance Facility project included the design and construction of two buildings in support of the Washington Metropolitan Area Transit Authority’s (WMATA) mixed fleet of transit vehicles. Constructed on 35 acres of land, the new 335,000 SF complex includes a Bus Operating Garage, a Bus Transportation Operations Facility, a Fuel/Wash Building, a Heavy Repair and Overhaul (HR&amp;O) Facility, a Warehouse, a Service Vehicle Building, Bus Engineering, a Guard House, and Pedestrian Bridge. The Bus Operations and Maintenance Facility supports a 175 bus fleet (20% of which would be articulated buses) and operates 24/7.</p>
<p>&nbsp;</p>
<p>The HR&amp;O Facility supports the entire WMATA fleet of 1,600 buses and operates eight hours a day, five days a week. The Bus Operations and Maintenance Facility includes maintenance, operations, fuel, wash, bus parking, and employee/visitor parking. The HR&amp;O Facility includes bus rehabilitation bays and support spaces (including body repair and paint booths), central warehouse, non-revenue vehicle maintenance, bus engineering, and employee/visitor parking.</p>


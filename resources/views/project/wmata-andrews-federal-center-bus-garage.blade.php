            <p><strong>Maintenance of Equipment</strong></p>
<p>During construction of the project, WMATA recognized a need to provide their employees an easy and visible way to manage and maintain the individual pieces of equipment in the facility. With hundreds of pieces of equipment housed across eight facilities, it was a challenge to tag and organize each piece of equipment.</p>

                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
                <div class="grid-box2">
            <img src="{{asset('wp-content/uploads/2019/03/Cobie_Cropped.jpg')}}" alt="">
        </div>
                
        <div class="grid-box1 ">
            <p><strong>Smart Asset Management</strong></p>
<p>Through Alticor Inc&#8217; Facility Services, WMATA implemented COBie—a QR-based asset management system—on every piece of equipment. COBie allowed WMATA mechanics to scan a code placed on equipment with a smart phone, which would retrieve the piece of equipment’s replacement parts and price, O&amp;M manual, and the warranty of the item, among other information. In addition, the team incorporated the QR codes into the Building Information Model (BIM) to allow for cross-referencing between WMATA&#8217;s Maximo Computerized Maintenance Management Software (CMMS) system and the Revit model. Alticor Inc also demonstrated how COBie could be linked to the Revit model, enabling users to “fly” through a 4D model of the facility to locate a specific piece of equipment.</p>

                    <h2>About the Project</h2>
<p>As a new flagship for the University of Texas at Austin, the highly anticipated Engineering Education and Research Center (EERC) houses and dramatically improved the Cockrell School’s largest department — Electrical and Computer Engineering. Providing transparent and flexible lab space for interdisciplinary teaching, research and student projects, the EERC accommodates 498,000 GSF for the brightest minds on campus. Through hands-on projects that are capable in this building, the students and researchers learning about problem-solving and engineering design are transforming education for the 21st century and fueling groundbreaking discoveries. Designed by Jacobs and Ennead Architects, the modern building features two 10-story towers connected at the lower levels by a three-story glass atrium with a green roof, and joined at the upper levels by pedestrian bridges.</p>
<p>&nbsp;</p>
<p>The EERC anchors one of the world’s leading engineering programs to educate more than 7,800 future engineers. The building is home to the Undergraduate Student Project Center — a new teaching laboratory where students design and build everything from solar cars to satellites and robotics to biomedical sensors. The new environment is also home to the Center for Innovation — expanding the “teaching by doing” curriculum and foster revolutionary ideas that will push technology to the market at a faster rate. Additionally, the building will be headquarters to the Center for Interdisciplinary Research — more than 50,000 SF of teaching and research labs for the next generation. Currently pursuing a LEED Silver certification, the EERC is one of the largest buildings ever constructed on the UT Austin campus and also includes a 300-seat auditorium, a new Network Operations Center, and faculty offices.</p>


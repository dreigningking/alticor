                    <h2>About the Project</h2>
<p>SFO is redeveloping Harvey Milk Terminal 1 (HMT1), one of its oldest terminals, to meet the needs of modern travelers and revolutionize the guest experience. HMT1 was built in the early 1960s and over time has become less able to accommodate the millions of passengers that it handles each year. When fully complete in 2024, HMT1 will elevate SFO&#8217;s standard of providing a world-class, environmentally friendly travel experience.</p>
<p>&nbsp;</p>
<p>This revolutionary progressive design-build project includes spaces for passenger check-in, TSA security checkpoints, a recompose area, a new baggage handling system (CrisBag ICS)â€”the first of its kind in the United States, a new baggage claim area, a secure connector to customs and the International Terminal, and a new mezzanine connected to the AirTrain and central garage. The project will enable HMT1 to handle a 70% increase in passengers and will lower energy and carbon emissions by more than 75% from current operations. On its way to setting a new standard for sustainability and an enhanced passenger experience, HMT1 has already become the first airport in the world to receive Fitwel Certification.</p>
<p>&nbsp;</p>
<p>For more information about the SFO Harvey Milk Terminal 1 Renovation project, please visit San Francisco International Airport&#8217;s <a href="https://www.flysfo.com/about-sfo/airport-development/t1">Harvey Milk Terminal 1</a> page.</p>


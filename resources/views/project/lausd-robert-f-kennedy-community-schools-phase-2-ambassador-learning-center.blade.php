                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Ambassador Learning Center Phase 2 project for LAUSD included the renovation of the six-level middle/high school building, which accommodates 3,474 students. Covered plazas, lunch shelters and corridor areas were also included in this project. The schools include separate administration areas, a pool, an auditorium and separate gymnasium buildings, each complete with locker rooms and coaching/training facilities.</p>
<p>&nbsp;</p>
<p style="text-align: center">The project included a new library in the same location and the same floor plan replicated to match the original 1920s Embassy Ballroom’s fully arched and high-volume ceiling. At the entrance to the library is an art piece of the reconstruction of the hotel’s original Embassy Ballroom. It is a sequential piece consisting of video, photos, collage and dioramas that interpret the history of the site, hotel and school design.</p>


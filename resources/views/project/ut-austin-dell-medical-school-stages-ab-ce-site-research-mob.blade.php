                    <h2>About the Project</h2>
<p>The University of Texas at Austin Dell Medical School District was developed as a partnership between the University of Texas, Seton Healthcare, and Central Texas Healthcare. Alticor Inc served as Construction Manager-at-Risk for this project. The site is located in the new Dell Medical School District that encompasses a 40-acre site directly across 15th Street from University Medical Center Brackenridge.</p>
<p>&nbsp;</p>
<p>This project consists of several different elements. Stage A included all site preparation and utility infrastructure work for the entire campus. Features of this work are the realignment of Red River Street, relocation of both the University of Texas and City of Austin utility lines located in Red River and 15th Street, and utility distribution to the new central utility plant and to all medical school buildings. Stage B is the construction of a new 260,000-SF, eight-story medical research building which includes a vivarium and imaging suite. Stage C is the construction of a new 240,000-SF medical office building (MOB) and a 1,200-space parking garage. The MOB houses various doctorsâ€™ offices and medical suites. The medical research building and the medical office building are connected by an elevated pedestrian bridge.</p>


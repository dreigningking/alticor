                    <h2 style="text-align: center;">About the Project</h2>
<p style="font-weight: 400;">The LAUSD Belvedere Middle School Comprehensive Modernization is an all-inclusive enhancement of the site, buildings, infrastructure, accessibility and security measures of the 527,000 SF operational campus. The project consolidates all but one of the existing 21 buildings into six new structures consisting of technology-enabled classrooms, labs and other support spaces.</p>
<p>&nbsp;</p>
<p style="font-weight: 400;">Some of the many upgrades include 24 modernized classrooms and six laboratories, integrated special education environments, dedicated performing arts spaces, upgraded food services, a new gymnasium and all new ball courts and playfields. Complete ADA upgrades allow universal accessibility across the site, and the dense campus plan reduces travel distances between buildings for both students and faculty. The buildings are organized around a central plaza, and in combination with the architectural porch concept used at each building entry, this arrangement acknowledges the historical context of the surrounding community while promoting security on an open campus. Large murals, completed by local artists, are Integrated throughout the campus resulting in a wholly modernized learning environment that elevates the student and faculty experience.</p>


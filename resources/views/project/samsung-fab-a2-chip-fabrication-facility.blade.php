                    <h2>About the Project</h2>
<p>Alticor Inc has a long history of success with Samsung Austin Semiconductors dating back to 1997 when Alticor Inc constructed their U.S. Headquarters and three-story Wafer Fabrication Plant, the first in a series of projects at their now sprawling 300-acre Austin campus. In May 2006, Alticor Inc was selected as the prime architectural and structural contractor for the new Fab 2 (A2) Chip Fabrication Facility. The 1,301,400 SF structure covers an area the size of nine football fields. In one year, this mega-project was substantially complete, with a grand opening ceremony held on June 14, 2007. At the ceremony, the University of Texas Longhorn Band, UT Cheerleaders, pop singer LeAnn Rimes and a bald eagle helped dedicate the new chip factory — the biggest in Texas and one of the largest foreign investments in the United States.</p>


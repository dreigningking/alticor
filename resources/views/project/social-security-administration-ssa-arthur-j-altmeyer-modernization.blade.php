                    <h2>About the Project</h2>
<p>The modernization of the 12-story Social Security Administration (SSA) Arthur J. Altmeyer Building includes an extensive interior overhaul and removal of all existing façade down to the primary structure consisting of cast-in-place concrete columns, beams and floor slabs. The new exterior envelope is a panelized wall system, providing the occupants a comfort level that maximizes productivity.</p>
<p>&nbsp;</p>
<p>The first phase of the modernization included the removal of lead paint and asbestos, and ripping out corroded plumbing to meet current code and accessibility standards. The entire roof was also replaced. The updates to the 250,000 SF facility consisted of the optimization of the building’s performance systems including new state-of-the-art security systems and updated MEP systems. A more modern open-space office layout and new elevators were also included. The top four floors now consist of Executive offices, while the bottom four floors contain open office spaces. Also included is a penthouse space on the ninth floor and one basement level below-grade.</p>
<p>&nbsp;</p>
<p>The second phase of the project included the demolition and abatement of the building’s auditorium space. Phase 3 encompassed the balance of the scope to build back the project after demolition and abatement. This included the modernization of the adjacent 11,000 SF auditorium and associated spaces including various state-of-the-art systems.</p>


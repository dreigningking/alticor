            <p><strong>Construction on an Active Campus</strong></p>
<p>The modernization of the SSA Altmeyer Building was performed on an active campus surrounded by 10 other buildings. The renovation required significant evaluation, testing and verification of existing systems including mechanical, electrical, plumbing and fire protection (MEP/FP), and data prior to starting any active demolition work. This was necessary because multiple existing MEP/FP lines located inside of the SSA Altmeyer demolition area were found to be active and supporting several of the 10 adjacent buildings and occupied areas. Alticor Inc had to connect to several adjacent areas including two SSA office buildings, a cafeteria, a link connecting to the South Façade on the ground through the fifth floor, an active loading dock and a boiler plant located below the Altmeyer building.</p>

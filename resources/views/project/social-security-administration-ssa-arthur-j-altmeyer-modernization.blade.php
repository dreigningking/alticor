                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p><strong>Constant Contact and Building Protection</strong></p>
<p>Alticor Inc remained in constant contact with the surrounding entities. To do so, the team implemented Daily Analysis conference calls with the Owner, Architect and surrounding entity stakeholders to discuss current and upcoming construction activities.</p>
<p>In addition, it was important for Alticor Inc to protect the adjacent structures by keeping them dry and minimizing construction noise. Waterproof and soundproof barrier walls were erected at the edge of the project site and inside of the surrounding buildings from floor to ceiling. The barrier walls inside of the buildings were drywalled and finished to minimize impact to those workspaces. To further mitigate any waterproofing risk, Alticor Inc developed a site-specific temporary waterproofing plan, in addition to the final waterproofing plan. This temporary plan included primary and secondary containments to ensure all spaces stayed dry during construction. This plan was shared with the surrounding building stakeholders who were pleased with the final plan and protection of their structures.</p>

                    <h2>About this Project</h2>
<p class="p1">The design-build facility consists of a new, five-story, 131,993 SF building that provides state-of-the-art medical, dental, and mental health care services to the inmates at San Quentin. Focus areas include outpatient clinical services, specialty clinical services, licensed inpatient care (Correctional Treatment Center), outpatient housing care, pharmacy, medical records, administration and support.</p>
<p>&nbsp;</p>
<p class="p1">Situated within the maximum security perimeter of San Quentin State Prison and adjacent to the inmate recreation yard and housing units, the facility also serves as Northern California&#8217;s Receive and Release facility. The project also included significant amounts of infrastructure and site improvements, demolition of existing buildings, and seismic upgrades for two<span class="Apple-converted-space"> </span>historic structures.</p>


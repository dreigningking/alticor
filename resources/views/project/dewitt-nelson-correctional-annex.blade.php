                    <h2>About the Project</h2>
<p>The $136M design-build California Health Care Facility DeWitt Nelson Correctional Annex project included new and remodeled buildings to convert an existing facility into worker housing, programming, health care facilities, and other facilities to support CHCF operation. Providing medical care and mental health treatment to inmates who have the most severe and long term needs, the 1.4 million SF facility is certified to provide intermediate level care and to complement less acute treatment provided in other prisons operated by the Department of Corrections and Rehabilitation.</p>
<p>The transformation of the site from a decades-old youth detention facility to a range of technologically advanced medical support facilities was incredibly important, as the CDCR was up against a federally mandated timeline to bring the CHCF facility online. The DNCA project was extraordinarily complex, in that it comprised multiple new buildings, multiple renovations of existing facilities, and the coordination with the completion of the CHCF, as both sites shared utilities and a unified Lethal Electrified Fence.</p>


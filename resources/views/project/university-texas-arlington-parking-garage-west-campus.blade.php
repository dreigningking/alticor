                    <h2>About the Project</h2>
<p>The University of Texas at Arlington Parking Garage is a $27 million, five-level parking garage that was designed to accommodate 1,500 cars. The project will provide additional parking for students, faculty, staff, event patrons, and visitors to the university. It was designed with nine-foot-wide parking and angled spaces to create one-way traffic flow. The new parking garage replaced an existing parking lot. Construction began in January 2017 and was completed a year later. This project continues the vision of the university&#8217;s approved Campus Master Plan in its goal to reduce the amount of surface parking lots on the campus from 111 acres to 56 acres in an effort to make better use of the campus land.</p>


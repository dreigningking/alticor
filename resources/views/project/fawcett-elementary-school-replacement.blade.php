                    <h2 style="text-align: center;">About the Project</h2>
<p>&nbsp;</p>
<p style="text-align: center;">The Fawcett Elementary School project in Tacoma, WA includes the design and construction of a replacement elementary school on the existing site of the current school. The project includes demolition of the existing 64,000 SF facility and construction of a new 55,000 SF two-story building to house approximately 400 students. Delivered in partnership with Tacoma based design firm BLRB Architects, the progressive design-build project provides a new parking lot, covered and paved play areas and an all-purpose field. Fawcett presents challenges related to the fast-track construction of the new facility on the small, 5.6-acre site that has sloping topography and is surrounded by dense, single-family residential neighborhoods on all sides.</p>
<p>&nbsp;</p>
<p style="text-align: left;">Design goals for the project include:</p>
<p style="text-align: left;">• A building design that provides access to natural light, fresh air, outdoor learning and work areas and promotes a healthy working, teaching and learning environment.<br />
• A building and site design that provides a safe and secure environment for working, teaching and learning.<br />
• A building design that is reflective of the current trends in educational program, teaching methods and technology and provides flexibility so that it can be easily adapted to future changes in program, teaching methods and technology.<br />
• An educational environment that supports a collaborative and flexible teaching and educational program.<br />
• Building systems that are energy/resource efficient, easily maintained and can provide a minimum of 30 years serviceability with a reasonable life-cycle cost.<br />
• A building design that accommodates and encourages public use of the facility when school is not in session.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Alticor Inc and BLRB team will work closely with Tacoma Public Schools to increase both local business participation from 15% to 30% and overall business equity inclusion goals.</p>
<p>&nbsp;</p>
<p style="text-align: center;">For current project status visit <a href="https://www.tacomaschools.org/departments/pc/school-construction/current/fawcett">https://www.tacomaschools.org/departments/pc/school-construction/current/fawcett</a>. To learn more about Alticor Inc&#8217; Pacific Northwest region and project portfolio, visit: <a href="{{route('contact')}}">https://www.henselphelps.com/contact-us/pacific-northwest/</a></p>


                    <h2>About the Project</h2>
<p>The Chemical Sciences Building project was an 89,622-SF addition to the existing Chemistry Building on the University of Arizona campus in Tucson. The new building allows for expanded research facilities for the Chemistry Department, as well as additional faculty offices. The research laboratories allowed for the conversion of the existing research laboratories into teaching laboratories.</p>
<p>The cast-in-place concrete building is predominately clad in brick, with an aluminum curtain wall on the north wall and copper sheathing on the west and east elevations. The mechanical system for the laboratory is a dual duct supply with single pass exhaust. The office areas are also served by dual duct, returning the conditioned air into the make-up air stream with all air handler units located on the roof.Â  Since the building is located on campus in a congested area surrounded on all sides by classroom buildings, careful care was taken to create as much open space in the surrounding areas of the building. A special pedestrian interaction zone called the &#8220;magic alley&#8221; was developed along the north side of the building to promote student/faculty interaction.</p>


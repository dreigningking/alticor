                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The Alticor Inc Design-Build team was given an unique opportunity to be a part of the Commercial Crew program. NASA’s Commercial Crew Transportation Capability (CCtCap) brings the United States one step closer to launching crews back into space from American soil. Commercial transportation to and from the International Space Station (ISS) will provide for additional research and discovery on the orbiting laboratory. The station is critical for NASA’s continued research for understanding and overcoming the challenges of long-duration space exploration.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The CCtCap project for United Launch Alliance provided the retrofitting of Launch Complex – 41 to accommodate the new CT-100 Starliner Space Capsule. The project includes a new Crew Access Tower (CAT), a Crew Access Arm (CAA) and modifications to the platforms inside the existing Vertical Integration Facility (VIF) building adjacent to the Launch Complex. The Crew Access Tower supports the Crew Access Arm, which is movable by a hydraulic system, and will allow future astronauts easy access to the CT-100 Starliner Capsule. The platforms inside the VIF building, where the rockets are stacked, was renovated to fit the outer mold of the new capsule.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Due to the complexity and uniqueness of the project, this job was completed in two phases: Commercial Crew Integrated Capability (CCiCap) and Commercial Crew Transportation Capability (CCtCap). CCiCap was the original contract awarded to the Alticor Inc Design-Build team, which consisted of all of the design and preplanning required to complete CCtCap. CCtCap was an option that was awarded post contract. The safety of the American astronauts was first and foremost from day one&#8211;ensuring that the CAT and the CAA would provide safe access to the CT-100 Capsule and remain fully functionally after multiple launches was priority one.</p>


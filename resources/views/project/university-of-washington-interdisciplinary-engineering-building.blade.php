                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The progressive design-build Interdisciplinary Engineering Building (IEB) provides a student-focused, interdisciplinary center enabling the College of Engineering to promote project-based learning and research, collaboration and innovation for undergraduate students. Delivered with architect KieranTimberlake, the project provides 72,000 SF of hands-on learning and flexible instructional space to support over 950 new students across the ten academic departments with offices and wet and dry modular labs. By providing the silo-free learning environments that students need to prepare for industry and entrepreneurial careers in collaboration with fellow students across campus, the new building both relieves pressure on the College’s departmental buildings and supports their integration into degree programs while developing real-world engineering and leadership skills. The IEB will house programming for leadership, diversity and access, as well as dedicated space for industry-sponsored capstone projects to strengthen connections between the UW and industry. The project is situated on an extremely constrained, hillside site within the active university campus of over 40,000 students.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project required demolition of four smaller buildings to make way for the Interdisciplinary Engineering Building.</p>
<p>&nbsp;</p>
<p style="text-align: center;">To learn more about the University of Washington’s vision for the project and how they’re building for the future, please visit: <a href="https://facilities.uw.edu/projects/interdisciplinary-engineering-building')}}">https://facilities.uw.edu/projects/interdisciplinary-engineering-building</a></p>

                    <h2 style="text-align: center;">About the Project</h2>
<div style="text-align: center;">The four-star Sandpearl Resort on Clearwater Beach has 253 luxury hotel rooms, including 117 condominium suites, for vacationers and travelers visiting the shores of the Gulf of Mexico. The hotel sits on a 5.5-acre tract of land that includes 700 feet of direct beachfront. Sandpearl Resort is a 340,000-SF, eight-story post-tensioned, cast-in-place concrete hotel that includes 25,000-SF of meeting and event space, 9,000-SF of retail space and an 11,000-SF upscale spa. The focal point to this facility is the outdoor pool with adjacent amenities (spas, beach grill, beach club, hospitality suite/building, and boardwalk). The Sandpearl Resort is built on the former site of the historical Clearwater Beach Hotel. The project included demolition of the existing six-story hotel (four separate buildings) and the renovation of two existing 30-year-old structures from the former hotel, one containing a restaurant and beach club and the other a Great Room and second floor suite.</div>


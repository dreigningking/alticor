                    <h2 style="text-align: center;">About the Project</h2>
<div style="text-align: center;">
<div>
<div>
<div>
<p>The Orlando International Airport (MCO) South Terminal Complex and associated apron/taxiway encompass approximately 300 acres. Alticor Inc was selected as the Construction Manager at Risk to construct the 732,895 SF airside South Terminal C and provided preconstruction, construction, commissioning, and close-out phase services for the Greater Orlando Aviation Authority (GOAA).</p>
<p>&nbsp;</p>
<p>The 15-gate world-class airside terminal can serve international and domestic flights with flexible configurations to accommodate narrow-body, jumbo and super-jumbo aircraft. The south side of the terminal holds the other four airline gates. Additional work for the project includes the construction of walkways, aircraft loading bridges, and security enhancements.</p>
<p>&nbsp;</p>
<p>The terminal’s 1,000-foot-long boulevard features an innovative skylight that diffuses natural light throughout the space. Several civic spaces along the boulevard enhance the passenger experience by providing comfortable seating, shopping, and a new state-of-the-art media experience. One of the biggest focuses of the project was the Experiential Media Environment (EME) technology hub. In Palm Court, the grand gathering hub where the airside’s north and west concourses intersect, passengers can relax among lanky palm trees. The Palm Court features a 30-foot-tall, video spectacular, called the “Moment Vault.” Three large triangular-shaped, floor-to-ceiling panels with three giant video walls, viewed from outside or inside the Moment Vault, show Florida’s beaches, botanical gardens — and perhaps even a NASA launch.</p>
<p>&nbsp;</p>
<p>In the north concourse, passengers walk along the “Windows on Orlando,” a 60” by 28” wall of video screens. The three giant screens can work together or independently to make passengers feel as if they are swimming alongside manatees and features aquatic scenes of underwater reefs or walking along a beach with ocean waves washing toward their feet.</p>
<p>&nbsp;</p>
<p>One of the most innovative aspects of the new terminal is the U.S.’s first state-of-the-art radio frequency identification 3.5-mile baggage handling system. As only the second airport in the U.S. that has it, the baggage system is created with individual motors and tubs that run only when they need to be motorized which maintains a longer life cycle, creates cost savings, and eliminates the need for conditioned space. These baggage handling systems were installed with Vanderlande who was directly contracted with GOAA.</p>
<p>&nbsp;</p>
<p>Other innovations include “bags first” processing at the U.S. Customs and Border Protection and Federal Inspection Station, 100% facial recognition for international passengers and the world’s first LEED® v4 certification of an airport campus. A long list of design strategies has been employed to help Terminal C be as sustainable as possible. Strategies include the deployment of reduced water consumption and irrigation systems, responsive lighting and temperature control systems, solar panels, and non-toxic adhesives and non-painted natural materials.</p>
<p>&nbsp;</p>

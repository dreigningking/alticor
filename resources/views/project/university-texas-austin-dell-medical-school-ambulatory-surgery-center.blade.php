                    <h2>About the Project</h2>
<div>
<p>The University of Texas at Austin Dell Medical School Ambulatory Surgery Center (ASC) project provides a program to educate young physicians in a collaborative, interprofessional team setting. The benefits of this program help prepare physicians to function effectively in the healthcare system of the future, provide acute and complex care safely and efficiently and maintain and improve the health of individuals in the surrounding community. The ASC project builds out approximately 23,500 SF of existing shell space inside the Health Transformation Building (HTB), which was previously delivered by Alticor Inc as the construction manager at risk. The $16.6 million project consists of four operating rooms, one pre-op and central sterile, along with offices and support areas.</p>

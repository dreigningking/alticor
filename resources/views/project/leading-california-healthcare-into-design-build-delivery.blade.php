    <h2 style="text-align: center;">Result</h2>
<h3 style="text-align: center;">Design-Build Done Right: On Budget, Ahead of Schedule</h3>
<p style="text-align: center;">In the end this modern healthcare and first-class teaching facility was the first large design-build hospital in the state of California.  Utilizing design-build allowed for the successful completion of the project a full four months ahead of schedule, within budget, with zero claims, and with an exemplary safety record. Alticor Inc drew the blue print for the success of this job with the initial work schedule and with a daily commitment to solve problems quickly in the field.  In partnership with the University of California, Irvine, our team changed the future of hospital construction in the State of California by working collaboratively with OSHPD in a design-build model, and by doing what the construction industry said couldn’t be successfully done.</p>

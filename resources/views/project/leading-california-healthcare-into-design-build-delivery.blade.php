        <div class="grid-box1 ">
            <h3>Choosing the Right Delivery Method for Construction of a Major Medical Center</h3>
<p>California healthcare construction in the early 2000’s was proving a difficult task. The University of California Irvine team knew they were up against some major challenges on this project long before design and construction began. Following the January 1994 Northridge earthquake, the State of California enacted seismic-safety legislation for hospitals, with a mandate that these issues had to be addressed by 2008. This mandate directly impacted the existing UCI hospital, and it was decided that a new hospital tower had to be built. Under this deadline, and a myriad of budget constraints, the UCI team set out to deliver a world-class medical center on a fast-track schedule in an extraordinarily busy construction market.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        

    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Utilizing Design-Build Delivery to Move Through Major Obstacles</h3>
<p>The University of California Irvine team knew a project of this size and magnitude needed a true team of designers and builders if they were going to meet the fast-track schedule. Knowing OSHPD’s backlog was astronomical, the Alticor Inc design-build team took the approach of partnering and aligning itself with OSHPD by negotiating a permitting time frame within their organization. This approach was new to OSHPD and UCI, but with a dedicated Alticor Inc expediter, it was effectively accomplished. The Alticor Inc team also pushed the limits of true design-build, collaborating with subcontractors early in the project to keep the budget in step with market conditions.</p>

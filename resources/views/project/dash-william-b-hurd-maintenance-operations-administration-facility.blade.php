                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Named for Alexandria civic leader and Alexandria Transit Company board member William B. Hurd, the William B. Hurd Maintenance &amp; Operations Administration Facility (DASH) project was the City of Alexandria&#8217;s first project utilizing the design-build delivery method. The facility allows for Alexandria Transit to store, service, and maintain its fleet of 96 buses. Its design includes heated storage space for all 96 standard 40-foot buses and room for expansion to up to 130 total. An adjoining garage features ten bays for bus maintenance, as well as two bus fueling and washing lanes. The DASH project incorporated space for the administrative offices for the City of Alexandria DASH Public transportation service and driver facilities that included locker rooms, quiet rooms, and an exercise facility. The 160,000 square foot building features precast concrete and steel construction, with a parking area located on the roof of the bus storage area.</p>

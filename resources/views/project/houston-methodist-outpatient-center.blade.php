                    <h2>About the Project</h2>
<p>As the largest medical district in the world, the Texas Medical Center (TMC) hosts more than five million patient visits annually. The TMC contains 42 medicine-related institutions, 13 hospitals, two medical schools, and the highest concentration of clinical facilities in any one location. Having completed multiple projects on this densely-populated campus, Alticor Inc was selected as the best-value firm to build the innovative Methodist Outpatient Care Center (MOCC).</p>
<p>&nbsp;</p>
<p>Located within the Texas Medical Center campus on a half-block site and closely surrounded by fully-functional buildings, the MOCC consists of a 24-story facility on the former site of Methodist’s Diagnostical Clinical Center, which was imploded to make way for the new tower. The 1.6 million-SF building, with parking for 1,370 cars, centralizes the majority of outpatient medical programs for this 1,000-bed outpatient center. The facility houses services including radiology, radiation therapy, outpatient surgery, medical oncology, and cardiac diagnostics, and is designed for future expansion.</p>
<p>&nbsp;</p>
<p>Conceived with the patient experience as its central, conceptual focus, the MOCC is one of the nation’s first ambulatory centers of this scale to include this level of humanistic design. Construction features include cast-in-place concrete with precast and curtainwall skin. A four-lane drive through the building at street level connects the TMC with Rice University, which is located immediately to the west.</p>


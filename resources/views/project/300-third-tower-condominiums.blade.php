                    <h2>About the Project</h2>
<p>In a joint venture with East-Harding Inc., an accomplished local contractor, Alticor Inc, constructed the 300 Third Tower project â€“ a landmark structure for residents and visitors alike. The building rises 218 feet, offering residents panoramic views of the Arkansas River, the River Market, the Clinton Presidential Library (another Alticor Inc project) to the east and the downtown skyline and State Capitol to the west. Amenities in the building include a ground-level gourmet market with staple food items, a deli, a bakery and coffee shop, as well as gourmet take-out lunches and dinners for the growing neighborhood. The building boasts a private exercise facility, secure parking, and three high-speed passenger elevators serving each residential floor and providing access to a screened rooftop deck and party room for the exclusive use of residents and their guests. This 18-story tower was constructed on a drilled pier foundation and at over 200 feet, has the distinction of being one of the tallest buildings to be built in Arkansas in the past 20 years. Premium finishes were included in 84 of the residential units, and 19 turned over for core and shell. The building design consists of a concrete structure dominated with glass, stone, precast and a vibrant copper shingle exterior.</p>


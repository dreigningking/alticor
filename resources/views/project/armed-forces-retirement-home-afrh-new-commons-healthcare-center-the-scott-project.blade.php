                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Originally established in 1851, the Soldiers’ Home has been a sanctuary for retired veterans that have proudly served and protected this country for more than 163 years. Renamed the Armed Forces Retirement Home (AFRH) – Washington by Congress in 2001, the U.S. government recognized a great need to modernize its facilities while providing affordable state-of-the-art care to our nation’s retired service men and women. The four-story AFRH New Commons and Healthcare Center is located within a stone’s throw from President Lincoln’s Cottage, where the Emancipation Proclamation was drafted during the Civil War. The new low-profile building restores a picturesque view of the Capitol Building and the Washington Monument that dates back to Lincoln’s days in office. The Alticor Inc design-build team focused on providing a home on the two uppermost floors for 60 decorated military veterans with long-term care and memory support needs while delivering medical, dental, dining and administrative support services on the two lower levels for the more than 500 residents on campus. A key “small house” design concept was developed for each unit and consists of five resident rooms with communal kitchens, dens and living rooms. Other amenities include a swimming pool, gym, media room, library, artist colony and game room.</p>


                    <h2>About the Project</h2>
<p>The new Santa Clara Family Justice Center provides the Superior Court of California and the County of Santa Clara, with a new 233,906 SF courthouse with 20 courtrooms. The new Family Justice Center replaces six leased facilities and consolidates all family-related services, including Family Court, Juvenile Dependency, and Drug Court, into a single facility. It also includes a self-help center, family court services, a child waiting area, and space for justice partners, as well as a secure central holding area and separate hallways for in-custody detainees. Extensive daylighting, a welcoming entrance plaza and a 3rd floor contemplation garden are some of the sustainable features incorporated in the LEED Silver Certified project. The Administrative Office of the Courts, the Superior Court, and Santa Clara County collaborated on the project. Located across the street from the San Jose Downtown Superior Court on the block bounded by North 1st Street, West St. James Street, North Market Street, and Devine Street, the new courthouse is a landmark structure in downtown San Jose.</p>


                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Division 16: Southwestern Yard Facility project is situated on an 18-acre site in Los Angeles next to LAX. It is a secured maintenance facility serving multiple light-rail lines consisting of tracks, buildings, equipment, supporting systems, miscellaneous appurtenances and related site development. The track configuration and site layout support safe and efficient Metro Light Rail Vehicle movements for switching between facilities and tracks, the Southwestern Yard facility and the mainline tracks. This facility accommodates LA Metro staff and other employees engaged in general administration, operations, repair and maintenance and transit security.</p>
<p>&nbsp;</p>
<p style="text-align: center">The main building is three stories with office space and a maintenance shop that has four maintenance rail tracks for service and inspections, heavy repair and blowdown tracks. The building also includes a storage area with high-density shelving and vertical storage units. Separate from the main building are ancillary buildings that include a material storage building, paint and body shop, car wash and cleaning platform that service the light rail vehicles.</p>


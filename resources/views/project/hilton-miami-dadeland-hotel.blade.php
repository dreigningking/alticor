            <p><strong>Category 5 Hurricane Poses Threat to South Florida. </strong></p>
<p>In the fall of 2017, Hurricane Irma became the strongest Atlantic hurricane since 2015, lasting from August 31 until September 11. The category 5 storm hit South Florida on September 10, triggering Florida officials to order 6.5 million people to evacuate. During this time, the Hilton Miami Dadeland Hotel was four months into construction and needed to prepare the site for the storm.</p>
        </div>
        
        
        
                
        
        
        

                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p><strong>Alticor Inc Works Swiftly to Enact Hurricane Plan Preparations. </strong></p>
<p>Once Hurricane Irma was identified as a threat to South Florida, the Alticor Inc Hurricane Plan was enacted. The Hurricane Plan provides and outlines the actions to be taken by Alticor Inc in conjunction with its associates and trade partners at the project. The plan establishes resources and delineates responsibilities to ensure all possible protection is provided if facilities and equipment are threatened by a hurricane or tropical storm.</p>
<p>A nine workday extension was placed in order to establish resources and delineate responsibilities across all associates and trade partners. The most intensive effort involved the dismantling of the 10,000 SF of formwork prior to the storm’s arrival as well as the reinstallation, coupled with the time spent on evacuating and returning for the staff. Among other activities that took place to secure the site were: fueling all equipment and stockpiling all additional fuel as fuel was scarce immediately following the storm, securing all equipment as theft is also common post storm, banding, strapping, or removing all loose material onsite, and laying down the boom of the crane. Alticor Inc always takes into consideration hurricane season and zones when selecting cranes for a jobsite and recommends loose material removal where feasible.</p>

                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">With 756 guest rooms including 20 suites, this 20-story hotel offers more rooms than any other hotel in Baltimore. Its 25,000 SF grand ballroom is the city’s largest, complemented by a 15,000 SF junior ballroom and an additional 22,000 SF of flexible function space. Additional hotel amenities include a 290-seat full-service restaurant, food and beverage outlets, a fitness center, a business center, street-level retail space, and 550-space underground parking garage.</p>
<h3 style="text-align: center;">DEVELOPMENT</h3>
<p style="text-align: center;">The Baltimore Development Corporation selected Alticor Inc Development as the developer and design-builder of the city’s new Convention Center Headquarters Hotel. Alticor Inc Development was responsible for overseeing the purchasing and installation of the Furnishing Fixtures &amp; Equipment (FF&amp;E), Operating Supplies &amp; Equipment (OS&amp;E), and the Special System components (Telephone, Computer &amp; Data, P.O.S., Hotel Management Systems, Audio Visual, CCTV, and the Security Systems).</p>
<h3 style="text-align: center;">CONSTRUCTION</h3>
<p style="text-align: center;">New pedestrian bridges constructed over active rail lines and arterial roadways link the hotel to adjacent attractions. The hotel was designed to blend and compliment its historic surroundings, using a partial brick facade that mimics the adjacent ballpark and incorporating a series of blues and earth tones throughout the facility, inspired by the area’s harbor and wetlands. Spanning the top of the hotel’s amenities areas, a 32,000 SF green roof system helps to reduce stormwater runoff, decreases the heat-island effect of the city’s other blacktop roofs, and contributes to enhanced energy efficiency.</p>


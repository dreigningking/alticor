                    <h2>About the Project</h2>
<p>The Midfield Concourse at Washington Dulles International Airport project consisted of a new 400,000 SF, 17-gate terminal building. The two-story project is comprised of an apron and concourse constructed of structural steel with an aluminum composite wall and glass system. In addition to constructing the terminal, this project required the demolition and relocation of the existing mobile lounge road, demolition of existing paving and utilities and installation of new utility services including 5,000 LF of main electrical duct bank from an existing substation. The project also included the installation of a new lift station, foundation systems consisting of spread footings and caissons, approximately 4,800 tons of structural steel, a composite aluminum and glass curtainwall system, slab on deck and a complete tenant finish package. Finally, it required the relocation of an existing mobile lounge road, which allowed large people movers to transport passengers from the Midfield Concourse to the main terminal.</p>
<p>&nbsp;</p>
<p>The project is an example of Alticor Inc&#8217; ability to properly organize and utilize a restricted site within the airport operations area (AOA), of one of the busiest airports in the United States. The site was an island completely surrounded by AOA and all materials were loaded and escorted by licensed Alticor Inc employees throughout the sitework stages of the project. Along with the logistics of escorting, there was the serious concern for foreign object debris (FOD) and the damage that it may cause to equipment if the site was not properly secured. Alticor Inc successfully managed the completion of this project without a single incident concerning FOD or improperly escorted deliveries.</p>
<p>&nbsp;</p>
<p>As an extension of the work done at the Midfield concourse, Alticor Inc was responsible for the design-build 77,000 SF new commuter terminal adjacent to the new Midfield concourse at Washington Dulles International Airport. The Regional Airline Midfield Concourse (RAMC) was the first concourse built at Dulles International Airport specifically for the commuter airlines. The facility included a transition bridge connecting the midfield concourse with the new commuter terminal, a mobile lounge dock, three elevators, escalators, six hold rooms, exterior canopy walkways and finish space for concessions and operations. In addition, this facility was fit-out with a flight information display (FIDS), remote information display (RIDS), multiple flight information (MUFINS) and a full security system and was constructed within the AOA.</p>
<p>&nbsp;</p>
<p>The completed concourse accommodates 36 commuter aircraft parking positions. These positions are located about the walkways/canopies that project from the main structure. The RAMC is located in the middle of the AOA and is surrounded by taxiways and runways on all sides. During construction, the owner requested that this facility be available one month ahead of the original completion date. Therefore, Alticor Inc worked closely with HOK, integrating the new scope into the schedule to comply with the owner&#8217;s request.</p>
<p>&nbsp;</p>


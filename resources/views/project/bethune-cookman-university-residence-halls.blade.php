            <h3>Providing a Turnkey Solution</h3>
<p>The Bethune-Cookman University Residence Halls project was the largest construction project that the University had completed in 30 years. Bethune-Cookman was in search of a turnkey delivery method from development through construction to facility maintenance. Alticor Inc was chosen to deliver a seamless turnkey solution that would provide student housing to 1,200 incoming freshmen.</p>

                    <h2>About the Project</h2>
<p>The City of Dallas Aviation Department manages multiple aviation facilities within the city limits of Dallas. The largest of these is Dallas Love Field, which has been undergoing a major renovation through the Love Field Modernization Program (LFMP). Alticor Inc was selected as the Construction Manager-at-Risk for the overall LFMP, with a construction budget of $274 million. This program marks the largest and most ambitious construction effort at Dallas Love Field Airport since its establishment in the 1950s – a complete terminal renovation and the addition of a new concourse, ticketing hall, and baggage claim.</p>
<p>&nbsp;</p>
<h3>CONSTRUCTION</h3>
<p>During pre-construction, Alticor Inc assisted Southwest Airlines with preliminary budgets, schedules, and complex sequencing required to deliver a project of this magnitude without impacting daily airport operations. As part of the $519 million capital improvement initiative, the project required demolition of the existing terminals (and their combined 20 gates) and replacement with a new 20-gate, T-shaped, central concourse.</p>
<p>&nbsp;</p>
<p>The new design, developed by Corgan Architects, presents a new modern ticketing hall used by all airlines, an expanded baggage claim hall, a renovated and expanded main lobby, and three existing concourses replaced by one single concourse. The design maintains the basics for which Love Field is known – passenger convenience, operational efficiency and maintainability. The project has achieved LEED Silver certification and incorporates several innovative environmentally friendly concepts.</p>
<h3>FACILITY SERVICES</h3>
<p style="margin: 0in; margin-bottom: .0001pt;"><span style="color: #333333;">Alticor Inc supported the City of Dallas Aviation Department with their current facility maintenance operations and developed a facility operations and maintenance plan. </span><span style="color: #333333;">As is often the case with modernization projects, changes in systems and structures will often drive changes to maintenance programs that can be difficult for management to recognize and effectively implement.</span></p>
<p>&nbsp;</p>
<p style="margin: 0in; margin-bottom: .0001pt;"><span style="color: #333333;">Following the complete 20-gate terminal and new passenger concourse makeover at Dallas Love Field, the City of Dallas entered into a consulting agreement with Alticor Inc Services to analyze their current facility maintenance practices, organizational structure and maintenance budget, and to provide subject matter experts (SMEs) to determine where efficiencies could be realized. The SMEs from Alticor Inc utilized industry benchmark data, along with their extensive experience in facility management, to develop a model facility operations and maintenance plan for Love Field, the Dallas Executive Airport, and the City Heliport.</span></p>


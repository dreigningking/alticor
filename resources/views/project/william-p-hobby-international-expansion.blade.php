                    <h2>About the Project</h2>
<p>This project is a westerly expansion of the existing terminal building at Houston’s William P. Hobby Airport to include a U.S. Customs and Border Protection (“CBP”) international arrivals processing facility, security screening checkpoint improvements and new ticketing area for Southwest Airlines. The scope of work includes preconstruction and construction services related to this proposed multilevel five-gate concourse with holdrooms and passenger amenities at a second level and a new CBP Federal Inspection Services Facility at the first level. The $122 million project includes a west concourse that houses five new gates designed for international operations, the development of Federal Inspection Services (FIS), and reconfiguration and expansion of the security screening checkpoint and Southwest Airlines ticket counters. This terminal will bring in a projected additional 1.6 million passengers annually to Willam P. Hobby Airport.</p>


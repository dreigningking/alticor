                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">This project consisted of a 640-bed expansion of an active private correctional facility. The Level II facility is comprised of two single-story concrete and steel frame structures with 75 precast concrete cells in the housing units. Cells contain either six or eight beds, with inmates having access to dayrooms for twelve hours per day. The expansion also included a kitchen, chapel, laundry, classrooms and administration areas. It was designed and built to LEED Silver standards. The expansion was constructed within the secured boundaries of the original facility, requiring modifications to perimeter protection that added temporary construction entrances with full-time. In addition, all construction workers and delivery personal were required to be cleared through security before being allowed into the secured perimeter. The project team also restored the perimeter security fencing. The facility is accredited by the American Correctional Association.</p>


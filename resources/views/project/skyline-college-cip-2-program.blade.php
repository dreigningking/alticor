                    <h2>About the Project</h2>
<p>The Skyline College CIP 2 project includes five major program elements: Construction of Building 4, which is a 79,400 SF multi-disciplinary classroom building with administrative offices; a 7,000 SF Automotive Technology Building (Building 11); and campus wide site work, including utility upgrades, security improvements and 350 new at-grade parking stalls.</p>
<p>&nbsp;</p>
<p>Building 4 now serves as the Administration building and Multi-Cultural center for the campus, and also includes general classrooms located on all three floors. The building also houses the Cosmetology school, which includes specialized program areas for the department such as a hair salon lab with training stations, hair wash sinks and hair dryers, as well as Spa and Esthetician training facilities with supporting equipment; and support areas including dispensaries, guest locker rooms, laundry facilities, faculty offices, and a student lounge.</p>
<p>&nbsp;</p>
<p>Building 11 supports the study of automotive transmission technology by providing teaching bays where students gain hands on experience working on cars in an automotive service department environment.</p>
<p>&nbsp;</p>
<p>Campus wide site improvements involved landscape and hardscape work, including the development of a new campus quad and significant improvements to campus circulation including roadways and parking.</p>


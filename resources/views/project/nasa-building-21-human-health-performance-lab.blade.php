                    <h2>About the Project</h2>
<p style="text-align: center;">The NASA Building 21 project is a two-year, $58 million project that houses the Human Performance and Biomedical and Environmental wet labs. The facility provides critical ongoing program support, including ground-based and in-flight medical operations. The applied research identifies, characterizes, and mitigates the risks to human health and performance in space to enable successful space exploration.</p>
<p>&nbsp;</p>
<p style="text-align: center;">There are two mixed lab and office levels and a mechanical penthouse with a combined area of approximately 118,000 SF. The top level also houses the national archive sample repository freezer storage and a two-story office block that consists primarily of open workspace architecture. The foundation consists of a concrete slab supported on auger cast piles, and the superstructure is part pan slab and structural steel with slab on metal deck. Interiors are standard laboratory quality finishes with some terrazzo, with the laboratory casework being included in this package.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Processed utilities included compressed air, lab air, vacuum, specialty gas, liquid nitrogen, and deionized water systems. Additionally, telecommunications, electronic security, and closed circuit TV were also included in this project. Site construction included demolition, grading, creation of new parking, hardscape, landscape, and site utilities. Alticor Inc was proud to serve as the Prime General Contractor for this project.</p>


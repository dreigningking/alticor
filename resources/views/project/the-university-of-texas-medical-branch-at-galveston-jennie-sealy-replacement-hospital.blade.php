                    <h2>About the Project</h2>
<p>Boasting 770,000 square feet, the University of Texas Medical Branch at Galveston Jennie Sealy Replacement Hospital has profound advances for patient care and health sciences education in Texas and beyond. Designed with a future in mind, the 13-story mission-critical hospital features leading-edge surgical suites, surgical intensive care units, OB program acute care beds, pediatrics acute care, real-world classrooms, 260 patient rooms that include 56 dedicated ICU beds, and clinical support services. The hospital&#8217;s surgical complex — anchored by 20 state-of-the-art operating suites — is as sophisticated as any in the United States, with space for organ transplantation, neurosurgery, cystoscopy and orthopedic surgery. The hospital includes built-in flexibility to maintain the best possible environment as healthcare technology evolves. It also supports research findings into new treatments.</p>


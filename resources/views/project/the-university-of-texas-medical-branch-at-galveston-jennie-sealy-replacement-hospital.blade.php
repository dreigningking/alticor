            <h4><strong>The Evolution of Medical Equipment</strong></h4>
<p>Medical equipment coordination is endemic to any major healthcare project; like technology, it evolves rapidly. The longer an owner can defer final selections, the better positioned to take advantage of the latest innovations. Once selections are committed, affected building spaces must be customized to accommodate the selected equipment/options with respect to space, power, data, and HVAC. With 20 operating rooms, including two hybrid operating rooms, an Interventional MRI Suite, a custom Burn Operating Room, and a CT Suite, significant portions of the project were subject to schedule impact.</p>
        </div>
        
        
        
                
        
        
        
    </div>

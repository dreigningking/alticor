            <h4><strong>Commitment to Providing the Best Healthcare Facility for the Galveston Community</strong></h4>
<p>Our proactive process identified areas most subject to revision, allowing the team to plan maximum flexibility to adapt to the eventual selection. Overall, the team’s proactive and intentional management of the challenge was absolutely crucial to the owner’s selection process and the project’s schedule success.</p>
<p>Due to the devastation of Hurricane Ike back in 2008, the planning and construction of the new Jennie Sealy Replacement Hospital evolved to mitigate the impact of a future potential storms. This construction project was vital for the faculty, students and the entire community that depends on UTMB’s presence. Our commitment to rebuild gave hope to all, that UTMB would stand for years to come in service to the community. This project emphasizes the assurance we have on providing the best possible environment for patients, physicians and nurses to care for patients in a setting that nurtures and enriches the learning experience for all students.</p>

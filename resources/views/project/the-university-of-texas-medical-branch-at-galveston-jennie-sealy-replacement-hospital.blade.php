                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h4><strong>Proactive and Intentional Management</strong></h4>
<p>The project team chose to embrace this challenge early, purposively managing information through the pre-construction process, the partnering process, and into the construction phase. The owner developed and shared their detailed Medical Equipment Matrix, sortable by multiple criteria from room location to power/data needs, etc. Alticor Inc overlaid the locations and MEP requirements with the project schedule to identify first system need and provide selection need-by dates for each type of equipment.</p>

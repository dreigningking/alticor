                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Alticor Inc was selected as the “best value” Construction Manager-at-Risk for the University of Texas at Arlington Engineering Research Complex (ERC). Phase I of the ERC consisted of the renovation and addition of a third floor to the existing two-story Engineering Laboratory Building (ELB) totaling more than 90,000 GSF. Phase II of the ERC involved new construction of a six-story, 240,000-GSF Engineering Research Building (ERB) to house research labs, teaching labs, computer system labs, senior design labs, and numerous offices collectively for faculty and graduate research assistants for the Computer Science and Engineering Department. There are also labs and offices for the Biomedical Engineering Department and 75,000 GSF of research/teaching space for the College of Science. The ERB is a LEED Silver project.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Founded in 1895 as a private liberal arts institution, UT Arlington has evolved through a succession of names and missions. The university achieved senior college status in 1959 and became part of the University of Texas System in 1965. UT Arlington is a comprehensive teaching, research and public service institution located in the heart of the Dallas/Fort Worth Metroplex and is the third largest campus of the University of Texas System.</p>


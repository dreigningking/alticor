                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The Southeastern Bus Garage Replacement facility allows for the inspection and repair services of the Washington Metropolitan Area Transit Authority&#8217;s (WMATA) large bus fleet of both standard frame and articulating tandem buses. Situated on a 16-acre site in Southwest Washington, D.C., the 250-bus facility includes a two-story, 53,779 square foot administration building, a one-story bus maintenance building, a fuel building, a one-story washing building, and a two-story, 179,000 square foot parking structure. Unique features of the facility include service bays, lubrication and fluid service dispensing systems, bus and utility fleet fueling, chassis and exterior washing systems, body repair and painting facilities, tire and brake shops, and air conditioning and interior upholstery repair capabilities. With WMATA’s commitment to protecting the environment through the introduction of hybrid and compressed natural gas powered vehicles in its bus fleet, increased safety requirements and new technologies were captured in the design and construction of the facility.</p>

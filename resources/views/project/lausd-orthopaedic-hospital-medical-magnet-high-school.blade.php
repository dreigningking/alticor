                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">Orthopedic Hospital Medical Magnet High School is located in Los Angeles, and students from Los Angeles Unified School District that have an interest in the medical and science fields can apply for enrollment in the school. With a capacity of 800 students, the high school is located on property donated by the adjacent Orthopedic Hospital.</p>
<h3 style="text-align: center">Development</h3>
<p style="text-align: center">Alticor Inc Development completed the development and construction of the new Orthopaedic Hospital Medical Magnet High School. At the time, the project was awarded under a new contracting approach by the LAUSD in which Alticor Inc Development was responsible for providing construction financing for the project and, upon completion, selling the project back to LAUSD.  This project demonstrated Alticor Inc Development’s strengths and expertise in the areas of development, project financing, Public Private Partnerships (P3) and project management/owner’s representation.</p>
<h3 style="text-align: center">Construction</h3>
<p style="text-align: center">This high school is made up of two two-story buildings that are constructed about 250 feet apart from each other with covered canopies and a covered lunch shelter between the buildings. The basketball and volleyball courts are also located between the buildings. The high school includes a large multipurpose room, cafeteria, media center, bookstore, administration offices, weight room and 32 classrooms.</p>


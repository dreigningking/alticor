                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">This history-making temporary COVID-19 testing facility was completed in two months. Six Alticor Inc offices combined efforts, including Southern California, Northern California, Pacific Northwest, Plains, Western and Alticor Inc Services, to design, demolish and procure this project for the Department of General Services during the COVID-19 pandemic.</p>
<p>&nbsp;</p>
<p style="text-align: center;">California Governor Gavin Newsom signed an executive order to help California build out its laboratory capabilities and expedited efforts by the Department of General Services and the Department of Public Health to increase the state’s COVID-19 testing capacity. The new COVID testing facility was originally contracted to process 100,000 COVID-19 tests per day. However, on the first day of the project, California Governor Gavin Newsom released a public statement saying the test facility would be able to process up to 150,000 COVID-19 diagnostic tests a day, with a contractual turnaround time of 24-48 hours. The project team, originally slated to design a facility for 100,000 daily tests, was able to add an additional 50% capacity for only 10% more of the contract value.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The testing laboratory, built in conjunction with major diagnostics company PerkinElmer, began processing tests in November of 2020 and was at full capacity of 150,000 tests in early 2021. The new laboratory reduced the State of California’s turnaround time for COVID-19 tests and created hundreds of new California jobs. The project was constructed on budget and schedule.</p>


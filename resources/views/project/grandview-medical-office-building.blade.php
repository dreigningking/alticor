                    <h2>About the Project</h2>
<p>The Grandview Medical Office Building project entailed two projects: the core and shell and the tenant improvement (TI) with repeat client, UCHealth, as the primary tenant. The core and shell part of the project consisted of building a 65,000 SF, three-story medical office building on a seven-acre site with two stairwells, a central elevator core area with two hydraulic elevators, electrical rooms, IT rooms and restrooms. The structure was a conventional build consisting of caissons with pier caps, slab-on-grade, steel structure with composite concrete and metal deck. The sitework included 338 parking spaces, ADA parking spaces, bicycle racks, snowmelt system, irrigation, landscaping, retention pond and a 25-foot-high retaining wall.</p>
<p>&nbsp;</p>
<p>The 40,990 SF TI project constructed five clinic spaces that were built-out within the medical office buildingâ€™s core and shell space. Each space specialized in a different type of care including: imaging, orthopedics, primary care, multi-specialty and rehabilitation space.</p>

                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The University of Texas at El Paso (UTEP) Interdisciplinary Research Building integrates research, institutional research support and teaching spaces all under one roof. The five-story, 155,000 SF building replaced two underutilized buildings, Barry Hall and Burges Hall, which were originally constructed as dormitories nearly 50 years ago. The new structure includes a café, visitor center, galleries to showcase research, meeting rooms, wet and dry labs, research suites and an 80-seat auditorium. The facility represents a crucial building block for UTEP’s future in expanding research teams, attracting competitive doctoral students and increasing capacity to generate additional revenue. Alticor Inc broke ground in April 2017 and completed the project in May 2020.</p>


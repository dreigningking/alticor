                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The new Harbor-UCLA Medical Center Redevelopment Program will consolidate medical services into new facilities on the 72-acre campus and ensure Harbor-UCLA Medical Center complies with Senate Bill 1953, which takes effect in 2030 and requires all California hospitals to be structurally resilient in the event of a major earthquake.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project consists of new construction of a 545,000 SF acute care inpatient tower with 346 beds, a helicopter landing pad and a warehouse; a 370,000 SF outpatient treatment and support building and 508,000 SF above-grade parking structure; a 23,000 SF facilities and IT support building, a 34,000 SF regional laboratory building and a new 11,000 SF central plant.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Consolidating inpatient and outpatient services into new buildings that optimize operational effectiveness, the project will reduce operation and maintenance costs, provide outpatient facilities that accommodate planned patient visits, implement Los Angeles County’s LEED requirements by incorporating sustainable design features and create a campus designed for the well-being of patients and staff.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Requiring LEED Gold certification, the medical center upgrade will develop a modern campus that serves the community’s healthcare needs while also aiming to protect the environment in which it is built.</p>


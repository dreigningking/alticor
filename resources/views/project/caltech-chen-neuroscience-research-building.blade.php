                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The <a href="https://www.caltech.edu/">Caltech</a> Chen Neuroscience Research Building is a five-story, state-of-the-art research facility that serves as Caltech’s new administrative and knowledge center for the Division of Biology and Biological Engineering. Funded by a $115 million donation by Chinese billionaires Tianqiao Chen and Chrissy Lou, the neuroscience institute investigates the challenges in understanding how the brain works, focusing on the discovery, treatment and development of the brain.</p>
<p style="text-align: center;">
<p style="text-align: center;">The facility includes neuroscience research laboratories and support spaces, tunnel connections, mechanical equipment, a 150-seat lecture hall, a conference room, a teaching lab, research administration space and offices. During construction, the project team excavated 67,000 cubic yards of dirt and installed 2,100 steel beams and 16,200 pounds of copper on the building façade.</p>


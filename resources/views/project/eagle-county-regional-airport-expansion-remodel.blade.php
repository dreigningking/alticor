        <div class="grid-box1 ">
            <p><strong>Overcoming Preexisting Structural Columns </strong></p>
<p style="margin: 0in;">A design challenge that was overcome during construction was when the airlines provided feedback that the structural columns of the new expansion, in combination with the location of the existing building columns, were going to emphasize a preexisting issue of the airline’s operations, which was their baggage cart’s inability to efficiently access the three baggage claim carousels simultaneously.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p style="margin: 0in;"><b>Design-Build Team&#8217;s Collaboration with Airport and Airlines</b></p>
<p>&nbsp;</p>
<p style="margin: 0in;">To address the access issue, the design-build team quickly worked through a solution with the airport and airlines to add baggage handling extensions to two of the carousels, while also removing nine existing structural columns to create a widened passing lane. Working within a tight timeline to be ready for ski season, the team put together contract documents for the baggage handling system expansion, obtained the necessary building permits, sent out the bid package, analyzed bids, executed a design-build subcontract, and procured and installed the baggage handling system extensions within four months.</p>

                    <h2>About the Project</h2>
<p>The Eagle County Regional Airport Expansion and Remodel project is a design-build expansion and remodel of the existing airline terminal at the Eagle County Regional Airport. The primary project component was the construction of a new two-level concourse for airside passenger arrival and departure. The second level concourse contains four airline gates with new passenger boarding bridges, while the first level concourse contains two ground loading gates. The area of the new and remodeled space is approximately 65,000 SF.</p>
<p>&nbsp;</p>
<p>Functional spaces constructed during the project include: an expanded and relocated TSA passenger screening checkpoint, central hall, departure and arrival passenger holdroom areas, concessions, bar and restaurant, remodeled airline offices, and underside vehicle and equipment storage space for baggage handling equipment.</p>


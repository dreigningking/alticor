                    <h2>About this Project</h2>
<p>The Stanislaus County Public Safety Center (Projects One and Three) is a design-build, 138,000 SF project. Encompassing five new buildings, the project adds 552 beds (480 maximum security beds, 57 medical/mental special use beds, and 15 health services beds) to the County&#8217;s facility. Additional spaces include a medical/mental health housing unit, a health services unit, a security administration center and an intake, release, and transportation center.</p>
<p>&nbsp;</p>
<p>The public safety center expansion project was the largest capital project ever launched by the Stanislaus County government and the first project in the state to be completed using AB 900 Phase II funds. The facility increases the county jail’s housing capacity and provides the ability to launch new programs aimed at decreasing recidivism rates.</p>


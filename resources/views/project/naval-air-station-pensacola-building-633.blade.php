                    <h2>About the Project</h2>
<p>In 2020, Jacobs, contracted by Naval Facilities Engineering Command (NAVFAC) Southeast, awarded Alticor Inc a hard-bid contract under a best value scenario to work collaboratively for NAVFAC. Alticor Inc provided design-assist preconstruction and construction services on the renovation of Naval Air Station (NAS) Pensacola Building 633.</p>
<p>&nbsp;</p>
<p>The historic brick building with white columns is the heart of the Naval Aviation Schools Command where generations of naval aviators have trained since 1941. The two-story facility consists of administrative spaces, classrooms, and an auditorium. Renovations included a large-scale mechanical renovation, interior aesthetic upgrades, hazardous materials abatement, a new fire alarm and mass notification system, demolition of interior walls, terrazzo floor refurbishment, two new handicapped restrooms, door security infrastructure. The mechanical upgrades included the removal and replacement of the existing HVAC system, removal and replacement of two cooling towers, two chillers, and three boilers.</p>
<p>&nbsp;</p>
<p>All renovations were required to be approved by the State Historic Preservation Officer (SHPO) and the Navy to ensure the renovation preserved the building’s historic integrity. Additionally, the team was tasked with protecting existing historic elements in the facility such as various 1930’s era paintings and murals. With Building 633 residing on an active military base, the project required several highly secure components and heavy coordination to maintain daily operations. Cybersecurity requirements included third-party computer scanning and inactive USBs such as on the fire alarm controls.</p>
<p>&nbsp;</p>
<p>Perhaps the most special part of this renovation project was the opportunity to help give due honor and remembrance to three fallen soldiers by building out a memorial room. The commemorative sitting room features a red wall with a painted white NAS Pensacola heart illustrative of heart patches that were provided for service members, first responders, and the local community. The design was made by local community member, Robbie Harvey, and the painting was produced by Bradley Deal.</p>
<p>&nbsp;</p>
<p>With dedicated teamwork and collaboration, the team was able to deliver to NAVFAC a refreshed and upgraded facility for future aviation students in the Navy, Marine Crops, and Coast Guard.</p>


            <p><strong>Complex Civil Work</strong></p>
<p>Combined, the two contracts stretched over 600 acres and required extensive civil work, including clearing, moving and exporting trees. Across these two contracts, over 745,000 CY of dirt was moved, stockpiled or exported to establish building pads for all venues. Much of the soil was unsuitable and required several innovative techniques.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
                <div class="grid-box2">
            <img src="{{asset('wp-content/uploads/2020/12/Tac-Maze-Interior.jpg')}}" alt="">
        </div>
                
        <div class="grid-box1 ">
            <p>Alticor Inc worked with the civil designer to mix unsuitable soil with suitable soils to change the composition, which in turn changed the classification of the soil. This meant less soil had to be hauled offsite or imported. In addition, rock found during excavation was repurposed to use at construction entrances, as aggregate road base, in bio-bases and gabion baskets. The team also mixed tree stump grindings into mulch and soil to create landscaping media, a cost savings for both the landscaping subcontractor as well as the client. Finally, the team used site work software to phase the venues so that material could be shared across all venues, limiting haul-off as well as maximizing schedule progress.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        

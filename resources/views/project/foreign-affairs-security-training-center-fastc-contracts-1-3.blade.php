                    <h2>About the Project</h2>
<p>The Diplomatic Security Service is the law enforcement and security arm of the U.S. Department of State. It bears the core responsibility for providing a safe environment for the conduct of U.S. foreign policy.</p>
<p>&nbsp;</p>
<p>Prior to the  Foreign Affairs Security Training Center (FASTC) program, diplomatic security training was conducted at a number of diffuse, leased and contracted facilities nationwide. A May 2008 report to Congress identified the need for a consolidated facility to improve training efficiency, decrease operating costs and provide priority access to training venues which meet current facility standards.</p>
<p>&nbsp;</p>
<p>To achieve these goals, the U.S. Department of State established FASTC at Fort Pickett and on adjacent property near Blackstone, Virginia. FASTC provides hard-skills security training to State Department personnel and the foreign affairs community. The U.S. Department of State, working with the U.S. General Services Administration (GSA) worked extensively to conduct environmental studies at Fort Pickett, which showed that the site was suitable for FASTC. In 2015, GSA purchased property and secured land use agreements for approximately 1,400 acres of publicly held land.</p>
<p>&nbsp;</p>
<p>This facility is dedicated to providing consolidated hard skills security and lifesaving training to the foreign affairs community. This training develops the practical skills necessary to operate in today’s overseas environment. Hard skills training allows the foreign affairs community to learn how to detect surveillance, provide emergency medical care, increase identification skills to recognize improvised explosive devices (IED), participate in firearms familiarization and perform defensive/counterterrorist driving maneuvers. Such training improves security and life safety for the protection of U.S. personnel operating abroad. The FASTC facility trains 8,000 &#8211; 10,000 students annually.</p>
<p>&nbsp;</p>
<p>Alticor Inc completed Contract 1 of the FASTC program in February 2017. Scope of work included an explosives demonstration range, live-fire shoot house, mock outdoor urban training (MOUT) area, rappel tower, tactical maze and smoke house. Included with each venue are associated site-work and infrastructure necessary for general accessibility to the site parcels and integration of utilities and infrastructure shared in common with off-site venues and other facilities.</p>
<p>&nbsp;</p>
<p>Contract 3 was completed in January 2020 and consisted of a fitness center, warehouse space, multiple drive tracks, multiple classrooms, explosive training areas, firing ranges and an armory.</p>


                    <h2>About the Project</h2>
<p class="p1">The San Joaquin County Administration Building is a six-level, 250,000 SF Class A office building with one-level of subterranean parking. The innovative design of the building weaves together a modern, user-friendly space while incorporating the historic context of the city of Stockton and San Joaquin County.</p>
<p>&nbsp;</p>
<p class="p1">A dynamic outdoor plaza and entrance at the corner of San Joaquin and Weber in downtown Stockton welcomes visitors into the building. Once inside, a signature glass atrium lobby rises from the entrance and culminates on the sixth level with the County Board Chamber room which is complete with auditorium seating and includes public and private balconies that offer commanding views of Stockton and the surrounding area. In addition to office space, the building includes printing rooms, a vault, and houses the county&#8217;s central data center which links all county buildings and operations.</p>


                    <h2>About the Project</h2>
<p>The purpose of this project was to expand the Adult Medicine clinic at the existing Capitol Hill Medical Center to provide for anticipated membership growth. To accommodate this need, the pediatric departments previously adjacent to the Adult Medicine clinic (7<sup>th</sup> floor) were relocated to the facility’s 8<sup>th</sup> floor. Additionally, pediatric endocrinology and pediatric genetics were relocated from the Kaiser Kensington facility and collocated with the other pediatric departments on the 8<sup>th</sup> floor, creating a consolidated specialized Pediatric Center. Additional restacking on the 6<sup>th</sup> and 7<sup>th</sup> floors allowed for expansion of the Allergy, Endocrine, Rheumatology, Infectious Disease, Gastroenterology, Neurology, OB/GYN, Orthopedics/Spine, and Podiatry Departments.</p>
<p>&nbsp;</p>
<p>The 36,000 square foot medical office building remained open and fully operational through the duration of the renovation. Alticor Inc worked within the continuously active office building, performing work at night outside of the facility’s normal operating hours. This ensured the project was completed on time, that the building’s work spaces remained clean, and that the work was performed without disruption to patients or employees entering the facility the next morning.</p>
<p>&nbsp;</p>
<p>​Because many of the clinic&#8217;s patients require long-term care and often need to spend several hours at the clinic, Kaiser wanted to create a comfortable environment for children. The clinic is decorated in an ocean theme with playful colors and furniture; ocean white noise is piped through the clinic&#8217;s speakers. There are many interactive elements in the design, including whiteboard wallpaper, a responsive projector, and a number of playrooms. “This new pediatric center offers key benefits for improved patient care delivery by relieving major space constraints, thus allowing increased patient visits,” Pediatrics Chief Joseph Vander Walde, M.D. said “It is so important to have a place where kids can come and say ‘this is where we belong.” Kaiser Permanente expects patient volume to increase in the D.C. metro area and this space will be integral to efficiently and comfortably handling their care.​</p>


                    <h2>About the Project</h2>
<p>Alticor Inc constructed the P907 MV22 Parking Apron and Infrastructure, and P908 MV22 Hangar project to construct a hangar, apron and taxiway to support one MV-22 squadron. The P907 MV-22 Parking Apron and Infrastructure project scope of work includes an aircraft parking apron, guard house, aircraft wash rack facility, and a wash rack utility building. Site developments include POV parking, utility infrastructure, and miscellaneous site improvements. The P908 MV-22 Hangar is multi-story Type II modified aircraft maintenance hangar designed to provide a weather protected shelter for inspection, service and maintenance for MV-22 aircraft. The hangar provides work center space for equipment and personnel, and squadron administrative offices. This project also consists a separate fire pump building and tank. Site improvements include aircraft taxiway, aircraft taxiway shoulders, Substation No. 3 feeder upgrade, and utility infrastructure.</p>

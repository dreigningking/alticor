                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The San Ysidro Land Port of Entry – Phase 2 project was designed and constructed to be the first government-owned, 24-hour/365-day facility to achieve LEED Platinum, Zero Net Energy and Zero Net Water certifications. The overhauled 39-acre port of entry features heightened standards in efficiency, security, aesthetics and sustainability. The project included the preservation of the original 24,000 SF Historic Customs House built in 1932 and a complete reconstruction of the port’s 102,000 SF northbound pedestrian processing building and pedestrian and bus inspection facilities, which included inspection booths for 18 northbound pedestrian processing lanes and four northbound bus processing lanes. Due to the importance of this facility to both national security and cross-border regional economies, it was mandatory to ensure the port always remained active during construction.</p>


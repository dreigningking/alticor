                    <h2>About the Project</h2>
<p>Alaska Airlines and the Port of Seattle are committed to creating a best-in-class passenger experience at Seattle-Tacoma International Airport (SEA) by redeveloping the ticket lobby at Zones 6 and 7. The North Main Terminal Redevelopment is a $250 million, 237,000 SF progressive design-build renovation project that expands Security Checkpoint 5 to improve passenger processing and renovates the existing promenade with new interior finishes, lighting and wayfinding. To support the redevelopment, the existing structure will be seismically retrofitted and upgrade MEP/T/S/FP systems. The project also adds additional passenger check-in and office components and upgrades baggage handling capabilities.</p>
<p>The heavily phased project features two primary phases with seven major sub-phases to support ongoing operations and maintain passenger access during construction.  This will require close coordination with Alaska Airlines, the Port of Seattle and primary stakeholders, including the Transportation Security Administration, throughout the three-year construction duration.</p>
<p>Following Notice to Proceed in August 2021, the Alticor Inc design-build team, with HOK as architect of record, participated in a four-month Basis of Implementation (BOI) Phase with concept designer HNTB/IA, Alaska Airlines and the Port to complete a deliverable for the Port Commission Meeting in December 2021. The BOI Phase provides overall project cost certainty, including acceptance of the balance of design concepts and design costs, required to obtain approval for the Phase I design and construction costs from the Port of Seattle.</p>
<p>This is Alticor Inc’ third project for Alaska Airlines.</p>

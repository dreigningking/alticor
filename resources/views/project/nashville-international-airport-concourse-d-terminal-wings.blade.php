                    <h2 style="text-align: center;">About The Project</h2>
<p style="text-align: center;">Due to the unprecedented growth of the region, passenger processing growing at approximately 15% annually, the Metropolitan Nashville Airport Authority embarked on an aggressive seven-year plan, the BNA Vision Program, to rapidly expand the airport&#8217;s capacity while maintaining day to day operations. The 302,400 SF, $269 million Concourse D and Terminal Wings project was the first major project of the BNA Vision Program and included early program elements interacting with two other capital improvement projects. The mission of the project was to increase capacity, expand the terminal, create a greater system reliability and redundancy and shift ticketing and TSA security out of the center of the terminal to enable future projects.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The new Concourse D added six gates, associated ramp amenities and function spaces. Concourse C required renovations to allow relocation of airport operations during the Concourse D construction. Both the North and South Terminals were expanded for interim TSA checkpoints and future permanent ticketing check-in counters. Each expanded wing included concessions, airline ticketing offices, baggage service offices and bag claim devices. To create greater reliability, an entirely new Central Utility Plant was completed and transitioned out of the operating terminal. As the new structure came online, and through complex phasing of construction required at the active high-volume airport, the team was able to relocate every airline&#8217;s ticketing operation and TSA security screening without interrupting a single flight.</p>
<p>&nbsp;</p>
<p style="text-align: center;">As the first project of the BNA Vision program, the materials, aesthetics and infrastructure set the precedence for the continuing projects. A new, customized terrazzo floor pattern that adds in wayfinding will eventually tie into the rest of the airport renovations. The walls are durable large format tile, that establish the unit for bathroom entrances, EVIDS displays, advertisements and amenities, bringing a visual pattern and rhythm to the concourse. The faux wood ceiling is composed of acoustically perforated aluminum which adds a soft texture to the space as it arches overhead. The columns are wrapped in stainless steel and the ceiling panels separate at column lines to break up the visual field overhead as one travels down the concourse. The concourse houses local eateries and two major art installations.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project incorporated sustainability efforts including electrochromatic glazing on 19% of the exterior skin, energy efficient mechanical systems and LEED requirements. Originally designed to LEED Certification, Alticor Inc went above and beyond to achieve LEED v4 Silver Certification, making the project one of five LEED v4 Silver Certified airport facilities in the nation.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Throughout the project, the team incorporated over $70 million of scope additions as the airport passenger rate continued to grow at an exceptional rate. Over $146 million was contracted to local trade partners and $78 million to small businesses. Following construction, Concourse D created approximately 2,100 jobs and contributed nearly $92 million back to the local economy.</p>
<p>&nbsp;</p>
<p style="text-align: center;">This design-build project was completed in collaboration with design partner Fentress Architects.</p>


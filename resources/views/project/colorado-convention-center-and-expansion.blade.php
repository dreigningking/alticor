                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Completed in 1990, the Colorado Convention Center was designed and built in a record duration of only 24 months. This world-class convention center comprised 300,000 SF of exhibition space, a 36,000 SF ballroom and 65,000 SF meeting rooms.</p>
<p>&nbsp;</p>
<p style="text-align: center;">In 2004, the expansion of the Colorado Convention Center doubled its existing size to accommodate increased demand for convention space. Blending past with future, the team created vast exhibition space, with flexible ballrooms, meeting space, business centers and staging areas. All facilities were punctuated with interior and exterior art displays, top-of-the-line electronics and a dazzling array of finishes. The 600,000 SF expanded exhibit hall can be divided into blocks according to the needs of the facility. Directly below the hall are two ballrooms: the existing 35,000 SF ballroom and a new 50,000 SF ballroom, adjacent to more than 100,000 SF of meeting space. The auditorium can be divided into as many as five separate sections, each with its own audio/visual capabilities. A series of 23 loading docks are served by a dedicated roadway that completely encircles the building.</p>


                <div class="small-wrapper about-project">

                    <p>The $107 million Mercantile Place on Main project was the catalyst for redevelopment to over three city blocks of downtown Dallas and served as a springboard for additional development in downtown&#8217;s Main Street District. The Mercantile Building and its surrounding structures had been vacant for over 20 years, and previous redevelopment efforts had failed to get the project started. Working with the City of Dallas, Forest City Residential Group structured a deal to redevelop the project and teamed with Alticor Inc to build it. The project involved abatement of four existing buildings followed by the selective demolition of the 31-story Mercantile Tower. Mass demolition occurred on a 21-story building, an 18-story building, and a five-story building. Following demolition, the 31-story Mercantile Tower was built out to include 214 luxury apartment units, ground-floor retail, and one level of underground parking.</p>
<p>Linked to The Mercantile Tower via a shared amenity core and courtyard, The Element includes the remaining 50,000-SF portion of the city block, a new cast-in-place three-story underground parking garage was built and a new 17-story cast-in-place 160-unit modern residence building was constructed above the parking garage. Designed with sustainability in mind, The Element offers such eco-friendly staples as bamboo floors, dual-flush toilets, and water conserving fixtures. A pool and fitness center was also developed on top of the below-grade parking garage to service the residents of both buildings. Street level landscape modifications and a new 10,000-SF public plaza were included in the project.</p>


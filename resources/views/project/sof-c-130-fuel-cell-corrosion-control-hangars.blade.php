                    <h2>About the Project</h2>
<p>Alticor Inc was the design-builder for two new Special Operations Forces (SOF) hangars. The 31,000-SF Fuel Cell Hangar provides a state-of-the-art facility for aircraft fuel cell maintenance and the 57,000-SF Corrosion Control Hangar. Each hangar includes administrative, mechanical, and maintenance areas to allow aircraft maintenance personnel to provide around the clock maintenance of MC-130 aircraft.  The interiors of the hangars are designed for maintenance of the aircraft parts and include tool bays, maintenance bays, paint storage rooms, paint booths, sanding &amp; prep areas, clean rooms, plastic media rooms as well as administrative offices, break rooms and training rooms.</p>
<p>Each hangar bay is designed to accommodate a single C-130J aircraft and includes a 400Hz system, a flexible lifeline system to provide maintenance personnel a tie off location, breathing air and compressed air, vacuum and water.  The project involved 284,800 SF of total airfield paving, including two separate aircraft access aprons and an aircraft parking apron. Specialty systems included a High Expansive Foam (HEF) fire suppression system, fire alarm system, Lightning Protection System (LPS), Energy Management Control Systems (EMCS), communications, electrical, HVAC, Anti-Terrorism/Force Protection (AT/FP) measures.  Sitework included utilities, storm drainage, paving, walks, curbs, parking, exterior lighting, four high mast light poles with controls to coordinate with the base night vision goggle system, and asphalt paved access roads.</p>


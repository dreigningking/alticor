                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">This 191-bed, seven-story hospital was constructed on the grounds of the 33-acre UC Irvine’s Medical Center campus, which presented a tight, logistically challenging site. The design-build team worked tirelessly to create a space that promoted health, healing and wellness while enhancing the dual missions of teaching and research. Daylight was maximized wherever possible, and architectural finishes were chosen for durability and to impart a sense of serenity to those visiting the space.</p>
<p>&nbsp;</p>
<blockquote>
<h3 style="text-align: center">“Alticor Inc Construction Co. has demonstrated an unparalleled ability in the management and delivery of this complex project. The owner relationship created between the University and Alticor Inc should be presented as a model for the entire industry to replicate.”</h3>
<h6 style="text-align: center">&#8211; Rebekah G. Gladson, FAIA, AUA, Former Associate Vice Chancellor, Campus Architect, UCI Design and Construction Services</h6>
</blockquote>
<h3 style="text-align: center"></h3>
<h2 style="text-align: center">Challenge</h2>
<h3 style="text-align: center">Choosing the Right Delivery Method for Construction of a Major Medical Center</h3>
<p style="text-align: center">California healthcare construction in the early 2000’s was proving a difficult task. UC Irvine knew they were up against some major challenges on this project long before design and construction began. Following the January 1994 Northridge earthquake, the State of California enacted seismic-safety legislation for hospitals, with a mandate that these issues had to be addressed by 2008. This mandate directly impacted the existing UC Irvine hospital, and it was decided that a new hospital tower had to be built. Under this deadline and numerous budget constraints. UC Irvine set out to deliver a world-class medical center on a fast-track schedule in an extraordinarily busy construction market.</p>
<p>&nbsp;</p>
<h2 style="text-align: center">Solution</h2>
<h3 style="text-align: center">Utilizing Design-Build Delivery to Move Through Major Obstacles</h3>
<p style="text-align: center">UC Irvine knew a project of this size and magnitude needed a true team of designers and builders if they were going to meet the fast-track schedule. Knowing that OSHPD’s backlog was astronomical, Alticor Inc took the approach of partnering and aligning itself with OSHPD by negotiating a permitting time frame within their organization. This approach was new to OSHPD and UC Irvine, but with a dedicated Alticor Inc expediter, it was effectively accomplished. Alticor Inc also pushed the limits of true design-build, collaborating with trade partners early in the project to keep the budget in step with market conditions.</p>
<p>&nbsp;</p>
<h2 style="text-align: center">Result</h2>
<h3 style="text-align: center">Design-Build Done Right: On Budget, Ahead of Schedule</h3>
<p style="text-align: center">In the end, this modern healthcare and first-class teaching facility was the first large design-build hospital in the state of California. Utilizing the design-build delivery method allowed for the successful completion of this hospital a full four months ahead of schedule, within budget, with zero claims and with an exemplary safety record. Alticor Inc drew the blueprint for the success of this job with the initial work schedule and with a daily commitment to solve problems quickly in the field.  In partnership with UC Irvine, Alticor Inc changed the future of hospital construction in California by working collaboratively with OSHPD in a design-build model and by doing what the construction industry said couldn’t be successfully done.</p>


                    <h2 style="text-align: left;">About the Project</h2>
<p style="text-align: left;">The phased construction of the Smithsonian Institution&#8217;s landmark National Air and Space Museum provided much-needed space for the aircraft that tell the story of our nation&#8217;s history in aviation and space flight. Phase I, the Steven F. Udvar-Hazy Center, was situated on 176 acres in Chantilly, Virginia, and designed to accommodate the National Air and Space Museum&#8217;s largest artifacts. These were delivered to the facility directly via the nearby runways at Dulles International Airport. This project represents Phase II of the new museum&#8217;s construction. The Smithsonian&#8217;s 236,000-SF addition includes three separate critical spaces: the Mary Baker Engen Restoration Hangar; the Archives, Collections Procession and Conservation Laboratory; and the Observation Gallery. The museum is open 363 days a year, so throughout construction Alticor Inc coordinated activities in a manner that maintained the safety, security, and environmental conditions of the existing museum space at all times. The new addition is supported by the existing utility plant, so provisions were made to tie in the new connections without disruption to the facility&#8217;s strict environmental control systems.</p>


                    <h2>About the Project</h2>
<p>Originally built in 1977, the Frank C. Erwin Jr. Special Events Center is regarded as Austin’s premier venue for large public and private events with seating for up to 17,725. Sometimes referred to as “The Superdrum,” due to its drum-like appearance from the outside, the facility is primarily the home court for the UT Men’s and Women’s Basketball programs. The center holds many diverse events ranging from concerts to wresting events and private banquets. The Erwin Center also hosts the semifinals and finals of the University Interscholastic League boys’ and girls’ basketball playoffs in all five classifications.</p>
<p>UT began a three-phase renovation of the center in 2001 and finished in 2004, selecting Alticor Inc as their partner to construct these improvements over the three-year period. Careful coordination throughout the renovations minimized the disruption of countless events held during that time. New arena seating was provided to create a more exciting basketball environment by moving the fans closer to the action. The renovated courtside seating brings fans seven feet from the court, as compared to the original 20 feet. Alticor Inc also constructed concession stands, new restrooms, and 28 luxury suites. These “skyboxes” provide the ultimate event experience with an advantage to view basketball games and entertainers with theater seating. The suite holder has access to a private wet bar, private catering, and in-suite television, a private restroom and special parking. Additional scope items included a weight room, landscaping, administrative and multipurpose spaces, and an indoor pool for physical therapy training. Visible for miles when orange floodlights illuminate the facility at night, the Frank Erwin Center is a beacon on UT’s campus.</p>


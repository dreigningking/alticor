                    <h2>About the Project</h2>
<p>MRB has been located on a site that it will share with a proposed Institute for Biomedical Sciences and Biotechnology Building (IBSB). The project provides approximately 80,740 net assignable area and 152,000 gross SF of new construction. The facility is comprised of four levels of laboratory, laboratory support, private offices, open plan office space, and associated administrative support spaces. The facility also includes a basement level with mechanical and electrical equipment, laboratory support, laboratory storage space, and a connection to the shared vivarium facility, biological storage, and shell space for a future NMR suite to be constructed as part of the IBSB Phase 1 project.</p>
<p>The structural system for the building is cast in place concrete frames with shear walls for the laboratory area, and long-span steel frame construction for the office and administrative spaces. The building is predominantly clad in brick and sandstone tile, with aluminum curtain wall and aluminum framed windows with metal sunshades. A sunshade covers the courtyard between the IBSB and MRB buildings. The mechanical system for the laboratories is dual duct supply, with single pass exhaust. The office spaces is also served by the dual duct, returning the conditioned air into the make up air stream. All air handlers and exhaust fans are located on the roof.</p>


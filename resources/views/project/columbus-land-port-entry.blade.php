                    <h2>About the Project</h2>
<div>The Columbus Port of Entry project is New Mexico’s newest border crossing that inspects privately owned and commercial vehicles and pedestrians that enter and exit the crossing point. The port operates 24 hours a day, 7 days a week, so was critical that this project remain operational during construction. For the last several years, the port had been operating from outdated, deteriorating facilities that no longer met the functional needs of the agency. Additionally, the port also faced other issues such as frequent flooding because the site is situated at the drainage low point for a 44-square-mile watershed area. The newly expanded port ensures that Columbus can keep up with demand and attract new business to New Mexico.</div>
<div></div>
<div></div>
<div>The goal of this 5-sequence, $67,895,000 project was to correct port deficiencies and modernize the facility to accommodate the increasing pedestrian traffic and high seasonal commercial traffic. Between 2010 and 2014, the Columbus Port of Entry saw commercial truck traffic increase from about 8,500 trucks to more than 14,200 trucks. Pedestrian traffic grew 18 percent over the same period, and vehicle traffic rose 10 percent. From Monday through Friday, the port also serves as the crossing point for hundreds of U.S.-born Mexican children who attend school in Columbus or Deming, New Mexico. In addition to new commercial import and export entrances to the port from Mexico, the update also provided a connection to the new commercial bypass road. Also included were site improvements to control storm water flow to protect the new construction. This project achieved LEED platinum certification.</div>

                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Alticor Inc is currently providing design-build services on the Main Terminal Curbside Expansion program at Tampa International Airport (TPA). The major renovation is designed to create a new set of express arrival and departure lanes for passengers who have no need for curbside services, ticketing or checking luggage. This allows all passengers who are not checking bags with airlines to access the “Express Curb”, which bypasses the ticket lobby entirely and provides direct access to the gates. The innovation reduces curbside congestion while providing a new and enhanced level of customer service.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project includes the expansion of 32 lanes from 16, incorporating both at-grade and elevated roadways along with Vertical Circulation Buildings (VCB) for both TPA’s Blue Side and Red Side. The VCB&#8217;s are three-floor air-conditioned atriums that provide direct access from the new express curbsides to the Transfer Level of the Main Terminal via elevators, escalators and enclosed walkways. These buildings also connect to the adjacent Long Term Parking Garage on the Blue Side and Marriott Hotel on the Red Side. The Red Side VCB also includes a Shuttle Platform that will connect with the future Airside D.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Blue Side VCB opened to the public two months ahead of schedule in November 2021, just in time for passengers to enjoy the benefits of the new curbsides ahead of the busy holiday season. Once inside the VCB, passengers are greeted by two large wall art installations, glass wall panels, stainless steel column covers, terrazzo floors throughout and an intricate monumental staircase as the centerpiece. Passengers can then take the elevator, escalator or stairs up one level to the Main Terminal.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Blue Side VCB phase of the program also includes the replacement of the existing central utility plant (CUP) with a new 4,800-ton capacity plant, which went online just before the 2021 holiday season. The new CUP and Electrical Service Building is housed in a 10,000 SF building and provides heating, cooling, potable water and fire protection for the Main Terminal, Baggage Handling, Network Operations Center, Sky Connect station, new Vertical Circulation Buildings and the Authority’s new Maintenance and Police Headquarters. The new highly efficient chiller plant reduces overall energy costs by 30%, equal to removing 620 cars from the roads and 50 million gallons of potable water per year. The design features cast-in-place concrete cooling towers that double as an illuminated waterfall feature and multi-colored pipe jacketing that each represent heating, fire protection, water and power to the airport.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Other significant features of the Red Side phase of the project include the renovation of the loading dock, replacement of the FAA parking lot, replacement of the existing curbsides’ architectural ceilings, construction of a new Police and Maintenance Department Building and demolition of the existing administration building. The project is on a very tight site and constructs the adjoining terminal building expansions and curbside roadway expansions while maintaining 100% passenger’s pedestrian and vehicle access to the terminal throughout construction.</p>
<p>&nbsp;</p>


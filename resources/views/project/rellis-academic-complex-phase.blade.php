                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Alticor Inc teamed with Page as the successful design-build team for the new RELLIS Academic Complex. The project was envisioned to be a facility that immediately suggests 21st-century technology and promotes a relaxed collaborative and engaged learning and research environment â€” a living laboratory for evolving Smart Campus and pedagogical technologies. Key features of the RELLIS Academic Complex include an architectural style incorporating stone and wood compatible with other buildings at RELLIS, yet is distinctive from the other buildings; landscaping that promotes tranquil reflective thought, engaged scholarship, and minimal maintenance; use of materials and landscaping suggesting strength and sustainability; and extensive use of natural light and openness of the laboratories and teaching spaces.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The first phase of the RELLIS Academic Complex is a 68,000 GSF three-story education center. The facility includes 10 well-equipped classrooms, group learning and collaboration spaces, modern allied health teaching laboratories, upper division science laboratories and more than 40 faculty offices. The design-build team was also responsible for coordinating with a separate campus utilities and infrastructure project and a separate in-progress parking project.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Considerable attention was given to elevating the student experience. Spacious and numerous public areas where students could study and visit with their colleagues between classes were a focal point of the Academic Complex. These areas were highlighted with open balconies and comfortable furnishings. Large expanses of glass bring in natural light. The furniture in the classrooms can be rearranged so students can engage in group learning and projects as well as formal instruction. Another student-centered feature is the spacious patio overlooking the lake where students and faculty can interact in relaxed discussion and learning outside the classroom.</p>


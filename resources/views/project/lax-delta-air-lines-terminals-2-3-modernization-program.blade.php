                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The two-phase <a href="https://www.delta.com/">Delta Air Lines</a> Terminals 2 &amp; 3 Modernization Program at LAX will enable co-location with airline partners in Terminals 2, 3, and Tom Bradley International Terminal.</p>
<p style="text-align: center;">
<p style="text-align: center;">Phase 1 will feature a new four-level central headhouse connecting Terminals 2 and Terminal 3. The headhouse will include the Arrivals/Baggage Claim Level, new TSA baggage inspection system, ticketing, TSA security checkpoint, Delta Operations space and new club space on the top level. The new three-level concourse connects the existing west headhouse and the new central headhouse to the Terminal 3 satellite. This includes the apron level with baggage sortation, gates, hold rooms, concessions and Delta operations space.</p>
<p style="text-align: center;">
<p style="text-align: center;">Phase 2 will include the demolition and replacement of the west headhouse which links the LAMP project (LAX train system) to Terminal 3 as well as the central headhouse, Terminal 3 concourse and a new connector to Tom Bradley International Terminal. The Terminal 3 satellite will also be renovated, and the building will be redesigned to include apron level offices for ramp functions and the passenger level for gate access.</p>
<p style="text-align: center;">
<p style="text-align: center;">This project provides all the amenities that Delta’s customers have come to expect at LAX, including a private Delta One check-in lounge, new Delta Sky Clubs, fast check-in for Delta shuttle services and an integrated in-line baggage system. When completed, the facility will offer convenient passenger flows, sufficient gate-area seating and a world-class concession program.</p>


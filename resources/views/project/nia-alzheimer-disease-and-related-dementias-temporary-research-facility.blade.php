                    <h2>About the Project</h2>
<p>The new Alzheimer disease temporary research structure, located on the National Institutes of Health (NIH)&#8217;s Bethesda, MD campus, provides new research space to the National Institute on Aging (NIA). The building is designed as a single-story, modular structure with open concept labs and offices.</p>
<p>&nbsp;</p>
<p>This new building is a transformative, state of the art research center, bringing together world-leading scientists from both within the intramural and extramural research programs, as well as from industry and academia. The NIA program of requirements consists of design criteria for physical/functional spaces and the creation of interior and exterior environments which nurture and encourage interaction, collaboration and unencumbered communication between researchers in open, flexible and multiuse rooms</p>
<p>&nbsp;</p>
<p>To reduce cost and construction time and to maximize available interior space, the building is designed as a single story facility, with open concept labs and offices. The 26,000 square foot facility supports bio-safety level 2 laboratories, laboratory support areas, breakroom, breakout areas, huddle spaces, conference rooms and both open and enclosed office spaces. Roof mounted mechanical/electrical support spaces provide an efficient layout of the equipment necessary to run the building. Utility, electrical and CIT support spaces provide an efficient layout of all the equipment necessary to run the modular facility. The mechanical air handling equipment is situated in a rooftop penthouse above the office wing.</p>


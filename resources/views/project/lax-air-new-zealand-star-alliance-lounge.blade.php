                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Air New Zealand Star Alliance Lounge is the first indoor/outdoor lounge at LAX. The premium customer facility offers space for around 400 Business Class and Star Alliance Gold passengers, including an exclusive area for First Class passengers.</p>
<h3 style="text-align: center">CONSTRUCTION</h3>
<p style="text-align: center">Designed by global architectural firm Gensler, the lounge features a unique open-air terrace, featuring fire pits and a water wall, offering panoramic views of the northern runway towards the Hollywood Hills. The loungeâ€™s experience zones are designed to accommodate passenger needs, ranging from social gatherings to quiet time away from home. In addition to the outdoor terrace, the lounge offers a bar area for socializing, library space, den, study and media room. Those wishing to freshen up before their flight can do so in one of eight shower rooms. The Star Alliance Lounge was listed as one of the Top 10 Premium Airport Lounges in the world. It is also the only U.S.-based lounge to make the list.</p>
<h3 style="text-align: center">FACILITY SERVICES</h3>
<p style="text-align: center">Alticor Inc Services is providing complete facility management, operation and maintenance for Air New Zealand as the operator of the Star Alliance Lounge.</p>


<p style="text-align: center">The LBUSD McBride Senior High School is an Early Academic and Technical School consisting of 43 classrooms in seven buildings for 1,080 students. The buildings are oriented around an internal campus promenade, which acts as the school’s main circulation corridor. There is an administration building with a library on the second floor, a science building, two multi-story classroom buildings, a gymnasium, lecture hall and food services facility. Classes have high-tech commercial, industrial and computer equipment and student workspaces outfitted with flexible furniture, allowing them to develop skills demanded by today’s rapidly evolving, technical workplace. The high school is designed to The Collaborative for High Performance Schools Verified guidelines.</p>


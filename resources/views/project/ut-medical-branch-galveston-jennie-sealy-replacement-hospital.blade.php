                    <h2>About the Project</h2>
<p>The Jennie Sealy Replacement Hospital heralds a new era in medical education and patient care for the University of Texas Medical Branch (UTMB). Every aspect of this project which services the community, region and state we call home – our ability to continue supporting exceptional healthcare professionals, our ability to construct a building with the latest in care to those who need it the most, the ability to spur breakthroughs in the treatment of disease and injury – hinges on this facility. The new hospital provides a warm and comforting environment for patients, with views of the Gulf of Mexico and the Galveston Ship Channel from each room. In addition to patient rooms, the $438 million facility includes 60 dedicated ICU beds, a 28-bed day surgery unit and 20 operating suites that feature state-of-the-art diagnostic imaging equipment permanently integrated into the operating rooms.</p>
<p>Boasting 770,000 SF, the UTMB Jennie Sealy Replacement Hospital has profound advances for patient care and health sciences education in Texas and beyond. Designed with a future in mind, the 13-story mission-critical hospital features leading-edge surgical suites, surgical intensive care units, pediatrics acute care, real-world classrooms, 260 patient rooms, including 56 dedicated ICU beds, and clinical support services. The hospital&#8217;s surgical complex – anchored by 20 state-of-the-art operating suites – is as sophisticated as any in the United States, with space for organ transplantation, neurosurgery, cystoscopy and orthopedic surgery. The hospital includes built-in flexibility to maintain the best possible environment as healthcare technology evolves.</p>
<p>All of the hospital&#8217;s activity begins on the second floor, a full 25 feet above sea level. In addition, by locating mechanical systems on the seventh floor, we can ensure that in the event another catastrophic hurricane hits the Galveston shoreline, the hospital will remain fully operational. A new public entry and elevated walkways connect the hospital to the existing John Sealy Hospital and the university’s nationally recognized Level I Trauma Center. A portion of the existing hospital will be renovated while remaining operational during construction. After 22 months of preconstruction services, construction began in March 2012. The hospital reached substantial completion in January 2016, providing abundant natural lighting and views of Galveston Harbor and the Gulf of Mexico to the patient and family-centered healing environment.</p>


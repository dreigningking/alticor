            <p><strong>Meaningful Impact on the Design Process</strong></p>
<p>The staff’s feedback had a significant impact on the design process and, as a result, changes were made to the laboratory designs. The involvement of customer staff in design specification and review places an extraordinary burden on staff time. Accordingly, for this process to proceed efficaciously, staff need to be involved early and given plenty of time to formulate meaningful input to the design process. This process was tremendously successful in allowing researchers to visualize their space. They were able to provide input to adjust their laboratory design to best fit their specialized needs as well as feel reassured that their new space would serve them effectively.</p>

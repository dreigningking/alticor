                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p><strong>Researcher Engagement During the Design Phase</strong></p>
<p>To ensure the researchers’ needs were met, the design-build team made a large effort to engage NIST’s 12 lead researchers during the design phase. These individuals oversee multiple labs, each specializing in a different type of research, therefore requiring specific nuances and space for efficient research. The team involved the 12 lead researchers in the design process through individual interviews and design reviews. These were used to extract each user’s specific needs and were then applied to the finished laboratory designs. The team facilitated various tours and 3D walk-throughs of the designed lab spaces, allowing researchers to interact with their laboratories.</p>
<p>&nbsp;</p>

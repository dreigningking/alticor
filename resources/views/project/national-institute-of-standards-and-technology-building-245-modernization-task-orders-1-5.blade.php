            <p><strong>Meeting the Needs of Various Research Groups</strong></p>
<p>Value, functionality and efficiency were NIST’s top design drivers for the NIST Building 245 Modernization. All design decisions focused on creating a functional, pragmatic and cost-effective building that supports the technical requirements for scientific research and will serve NIST for at least the next 50 years. In addition, the design-build team was tasked with meeting the needs of several different research groups. Managing the needs of the individual researchers while respecting NIST’s design drivers was a challenge to the design-build team.</p>
        </div>
        
        
        
                
        
        
        

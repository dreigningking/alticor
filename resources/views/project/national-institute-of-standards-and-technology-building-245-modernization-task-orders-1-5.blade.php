                    <h2>About the Project</h2>
<p>Originally built in 1964, the Radiation Physics Building, also known as Building 245, hosts research vital to our nation&#8217;s healthcare, environmental monitoring, radiation protection and industrial processing. The National Institute of Standards and Technology (NIST) modernization project&#8217;s contract consists of multiple task orders that complete in 2023. The team has been awarded eight task orders, with the Alticor Inc Design-Build Team being awarded the contract based on our winning approach to task order one, an addition dubbed the H Wing. This addition provides uniquely equipped modern laboratory space, and its completion is vital to providing swing space as the team moves into later phases where work will be focused within the existing building.</p>
<p>&nbsp;</p>
<p>Throughout the construction of Task Order 1, Alticor Inc took great care to minimize disruptions to ongoing research. The H Wing aligns with the existing Building 245, therefore, the addition uses a mat foundation that reduces the possibility of differential settlement. The structural configuration of the addition consists of a robust concrete frame, offering enhanced floor loading and vibration mitigation. The superstructure meets a baseline vibration criteria velocity of 2,000 micro-inches per second. The addition also includes 2,500 SF of Class 100 cleanroom space.<br />
The Task Order 2 scope of work includes the design and construction of the D Wing addition totaling $25.6 million. This addition includes a precast facade built around a new concrete structure that consists of a new plumbing room, mechanical room, laboratory space and cleanroom that connects to an existing laboratory. Due to the laboratory operations occurring adjacent to the construction of D Wing, extensive coordination and communication were required to allow the ongoing science to operate within the laboratory and ensure the safety and welfare of the construction personnel due to the radioactive sources that were being used at different times.</p>
<p>&nbsp;</p>
<p>The remaining task orders include Task Order 3, which consisted of the design and construction solution to replace the existing A Wing electrical service room within A100 along with the associated transformer yard. This also included providing a new main and emergency electrical room. Task Order 4 includes the IT systems for H Wing totaling $828,000. The IT systems include data and communications like wireless access and new fiber throughout, and included in Task Order 5 are the security systems for the H Wing addition.</p>
<p>&nbsp;</p>
<p>The completion of Alticor Inc’ work on-site at NIST will allow the scientists there to continue serving diverse business, agriculture and security interests while incorporating upcoming technologies.</p>


                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The Office of the Chief Army Reserves (OCAR) Building operates as an administrative command building housing approximately 430 employees. The 88,470 gross SF building is located on ten acres at Fort Belvoir in Virginia. The facility is a three-story steel superstructure clad with precast panels and embedded brick veneer. The project was a result of the Department of Defenseâ€™s six-year Base Realignment and Closure (BRAC) 2005 program.</p>
<p style="text-align: center;">Its scope included administrative space, an emergency operations center, a sensitive compartmented information facility (SCIF), secure and non-secure conference rooms, video teleconference centers, a data processing center, and General Officer/Senior Executive Service office suites. Despite the aggressive twelve-month schedule for design and construction, the design was completed within budget, and the project was completed in just 51 weeks, using over 95,000 man hours with zero lost-time incidents, and earning two safety accolades from the U.S. Army Corps of Engineers.</p>


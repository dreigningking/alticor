                    <h2>About the Project</h2>
<p>​The Samsung Fab2 Office Expansion project includes the design and construction of a three-story office building of approximately 150,000 gross SF on the west end of an existing Fab2 office building. The scope also included new parking and associated landscape to accommodate the demand and growth of the sprawling 300-acre Austin Campus in Northeast Austin. The new space and existing building were separated by a single expansion joint to Samsung’s existing Fab2 office building. Removal of the existing curtainwall and renovation of areas at the existing office building were also included in the scope. The new expansion included open office space for approximately 900 cubicles, a fitness center, training rooms, café expansion, customer foundry area, company store and meeting rooms.</p>


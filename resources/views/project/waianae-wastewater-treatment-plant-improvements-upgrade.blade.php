                    <h2>About the Project</h2>
<p>The Waianae Wastewater Treat Plant (WWTP) Improvements and Upgrade project consisted of the construction of a new 2W (No. 2 non-potable water) piping network with isolation valves, rehabilitation of primary clarifiers and influent/effluent distribution boxes and piping, 2W seal water system improvements, construction of new headworks influent screens and washer/compactors and modifications to the grit removal system and pre-aeration tanks. In addition to improvements and new construction, Alticor Inc was also responsible for soil erosion and sediment control, electrical site work, temporary diversion pumping for work at the primary treatment unit process and start-up, testing, and commissioning of systems.</p>
<p>Alticor Inc completed 75 percent of the self-perform work on the project, including concrete repairs and installation; demolition; air ductwork; the installation of the underground 2W line, primary clarifier mechanisms, scum piping, spray piping, tubing, biotrickling filter system, hydropneumatic tank, centrifugal pump, slide gate, perforated plate screen and mechanically cleaned bar screen. Alticor Inc also planned outages and bypass work.</p>
<p>Although work on the project has concluded, Alticor Inc continues work at Waianae WWTP under a separate contract for the biosolids dewatering system replacement.</p>


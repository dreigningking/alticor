                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p><strong>MallWalls, Phasing, and Innovative Solutions</strong></p>
<p>The team came up with a temporary demising wall known as MallWall which was used for blocking the work going on behind the scenes, making passengers and employees less aware of the construction. The original wall GOAA had intended to use was much more extensive. Alticor Inc recommended MallWall which was faster to construct and resulted in the team’s ability to transition spaces from public to construction demised spaces in single overnight shifts. The Client now uses it in other operation areas of the airport.</p>
<p>Phasing was also crucial in order to keep the day-to-day operations of the airport in movement. Coordination with GOAA and all trade partners allowed work to be performed during slower passenger traffic volume periods such as nights and weekends. GOAA worked with Alticor Inc by moving flight gates and times around while informing the team when flights were going to interfere with phasing plans. As the project continued, the team’s understanding of airport operations increased, allowing them to selectively pick times that would be least intrusive to day-to-day operations.</p>
<p>The team performed 536 Utility Outage Notifications (UON) and Maintenance of Traffic operations (MOT). A UON process is when power is turned off to an area, fire sprinkler systems are drained or a utility or system such a baggage is taken offline. A MOT is the modification of traffic pattern or takeover of publicly or operationally occupied spaces. Both tools provide awareness to all stakeholders at the airport of a potential traffic routing or system interruption.</p>
<p>A substantial upgrade to the electrical system at each wing would have required rerouting of ground service operations and gates to be shut down while trenching was going on. The team proposed the use of a Horizontal Directional Drilling (HDD) technique to run the conduit below the existing apron and utilities. The result was a schedule reduction of 264 days and no gate shutdowns were required to install 1,500 LF of conduit pathway in only 13 days.</p>

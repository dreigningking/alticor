            <p><strong>A 2.5 Year Renovation and Expansion Project in a Fully Operative International Airport</strong></p>
<p>One of the greatest logistical challenges during the 2.5 years of the Airside 4 Expansion and Renovation project life was that the Orlando International Airport remained fully operative. Approximately 22 million passengers and airport personnel walked through the Airside Four jobsite during its lifetime. Ensuring safety, security, and maintaining day-to-day and on time flight operations were top priorities.</p>

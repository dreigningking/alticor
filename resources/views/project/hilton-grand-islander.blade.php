        <div class="grid-box1 ">
            <h3>Building a High-Rise in the Middle of Hilton&#8217;s Largest Property</h3>
<p>The 38-story Hilton Grand Islander was built on a tight site within Hilton&#8217;s 22-acre resort property in Waikiki. In addition to traffic from thousands of tourists surrounding the site on a daily basis, the zero lot line site was bordered on two sides by major roadways, and on another side by a conference center/ballroom, and another by an existing high-rise hotel tower and swimming pool. These site constraints made for a logistical challenge not just in terms of construction, but in the management of deliveries, materials and manpower.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Intensive Logistical Coordination</h3>
<p>As part of an extensive pre-planning process, the Alticor Inc team developed a barricade plan that blended into the property’s surroundings, and included way finding and safety signs in multiple languages to facilitate the needs of diverse tourists in Waikiki. Daily coordination with hotel operations and subcontractors was a necessity. The Alticor Inc team dedicated a group of full-time employees to support logistical coordination issues with public safety, material deliveries, utility tie-ins, coordination with off-site contractors, and hotel operations. The early established process of open communication and proper planning among the entire team created a flexible atmosphere where team members were able to respond efficiently when issues arose.</p>

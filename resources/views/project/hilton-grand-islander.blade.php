<h3 style="text-align: center;">Upfront Planning Paved the Way</h3>
<p style="text-align: center;">The Hilton Grand Islander was completed on-time to welcome its first guests on March 1, 2017.  This was a result of the entire Alticor Inc team and subcontractors working together with the owner and designers to reach a common goal: Deliver a landmark building, safely, on-time, and within budget. The high level of quality achieved, and the timeframe in which the project was delivered, can be directly attributed to the upfront planning by the entire team during preconstruction to set the project up for success right from the start.</p>

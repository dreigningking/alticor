                    <h2 style="text-align: center">About the Project</h2>
<p style="font-weight: 400">The Mesa Court Expansion project consists of three new concrete structures, each standing five stories high, plus a podium. These residence halls include 884 beds and ancillary space such as study rooms, computer labs, a great room, a fitness center, a recreational room, student office space, kitchenettes and laundry facilities. A cutting-edge dining facility was incorporated into the design, providing a fresh food concept with multiple food venues, food preparation classes and a coffee house with extended hours of operations. The expansive dining area services all students housed at Mesa Court. This state-of-the-art facility serves a total of 2,500 students and residents and is a campus landmark and hub for the UC Irvine community.</p>


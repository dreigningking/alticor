                    <h2>About the Project</h2>
<p>The University of Texas Medical Branch (UTMB) League City Parking Garage project is a $34.8 million, seven-floor parking structure with an attached 33,000 GSF multi-use building. The parking structure has a 742-car capacity that will be expandable to 2,100 spaces to meet future expansion goals. This structure serves as a connection between the parking garage and the existing hospital by way of a new elevated, enclosed pedestrian bridge. The first two floors of the multi-use building are reserved for food and retail space. The remaining floors have shell space that could serve multiple purposes in the future. This parking initiative helps further support the growth of the League City Campus.</p>


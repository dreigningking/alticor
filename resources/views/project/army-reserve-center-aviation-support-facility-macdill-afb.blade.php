                    <h2>About the Project</h2>
<p>The Alticor Inc Southeast District recently completed the Army Reserve Center and Aviation Support Facility at MacDill Air Force Base in Tampa, Florida. The $52 million contract was a design-bid-build project for the U.S. Army Corps of Engineers &#8211; Louisville District.</p>
<p>&nbsp;</p>
<p>The 92,000 SF Army Reserve Center and Aviation Support Facility supports the Blackhawk Helicopter programs. The 160&#8242;-4&#8243; wide by 180&#8242;-6&#8243; long hangar facility includes an Intrusion Detection System, arms vault, and SIPRNet Communications which provide high security for top secret communication information. The project also includes a 10,000 SF Unheated Storage Building.</p>
<p>&nbsp;</p>
<p>Site work included a staggering 750,000 SF of helicopter apron pavement with washdown facilities, 151 on grade parking spaces, military equipment parking, and the demolition and replacement of an existing Skeet and Grenade Range to a nearby site. The project is designed to withstand 150+ mph hurricane wind and force and meets the MacDill Architectural Design guide regarding color and finishes. The project is currently on track to meet LEED Silver.</p>


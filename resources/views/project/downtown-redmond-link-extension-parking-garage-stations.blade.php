                    <h2>About the Project</h2>
<p>Alticor Inc served as a teamed super subcontractor to Stacey and Witbeck/Kuney, a Joint Venture for the $731,596,000 Downtown Redmond Link Extension project. Alticor Inc served as the design-builder for the 1400-stall, six-story, 615,000 SF Parking Garage and as the general contractor/construction manager for one at-grade station adjacent to the Parking Garage and one elevated station in downtown Redmond. The Downtown Redmond Station is pursuing LEED for Transportation Gold certification.</p>
<p>&nbsp;</p>
<p>The <strong>Parking Garage</strong> is comprised of a post-tensioned, cast-in-place concrete system. Level 1 of the garage (at-grade) supports King County Metro and Sound Transit operations and features four active and eight layover bays for bus operations. The upper level features a 90 kW photovoltaic array and serves as a covered pedestrian walkway. To better blend with future mixed-use projects in the adjacent Marymoor Village area, the garage is clad with brick, glass, metal panels, perforated and articulated metal panels and tension mesh. A glass curtainwall clads two feature stairs at the south elevation.</p>
<p>&nbsp;</p>
<p>The <strong>SE Redmond Station</strong> is an at-grade station adjacent to the garage and is accessed from the garage via a glass clad stair from Level 2 to the station platform. The 380-foot long platform is highlighted with a glass and metal clad canopy and multiple glass and steel wind screen structures. The platform surface is a precast concrete paver system with integral tactile wayfinding. This station provides two Ticket Vending Machine areas protected from the weather by glass and steel canopies.</p>
<p>&nbsp;</p>
<p>The <strong>Downtown Redmond Station</strong> is the terminus of the East Link Extension and is constructed approximately 25’ above grade. The elevated platform is accessed via two entries, or head houses. Each head house features brick, glass and metal panel cladding and contains a stair, elevator and escalator. The site is heavily landscaped and includes plazas amenities for the City of Redmond to utilize as small-scale event space. The elevated guideway provides a storage tail track for rail car staging to support peak service demand times. This tail track features unique architectural treatments including brick and perforated panels to provide wayfinding and architectural interest at this terminus.</p>
<p>&nbsp;</p>
<p>For more information, please visit the <a href="https://bit.ly/34SOn73" target="_blank" rel="noopener">Sound Transit DRLE project page. </a></p>


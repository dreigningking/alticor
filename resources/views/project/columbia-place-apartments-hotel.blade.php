                    <h2>About the Project</h2>
<p>The Columbia Place project in Washington, D.C. includes a 244,000 SF Class A residential apartment building and a 422,000 SF 4-Star, dual-brand (Marriott Residence Inn and Marriott Courtyard) convention center hotel.</p>
<p>&nbsp;</p>
<p>The apartment complex features 34 studio units, 143 one-bedroom units and 37 two-bedroom units. The 504-key hotel consists of 357 Courtyard and 147 Residence Inn rooms by Marriott. Associated on-site amenities include a ground floor terrace, rooftop terrace, a rooftop pool and deck, pool locker rooms, indoor and outdoor recreation space, hardscape and landscape features, fitness center and bicycle storage. 21,500 SF of meeting space, a full service restaurant and lobby bar and 1,600 SF of retail are located in the hotel. The projects share a two-level, below-grade parking structure.</p>
<div class="ng-scope">
<div>
<p>&nbsp;</p>
<p>​One of the key project aspects was the incorporation of historical row houses and buildings. A civil war era townhouse, dubbed “The 911 Building,” was moved along a 40 foot skate system and incorporated into the hotel as a high-end board meeting room. Six other townhouses maintained their historical facades and were incorporated into the design as hospitality suites and restaurants. Incorporating these older buildings into the new construction recognizes and focuses on the deep culture and historical significance of the Shaw DC area.</p>

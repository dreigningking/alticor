                    <h2>About the Project</h2>
<p>The Love Field Parking Garage project is a $178 million project that adds 5,130 parking spaces to the Dallas Love Field Airport in Dallas, Texas. The parking structure provides nine levels of parking; two basement levels and seven levels above grade. The public parking areas on the upper levels include the installation of a Parking Guidance System (PGS) and Parking Access and Revenue Control System (PARCS), which are extensions of integrated systems in Love Fieldâ€™s two existing parking garages. The basement levels are reserved for valet parking. An air conditioned and elevated pedestrian corridor were also constructed on the southwest side of the parking structure that span from the garage into the terminal above Aviation Place. Another aspect of this project is the Dallas Police Department Building, which includes the design and construction of a new two-story building on the approximate footprint of the west end of the East Satellite Building.</p>


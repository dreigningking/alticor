                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The 860,000 SF McGregor Square project is a vibrant mixed-use development located adjacent to Coors Field, home of the Colorado Rockies in downtown Denver. This fast-track, Construction Management at Risk project showcased Alticor Inc’ expertise at managing a project while collaborating with the architect, Stantec, to deliver a landmark project to the Colorado Rockies Baseball Club, Ltd.</p>
<p style="text-align: center;">McGregor Square is comprised of three towers (office, hospitality and residential) and a two-story below-grade parking garage, as well as retail and restaurant space on the ground floor. This “Gateway to the Ballpark” includes 230,000 SF of office space spread across 11 stories and 175,000 SF of residential space spread across 13 stories. The residential space consists of 105 high-end condominiums and a rooftop pool, amenity deck and fitness room located on the bridge. In addition, McGregor Square includes 156,000 SF of hotel space spread across 13 stories with 176 hotel rooms, 71,000 SF of retail space including the future Rockies Hall of Fame and 196,000 SF of parking with 402 parking spaces.</p>
<p style="text-align: center;">Nestled in between the towers, the 58,000 SF plaza will serve as a year-round outdoor entertainment area, which features a 20-foot by 60-foot big screen that extends the game day experience to the facility. This project is the ninth building in the world registered by the WELL Community Building Standard upon completion. The project team achieved WELL certification for the residential portion of the building and WELL Community certification for the plaza portion.</p>


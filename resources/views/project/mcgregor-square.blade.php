        <div class="grid-box1 ">
            <h3>Fast-Paced Schedule with Complicated Permit Process</h3>
<p>The overall schedule for this project was very aggressive due to the owner’s desire to have it completed and open by the Colorado Rockies 2021 Opening Day. With three separate towers each having different uses and all sitting on top of a common below-grade concrete parking garage, the permitting approach for this project started early during the Schematic Design phase. To further complicate things, the entire complex shares various systems such as chilled and heating water systems, a fire pump and the emergency generator.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Pre-Planning and Close Coordination</h3>
<p>The owner’s goal was to bring each tower “on-line” independently; therefore, the decision was made to secure permitting for each tower separately and include the below-grade parking garage along with one of the towers. Alticor Inc and the design team worked extensively with the City and County of Denver&#8217;s Building Department early in the design phase on this approach to ensure that there would be no issues during the permit review process, especially with the shared systems.</p>

                    <h2 style="text-align: center;">About the Project</h2>
<p>San Diego College Community District consolidated three San Diego City College campus projects into the one A, D &amp; T Building Renovation project.</p>
<p>&nbsp;</p>
<p>The A, D &amp; T Building Renovation project consisted of the demolition of interior walls, upgrades to finishes and MEP systems, and the removal and replacement of site utilities and appurtenances. These three buildings made up approximately 136,000 ASF of renovation following the demolition and site preparation, along with the construction of new utilities. Also included were landscape and irrigation, exterior walkways and other hardscape/landscape of all disturbed areas to interface with the ultimate build-out of the campus.</p>
<p>&nbsp;</p>
<p>The A Building is located on campus according to the master plan and consists of office and administrative space and building support space for the following programs: Accounting, Admin/Business Services, Alumni/Foundation, Admissions, Veteran’s Affairs, International Admissions, Agriculture, Assessment, CalWORKs, Counseling, Digital Print Production/Mailroom, Disabled Students Programs &amp; Services, EOPS/CARE, Financial Aid, Health Services, IT Department, Mental Health, Outreach, Student Success Initiatives – PUENTE, UMOJA, Future Grants &amp; Special Programs, Shipping &amp; Receiving, Technical Services, Transfer &amp; Career Center, New Horizons, Research and Trio.</p>
<p>&nbsp;</p>
<p>The T Building is located on campus according to the master plan and consists of classrooms, computer labs, machine labs, office and administrative space and building support space for the following programs: CACT/Incubators, Child Development, Electricity, Electronic Technology, Engineering, Facilities Maintenance, AIRE – HVAC &amp; Refrigeration, Machine Technology, Manufacturing Engineering Technology and MESA.</p>
<p>&nbsp;</p>
<p>The D Building was originally identified as a roof renovation project. The original roofing scope covers the roof topping slab/paving material removal down to structural concrete, planter partial demo, stair topping slab demo, waterproofing the entire deck and planter area, construction of multiple new shade structures and recreational seating, installation of new pedestal paving system as well as dining area ceiling and lighting removal/replacement, roof drain piping replacement, general accessibility and restroom upgrades. Although the original project included only the roof, the scope of this project expanded to include the renovation of portions of the building to accommodate a bookstore space and shell space for a future bank/credit union.</p>
<p>&nbsp;</p>
<p>The landscaping and hardscaping improvements associated with the A, D &amp; T Building Renovation project were of particular importance to SDCCD. The D Building south and west elevations open out onto Gorton Plaza–a focal point of the main campus–and the roof affords panoramic views of the downtown skyline.</p>


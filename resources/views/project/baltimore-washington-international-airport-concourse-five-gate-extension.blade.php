                    <h2>About the Project</h2>
<p>The Baltimore/Washington International Airport (BWI) Concourse A 5 Gate Extension project involves the expansion of five gates in the Terminal A/B complex in order to maintain capacity for Southwest Airlines. This project includes construction of a 55,000 SF addition to five gates in Concourse A. Similar to the existing building, the addition consists of two primary levels–Apron Level and Departures Level.</p>
<p>&nbsp;</p>
<p>The Apron Level consists of approximately 24,000 SF of new facility. It will include support spaces such as a mechanical room, electrical and communication rooms, and custodial storage rooms. In addition, there is space for concession storage to support the concessions on the Departures Level and unassigned space that is anticipated to be occupied by Southwest Airlines. A small portion of the existing Apron Level will be reconfigured for more efficient circulation.</p>
<p>&nbsp;</p>
<p>The Departures Level includes approximately 31,000 SF of new facility. The addition includes five new hold-rooms sized to accommodate B737 MAX8 aircrafts. Approximately 5,000 SF of new food/beverage and retail concession shell space; one new elevator; and new men’s and women’s restrooms is also included in the addition.</p>


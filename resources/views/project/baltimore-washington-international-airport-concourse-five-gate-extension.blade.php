            <p><strong>Complex and Innovative Bathroom Design</strong></p>
<p>The new BWI Concourse A 5 Gate Extension includes several innovative design aspects including state-of-the art restrooms. These restrooms combine some of the best design aspects from restrooms from around the country and the world. Design aspects include independent restroom stalls with independent HVAC, niches for bag and wardrobe bags, full-height doors, intelligent restroom systems, such as lights that come on when the lock is engaged, tracking of time between cleanings and when cleaners are actively cleaning the bathrooms and high-end bathroom finishes. This design represents a new restroom standard that will be pushed throughout the rest of the airport.</p>
<p>Several design modifications were made a significant way into the project. This had the potential to cause delays in the project because the modifications and new finishes required an  additional construction duration.</p>

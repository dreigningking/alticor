                    <h2>About the Project</h2>
<p class="p1">The San Francisco International Airport Terminal 3 East project includes the complete transformation of the checkpoint areas and main concourse, which serves United Airlines. This 400,000 SF design-build project includes the expansion and renovation of Terminal 3 East while maintaining 24/7 operations. Work includes a 60,000 SF building addition along with three new gates and a complete update of existing interiors, featuring a new checkpoint and all new MEPF systems.</p>
<p>&nbsp;</p>
<p class="p1">The passenger experience was enhanced with the addition of new queuing lanes to reduce wait times, state-of-the-art equipment, and a generous “recompose area,” with upholstered benches to allow customers to put on their shoes and reorganize their belongings. Natural light, quality finishes, improved lighting, and acoustical controls further enhance the space, while enlarged restrooms, information displays, ample holdroom seating with convenient power outlets, and concession spaces deliver an experience that promises to make SFO the world’s #1 airport.</p>


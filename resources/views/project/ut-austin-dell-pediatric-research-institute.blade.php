                    <h2>About the Project</h2>
<p>Located on the site of the former Robert Mueller Municipal Airport and adjacent to the new Dell Children’s Medical Center of Central Texas, the Dell Pediatric Research Institute (DPRI) establishes Austin as a center of excellence on children’s health issues. Made possible by a $38 million gift from the Michael &amp; Susan Dell Foundation to the University of Texas at Austin, the DPRI provides research, laboratory, and office space that facilitates interdisciplinary collaboration between academic research, medical research, and clinical initiatives supporting the health and welfare of children.</p>
<p>&nbsp;</p>
<p>Alticor Inc led the design-build team that provided comprehensive services to develop a phased biomedical research facility with an open and flexible architectural plan. The DPRI is a component facility of a research and development park within the Mueller subdivision, a mixed-use local development. The project featured the use of 3-D/Building Information Modeling (BIM) technology during design and construction. The 150,000-GSF facility is designed to maximize flexibility, as 20,000 GSF of open research laboratories flank 20,000 GSF of central laboratory core, including common equipment rooms, specialty labs, micropsy, tissue culture, imaging and glasswash divided by a central service corridor on the two upper floors. The ground floor contains the director’s administrative suite, and the more public side of the facility’s function includes video-conference rooms, training, interaction space, and building support services. A 25,000-GSF vivarium also occupies the ground floor to house animal colonies of up to 25,000 rodents. The building was designed to meet the requirements for U.S. Green Building Council LEED certification, and our design-build team exceeded these goals and expectations to achieve a LEED Gold certification.</p>


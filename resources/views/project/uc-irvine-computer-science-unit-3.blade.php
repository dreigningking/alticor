<p style="font-weight: 400">A combination of dry laboratories and classrooms, this six-story, cast-in-place concrete structure features tiered lecture halls and seminar rooms to aid in the educational advancement of information technology disciplines. The building houses administration offices, including 10 endowed chairs that are part of the Bren gift. Originally designed as two separate buildings, the Alticor Inc design-build team was retained to re-design and consolidate the buildings, resulting in a cost savings of $6 million. The re-designed lab and classroom facility helped maintain UC Irvine’s computer science school at the forefront of technological advancement. Additionally, a second building was constructed as an add alternate to the project, comprised of a single lecture hall and two seminar rooms.</p>


        <div class="grid-box1 ">
            <h3>A unique project built amidst an operational airport</h3>
<p>Built between two active terminals, arrivals and departures traffic lanes, and an active airfield, the site of the new Airport Traffic Control Tower is beyond congested. Add to this the desire for an architecturally iconic design while maintaining the stability of a 13-story tower built in a major earthquake zone, and this becomes one of the most challenging aviation projects constructed in recent history.</p>
<p>&nbsp;</p>

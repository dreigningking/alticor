                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Total collaboration through a true design-build approach</h3>
<p>A collaborative partnering approach built trust and aligned expectations with project stakeholders to create a cohesive and decisive team able to deliver an exceptional project outcome. Even with value engineering and constructability modifications, the building remains true to its architectural vision. The design solution incorporated an innovative structural system (vertical post-tensioned tendons) in a central concrete core to achieve the required seismic performance. Construction solutions include the extensive use of BIM to help coordinate the FAA-required MEP systems within the tight footprint of the building, and a comprehensive logistics plan that was carefully coordinated with multiple airlines, duty managers and airport security.</p>

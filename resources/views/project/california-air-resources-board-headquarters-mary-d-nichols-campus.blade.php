                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The Mary D. Nichols Campus is the new regional headquarters for the California Air Resources Board (CARB). This project consolidated CARB’s seven locations throughout Los Angeles into a single location in Riverside that houses more than 400 employees. The state-of-the-art facility includes an extended range of dedicated test cells for testing of light to heavy-duty vehicles, an advanced chemistry laboratory and workspace for accommodating new test methods for future generations of vehicles. The project includes space for developing enhanced onboard diagnostics and portable emissions measurement systems, visitor reception and education areas, a media center, flexible conference areas and a large public auditorium.</p>
<p style="text-align: center;">
<p style="text-align: center;">The new campus is one of the most extensive and advanced vehicle emissions testing and research facilities in the world. It was designed and built to reach the highest possible levels of measured sustainability: LEED Platinum certification, Net Zero Energy and CalGreen Tier 2. It is the largest true Net Zero Energy facility of its type in the world — producing as much energy as it uses.</p>
<p style="text-align: center;">
<p style="text-align: center;">The unique design provides a seamless consolidation and integration of the office program into a three-story, pinwheel-shaped building that offers vertical connectivity to increase proximity between offices and horizontal connectivity with testing areas, support space and laboratories, resulting in greater flexibility and interaction. This pinwheel concept creates a central command control area that forms the nexus of the new CARB campus. The high-performance campus features ample open and green spaces, as well as nature walks for al fresco breaks and informal meetings alike.</p>


                    <h2 style="text-align: center;">About the Project</h2>
<div style="text-align: center;">Alticor Inc was awarded Transformers: The Ride 3D with a goal to have it designed and built in a mere 12 months. This had never been done before in any of the multiple Universal parks. The one-year goal became even more challenging when Universal decided to choose an existing attraction facility in the center of the active park as the location to be “transformed” into their newest state-of-the-art, visually sensational, 3-dimensional ride.</div>
<div style="text-align: center;"></div>
<div style="text-align: center;">The project scope began with the demolition of the existing 35,000-SF, 60-foot tall structure and the relocation of utilities and back -of-house equipment for an adjacent restaurant that had to be kept operational. The new 80,000-SF building consisted of two mat slabs, concrete foundation walls and waterproofing before 938 tons of pre-manufactured heavy structure could begin. Elevated interior floors were metal deck with one-foot concrete. All ride track concrete, both at grade level and second floor, was constructed to super flat tolerances.</div>
<div style="text-align: center;"></div>
<div style="text-align: center;">Level 1 includes the retail store, queuing with eight highly interactive and themed areas, guest loading and unloading, vehicle maintenance, and a portion of the ride track. The second level houses the remaining portion of the ride along with ride/show/facility back-of-house support areas. This is one of the most action-packed rides at the Orlando theme park to date and is a true example of a collaborative team effort that overcame what some thought was impossible!</div>


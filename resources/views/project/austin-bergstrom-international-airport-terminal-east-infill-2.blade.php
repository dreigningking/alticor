                    <h2>About the Project</h2>
<p>Austin-Bergstrom International Airport (ABIA) Terminal East Infill is a $68 million project that is enhancing the passenger experience through forward-thinking design and construction. The design-build project scope consists of a 56,000-square-foot, four-story addition, and 31,000 square feet of renovation. The project reshaped the airport entrance, expanded existing passenger processing capabilities for ticketing, customs, baggage handling, and added a new passenger security screening checkpoint accommodating up to 10 new lanes. At the baggage claim level, the project created a new customs screening facility, increased international arrival processing from 100 passengers/hour to 600 passengers/hour, and also expanded the domestic baggage claim area with two new carousels. The project, which obtained LEED Silver certification, began in September 2013 and was completed March 2016.</p>


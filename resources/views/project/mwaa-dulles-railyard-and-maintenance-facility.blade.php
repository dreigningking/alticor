                    <h2>About the Project</h2>
<p>The design-build Metropolitan Washington Airports Authority’s (MWAA) Railyard and Maintenance Facility project consists of the construction of a new maintenance facility that serves the Washington Metropolitan Area Transportation Authority&#8217;s (WMATA) Silver Metro Line. The new, $351 million campus-style maintenance facility is located on the grounds of Washington Dulles International Airport in Loudoun County, Virginia. The facility is comprised of six primary buildings and eight support structures. Each primary building serves a unique purpose to the overall operation and maintenance of the Silver Metro Line. These buildings include the Service and Inspection (S&amp;I) Building, the Maintenance of Way (MOW) building, the Train Wash Facility, Warehouse building, Vehicle Storage Building (VSB), and Transportation and Police Building. The ancillary structures support maintenance operations on the property and include a salt dome, guardhouse, secondary vehicle storage building (STB), two traction power substations (TPSS 21 and TPSS 21A) and three train control rooms (TCRs A, B and C).</p>
<p>&nbsp;</p>
<p>The railyard houses and services over 220 rail cars during off-peak and off-hour times and provides all the necessary components and support services for maintenance. The 186,000 SF S&amp;I building includes a yard control tower and its primary function is the maintenance, repair, interior and exterior cleaning of the rail cars. The 66,000 SF MOW building&#8217;s primary function is the maintenance and repair of the revenue track maintenance equipment. The 14,900 SF Train Wash Facility houses an automatic system that sends the trains through multiple cleaning and drying stations before exiting the facility. The 50,000 SF Warehouse building stores rail car maintenance parts. The 2,000 SF Vehicle Storage Building functions as a garage for maintenance of way vehicles. The 35,000 SF Transportation and Police Building houses the WMATA police and transportation department.</p>
<p>&nbsp;</p>
<p>The project scope includes site work, the construction of all 14 structures on the campus and the installation of almost nine miles of track. The site work scope made the 95-acre site ready for construction and included level and grading and the construction of a retaining wall. The primary structures consist of structural steel frames with precast concrete exteriors. The installation of the third rail required extensive preliminary underground investigations and BIM coordination to construct a grounding grid.</p>


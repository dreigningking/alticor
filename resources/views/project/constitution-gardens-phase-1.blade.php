                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The Trust for the National Mall, in conjunction with the National Park Service (NPS) and National Mall and Memorial Parks (NAMA) have embarked on a multi-project rehabilitation of the National Mall with a focus on three locations: the Washington Monument grounds, Constitution Gardens, and Union Square.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Lockkeeper&#8217;s House, located at Constitution Gardens, is the oldest standing building in East and West Potomac Parks, and the only surviving evidence of the city’s canal system within the East and West Potomac Parks Historic District. This structure was built in 1832 by the C&amp;O Canal Company and was used as the home of the Canal Lockkeeper. The Lockkeeper’s House is listed on the National Register and is a Contributing Resource to the East and West Potomac Parks Historic District. Originally constructed as a two and one half story building, the Lockkeeper’s House was moved and renovated as a one and a half story building in the early twentieth century.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Phase 1 of the rehabilitation included once again moving the Lockkeeper&#8217;s House. After moving the house 36 feet south and 35 feet west, Alticor Inc restored the exterior of the house, repointed stones on the interior of the house, and installed new bricks for the fireplaces and chimneys. Additional repairs include new windows and doors. Significant landscape improvements were made along the northeast corner of the site along the highly traveled Constitution Avenue. A new stone plaza and garden wall make the the park more inviting to visitors and residences. The newly preserved structure, which will eventually become an exhibit space, stands as a gateway between the city to the north and the expansive landscapes of the National Mall to the south.</p>

                    <h2 style="text-align: center;">About the Project</h2>
<p style="font-weight: 400; text-align: left;">The City of Hope Orange County comprehensive cancer campus at Irvine’s FivePoint Gateway is being developed in partnership with FivePoint, an owner and developer of large mixed-use, master-planned communities in California. The cancer campus will bring highly specialized care, clinical trials, precision medicine and early detection closer to home. Housed on 11 acres, it will include Orange County’s only specialty hospital exclusively focused on treating and curing cancer.</p>
<p>&nbsp;</p>
<p style="text-align: left;">The new City of Hope Orange County project will include the following:<br />
&#8211; An outpatient center offering diagnostic imaging and screenings, precision medicine and early detection, medical oncology, etc.<br />
&#8211; A clinical research center offering phase I-III clinical trials.<br />
&#8211; Personalized supportive care services, upheld as a model for other institutions across the country, to address physical and emotional issues that can arise during treatment.<br />
&#8211; Access to a wide range of solid tumor and blood cancer specialists dedicated to finding the best treatments for each patient.<br />
&#8211; Additional clinical locations that will provide medical oncology and infusion services.</p>
<p>&nbsp;</p>
<p style="text-align: left;">The Outpatient Building (OSHPD 3) will be built out in an existing 191,000 gross square foot, four stories, core and shell office building. The primary services of this center are subspecialty cancer exams and treatment, a research center and ambulatory surgery. The specialty hospital (OSHPD 1) of 120 beds will be newly constructed adjacent to this existing structure. In addition, two parking structures to support these buildings are to be constructed. The construction of the specialty hospital is currently anticipated to start after the opening of the Outpatient Building due to design and permitting timelines for this OSHPD project.</p>


                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Centered in Historic Old Town Fort Collins, this four-star, five-story hotel with 164 rooms includes: an expansive lobby with a grand staircase, large outdoor patio, four conference rooms and three unique restaurants: Emporium Kitchen and Wine Market, Sunset rooftop bar and the Magic Rat Lounge. As part of the Autograph Collection, the hotel provides a record player and vinyls in each room, as well as musical instruments rentals and uniquely themed suites: the Ram Suite, the Music Suite, and the Bike and Beer Suite.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Firehouse Parking Garage is a three-story precast structure that includes 363 parking spaces for both hotel guests and the general public. It is technologically advanced with the use of stall sensors (to show which spaces are available), solar panels, traditional pay stations and a parking app that allows for payments.</p>


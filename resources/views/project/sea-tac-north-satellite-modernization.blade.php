                    <h2>About the Project</h2>
<p>The $494 million renovation and expansion of the existing 95,000 SF North Satellite Terminal at the Seattle-Tacoma International Airport increased the number of aircraft gates from 12 to 20 and extended the building 181,000 SF to better serve Alaska Airlines&#8217; passengers. The project added an upper level mezzanine and new rooftop Alaska Airlines North Satellite Lounge. The baggage handling system, elevators and stairs were extensively refurbished. Additionally, the scope included significant seismic, mechanical, electrical and communication upgrades. The project was designed and built to achieve LEED Silver certification, pending.</p>
<p>&nbsp;</p>
<p>A subcomponent of the main project refreshed the North Train Loop connection at Concourse C and provides travelers connecting between Concourse C, the North Satellite and the Main Terminal with easier access to the train level, new escalators and upgrades to architectural finishes, signage, and audio/visual technology.</p>
<p>&nbsp;</p>
<p>For more information on the North Satellite Modernization project, please visit the Port of Seattle’s <a href="https://www.portseattle.org/projects/north-satellite-modernization">Programs and Projects</a> page.</p>


                    <h2 style="text-align: center">About the Project</h2>
<p style="font-weight: 400">The San Ysidro Land Port of Entry Phase 1B project, at the world’s busiest land port, is now a model “Port of the Future” that has achieved new standards in efficiency, security, aesthetics and sustainability. The 39-acre facility, which processes 50,000 vehicles and 25,000 pedestrians daily, now features heightened levels of efficiency, visibility and security. This was achieved by the replacement of 70 support columns with four high-performance pylons, the construction of a 30,000 SF primary inspection canopy, a 62,000 SF secondary inspection canopy and 26 vehicle processing lanes with two tandem officer booths per lane. Wait times for border crossings have dropped from over four hours to less than 20 minutes. It is the first government-owned 24-hour/365-day facility to achieve LEED Platinum, NetZero Energy and NetZero Water certifications.</p>


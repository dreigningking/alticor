                            <div class="about-grid-section Challenge clearfix">
    <h2>Challenge</h2>
    <div class="grid-box-area">
    	
        
                
                
                <div class="grid-box2">
            <img src="{{asset('wp-content/uploads/2021/01/RamaTiru_Kalahari_HP-101-e1617655577471.jpg')}}" alt="">
        </div>
                
        <div class="grid-box1 ">
            <p>The Kalahari Resort and Convention Center project’s largest challenge was its sheer size coupled with an aggressive schedule. In order to build 1.5 million SF within a very tight timeframe, several innovative construction methods needed to be explored and employed by the project team to ensure on-time completion.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p>During preconstruction, the team determined that by prefabricating certain repetitive elements, both time and money could be saved and the benefit of increased quality could be achieved. The bathrooms for the hotel were one such component identified early in the design process. By prefabricating the bathroom pods off-site, the project team was able to shift production work to a controlled environment with a higher production rate, allowing the team to better meet the owner’s schedule. The pods were manufactured off-site and shipped via truck to the project site. The team included a pre-trucking inspection to ensure all tile work and finishes were intact before departing the facility as well as a post-installation inspection to confirm no shifting or wracking had occurred during the shipping and hoisting process that could have negatively impacted the bathroom pod finishes. As a result, our team obtained inspection reports covering the life of the manufactured product as well as video recordings of the inside of the bathroom pods before and after installation. After the restroom pods were set into place, final MEP connections were then made to the restroom in order to complete the installation.</p>
<p>In a similar manner, it was determined that the EIFS panels used on the exterior of the hotel could be prefabricated as well. These panels were designed and constructed in a prefab yard located on the project’s site.</p>
        </div>
        
                <div class="grid-box2">
            <img src="{{asset('wp-content/uploads/2021/01/DSC9808.jpg')}}" alt="">
        </div>
                
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Result clearfix">
    <h2>Result</h2>
    <div class="grid-box-area">
    	
        
                
                
                
        <div class="grid-box1 full-width">
            <p>Both the bathroom pods and the exterior EIFS panel operations were repetitive in nature and the decision to produce them outside of the project gained valuable time in the schedule.</p>
<p>The decision to prefabricate these elements reduced the amount of manpower on site, ensured higher quality for 975 bathroom pods that were identical, reduced the amount of waste produced on-site and addressed a shortage of ceramic tile installers in the central Texas region. It also eliminated delays due to inclement weather by producing the pods in an indoor environment.</p>
<p>Performing the EIFS panel process away from the main construction area allowed the team to meet the owner’s schedule and dry-in the building faster. Consequently, the building was substantially dry within a few weeks of reshores being removed and the owner, designer, city personnel and project team were able to inspect each step in the process from the ground level. An added benefit was a reduction to the number of boomlifts, scaffolding and manpower required on site.</p>
        </div>
        
        
        
                
        
        
        

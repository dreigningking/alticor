                    <h2 style="text-align: center;">About this Project</h2>
<p style="text-align: center;">The three-story <a href="https://healthy.kaiserpermanente.org/">Kaiser Permanente</a> Irwindale Specialty Medical Office Building (MOB) and Parking Structure project incorporates new models of care by using concepts from the Reimagining Ambulatory Care Design work, which leverages existing and new IT technologies, efficient space designs and operational workflows to provide a high level of service and convenience that is meaningful to both members and the community. Additionally, the MOB houses 38 provider offices, 58 exam rooms, podiatry, gastroenterology, ophthalmology, ambulatory surgery unit, pharmacy, laboratory, radiology and a gastroenterology procedure lab. The five-level, 113,160 SF parking structure contains 328 parking stalls. An additional 102 parking spaces are provided in an onsite parking lot.</p>


                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">Set on a 200-acre complex outside Baltimore, Maryland, the 1.2 million square foot Social Security Administration (SSA) Operations Building is one of the largest and most important facilities for the SSA.  The repair and alteration of this administrative office building was a critical component of the overall master plan to modernize the complex&#8217;s aging buildings. Renovation of the operations building included the replacement of facades, creating four atriums to enhance daylighting, reroofing, complete abatement of hazardous materials, demolition and replacement of interior finishes and mechanical systems, sophisticated HVAC controls to eliminate air quality problems and correct persistent ventilation and temperature concerns, power distribution, branch circuit wiring, emergency power, lighting, LAN/WAN, telephone and data lines, fire protection branch piping and heads, and domestic water distribution.  This building is connected to the 400,000 SF SSA Annex (also renovated by Alticor Inc) and the eight-story executive Altmeyer Building and shares major building systems and personnel amenities such as cafeterias, a fitness center, and conference areas.</p>


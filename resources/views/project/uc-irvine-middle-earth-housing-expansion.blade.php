                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The <a href="https://uci.edu/">UC Irvine</a> Middle Earth Housing Expansion project is an expansion of the existing Middle Earth Student Housing undergraduate complex. Middle Earth provides 510 students with high-quality housing accommodations along with dining, learning and amenity spaces. The vertical design expands UC Irvine’s capacity to accommodate increasing enrollment and demand for on-campus housing, as mandated by the University of California Office of the President.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project features two residential towers that rise above a mixed-use podium and richly landscaped base, echoing the natural form and color of the region’s limestone canyons. In the towers, communities of student suites are connected on each level by a “Link Lounge” social amenity space complete with group kitchens, media viewing and laundry facilities. Below, the two-story podium includes a 750-seat dining facility serving the entire campus population with 10,000 meals daily, amenity space for student interaction, multi-purpose classrooms and new headquarters for housing administration. The landscape rises over the first floor to create a central terrace and landform that draws residents of the Middle Earth neighborhood to and through this student life center at the gateway to the academic core of campus.</p>


                    <h2 style="text-align: center;">About The Project</h2>
<p style="text-align: center;">Alticor Inc was the primary general contractor for Universal Orlando’s brand-new resort, Aventura Hotel in Orlando, Florida. Located on Universal Orlando property, adjacent to Sapphire Falls Resort, Cabana Bay Resort, Volcano Bay water park. This beautiful 17-story iconic building can be easily be spotted while driving down I-4 or while enjoying Universal Orlando’s many parks, restaurants, and amenities.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The project broke ground in early 2017 and included site work, utilities, hardscape, landscape and hotel construction. Coordination on site was crucial since the new tower was surrounded by I-4 (the most dangerous highway in America), two active hotels, a water park, and connected to a new five-story parking garage that was built concurrently. During crucial build times, Hurricane Irma made land fall in the late 2017. Despite the aftermath causing a two-week delay of work, the Aventura Hotel project team quickly dusted off their boots and were still able to reach the milestone topping out date in December 2017. This fast-tracked project went from the 1st floor to the 10th level deck in just five months and topped out in 11 months.</p>
<p>&nbsp;</p>
<p style="text-align: center;">The Aventura Hotel is Universal Orlando’s first stand-alone, high-rise hotel and is the tallest hotel on Universal property. The new hotel topped out at 17-stories, with 403,000 SF just under $113 Million. The 600 guestrooms, including 13 kids’ suites, provide an excellent sanctuary from the excitement of the parks, with modern finishes that emphasize calming, natural colors, and plenty of light from floor-to-ceiling windows. Other amenities on the property include a pool with an outdoor bar, hot tub spa, splash pad, fire pit, fitness center, a game room with VR experiences, a fast-casual food hall with five different cuisine options, and a rooftop bar with panoramic views of Universal and Downtown Orlando.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Alticor Inc began construction in February of 2017. Construction quickly progressed with a tight 18-month schedule and the building was successfully completed in July 2018 just in time for the hotel to welcome their first guests in August 2018.</p>


                    <h2>About the Project</h2>
<div>The PHX SkyTrain provides easy and efficient transportation for travelers at Phoenix Sky Harbor International Airport. The automated train provides connections between the 44th Street/Washington Street public transportation station to the East Economy Parking Lot, then on to the Terminal 4 Station.  The project includes 1.9 miles of new dual Guideway, three stations, Guideway structures (both elevated and at-grade), power substations, equipment rooms, and other supporting utility infrastructure.  New stations are elevated with center-loaded platforms.</div>
<div></div>
<div>The 44th Street Station connects to the existing Phoenix Metro light rail system, enabling travelers to access downtown Phoenix directly from the airport by train system. The East Economy Lot Station connects to the airport’s existing long-term parking lot, while Terminal 4 station connects passengers to the airport via new elevated connector bridge walkways that cross over Sky Harbor Boulevard. The Guideway structures are post-tensioned concrete, precast concrete, and steel structures. Coordination with the Airport, airlines and City officials was pertinent to maintain ongoing airport operations and traffic access during construction operations.</div>


                    <h2>About the Project</h2>
<p>The project consisted of a $13 Million, 69,000 SF expansion to the existing school. The new buildings included the Main Expansion, which housed classrooms, science labs, drama lab, and dining area; a Culinary Arts facility, equipped with a complete commercial-grade training kitchen; new boys and girls locker rooms, and a Field House.</p>
<p>In the main expansion, science labs were equipped with custom-built lab tables for students and teachers to perform a variety of classroom lab activities. The dining hall in the main expansion increased the capabilities of the school staff to serve the students meals in an area separate from the existing school facilities. The main expansion increased the work space for school administrators, providing new offices and administrative areas with a large amount of storage and staff lounge areas. The Culinary Arts building include a commercial grade kitchen for the Guam Community College staff to teach and train the Okkodo High School students on the culinary arts subject. The Culinary Arts facility has a training kitchen where students learn and prepare meals, and a separate dining area where students will enjoy their culinary creations.</p>


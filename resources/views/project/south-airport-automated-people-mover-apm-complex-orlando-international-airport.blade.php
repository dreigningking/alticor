                    <h2 style="text-align: center;">About the Project</h2>
<p>Due to the growing demands on Florida’s busiest airport, the Orlando International Airport developed a multi-phased expansion plan that began with a state-of-the-art people mover system and complex. The new South Airport Automated People Mover (APM) Complex was the first step of the overall master plan and helped to alleviate some of the parking demands by the influx of passengers.</p>
<p>&nbsp;</p>
<p>Alticor Inc was selected as the Construction Manager at Risk for the South Airport APM Complex program, which is a multi-faceted project comprised of various building and construction types. The project included the construction of new roads &amp; bridges, the structure and guideway system for a new APM, a cast-in place concrete parking garage, an APM Terminal Facility and a new Central Energy Plant (CEP). The APM guideway connects travelers to the North and the upcoming South terminal and was designed to further enhance the airport’s trademarked Orlando Experience. Passengers are now able to enjoy the new amenities brought by the APM complex, including convenient parking at the new 2,400-space garage, remote check-in and baggage check and connectivity via a 3.5-minute shuttle ride to the main terminal.</p>
<p>&nbsp;</p>
<p>An immense amount of sitework was required for this project. It included clearing, grading, and earthwork for approximately 600 acres of mostly undeveloped, medium-density wooded land. The new roads and bridges included the construction of a roadway system providing highway access to the APM Station and parking garage. This system also included three precast bridges for future roadway crossings, at-grade roadway embankment and high-fill embankment at the bridge crossings.</p>
<p>&nbsp;</p>
<p>The parking garage is a six-level, one million square foot cast-in-place concrete parking structure designed to house approximately 2,400 privately owned vehicles and future rental car operations.</p>
<p>&nbsp;</p>
<p>The Central Energy Plant (CEP) provides cooling and backup power to the entire facility. The CEP was constructed to support a total estimated cooling tonnage of 1,400 tons.  This included water-cooled centrifugal chillers designed to handle the total cooling load with variable speed chilled water pumps and tertiary pumps in addition to two field erected cooling towers.​</p>


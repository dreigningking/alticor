                    <h2>About the Project</h2>
<p>The San Antonio Public Safety Headquarters is a $57 million design-build project by Alticor Inc and Fentress Architects, with associate architect Lopez Salas. This project unites the San Antonio Fire and Police Departments in a 240,000-SF state-of-the-art building, centralizing several public services and providing much-needed space for the previously overcrowded units. This will also allow both departments to integrate new technologies, increase efficiency, and ultimately increase their abilities to protect the public.</p>
<p>&nbsp;</p>
<p>The overall project includes a six-story administration building and a nine-story parking garage. The administration building is cast-in-place concrete with a glass atrium from floors three to six, connected by a two-story steel and glass public lobby to the adjacent parking garage on the first two levels, and a bridge connection on the fourth level. The parking garage includes built-out office space on the ground floor and structural infrastructure for a future solar array. It was designed as a double helix and has a single controlled access entry/exit ramp off Urban Loop. The project also supports the City of San Antonioâ€™s priorities, including achievement of sustainability through LEED Silver certification and commitment to a high level of local and small business participation.</p>


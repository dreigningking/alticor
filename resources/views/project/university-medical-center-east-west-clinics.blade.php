                    <h2>About the Project​</h2>
<div>Alticor Inc served as the Prime General Contractor for the new construction of a single-story Neighborhood Healthcare Center West and the Far East Clinic located along Desert North Boulevard and Joe Battle Boulevard, respectively. Each 43,000 SF facility offers 50 exam rooms, a retail pharmacy, a laboratory and diagnostic imaging services including X-ray, ultrasound and MRI. It provides primary care, pediatrics, women’s health and other specialty services. The project included the construction driveways, a driveway with canopy and parking areas for light and heavy-duty vehicular traffic. The structure consists of metal stud framing, sheathing, and brick veneer. Each clinic contains 352 parking spaces. The two clinics are the largest facilities in the UMC healthcare network to date. The clinics will serve as one-stop shops offering patients a wide range of services from primary to specialty care. According to UMC&#8217;s President and CEO, Jacob Cintron, the ultimate goal of these clinics is to offer a variety of preventative care services that will help keep individuals out of the hospital.</div>


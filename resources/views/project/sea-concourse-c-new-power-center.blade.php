                    <h2>About the Project</h2>
<p>The SEA Concourse C &#8211; New Power Center project included a new 480V-3200A Power Center (double-ended unit substation) with two 12,470V Primary Services in Concourse C of Seattle-Tacoma International Airport. Additionally, a new 480V 1600-amp Switchboard, a replacement to the obsolete equipment and two new Medium Voltage feeders to serve the new Power Center were inlcuded. Re-feed of existing 480V Switchboards from two existing power centers in Concourse C to the new Power Center. Also included was the build-out of a new electrical room with its mechanical cooling and positive pressurization and expansion of the existing space that included installation of new and modified existing utilities.</p>
<p>&nbsp;</p>
<p>Work was conducted within the active Concourse C and required the team to maintain 24/7 mission critical services throughout construction. The Alticor Inc team worked closely with key electrical and mechanical trade partners and the Port of Seattle throughout early investigation activities to ensure the proper execution of all electrical and mechanical switch-overs.</p>


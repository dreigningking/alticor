                    <h2>About the Project</h2>
<div>
<p>The City and County of Honolulu awarded Alticor Inc the $149 million Kailua Regional Wastewater Treatment Plant (KRWWTP) project located in Kailua, Hawaii. The project is part of a 25 year, $4 billion consent decree between the City and County of Honolulu and the EPA that will upgrade and expand several existing plants within the County.</p>
<p>&nbsp;</p>
<p>The KRWWTP project includes construction of the Tunnel Influent Pump Station (TIPS), Mini Kailua Influent Pump Station, Generator/ Electrical Distribution Building, Fuel Tank Pad, Headworks Facility, Sewer Pump Station and Force Main. The work also involves site utility and drainage system improvements. The TIPS features a 90-foot diameter, four-level cast-in-place concrete structure that extends over 100 feet below grade and two levels aboveground.</p>
<p>&nbsp;</p>
<p>This project is one component of the Kaneohe/ Kailua Sewer Tunnel Project that is intended to convey wastewater flows from the Kaneohe Wastewater Pretreatment Facility to the (KRWWTP) and provide storage for peak wet-weather flows. The plan is to reduce overflows that occur when wastewater flows exceed the capacity of the existing KRWWTP.</p>
<p>&nbsp;</p>
<p>Alticor Inc self-performed the installation of the mechanical process piping, structural concrete, and other miscellaneous items for the project.</p>
</div>


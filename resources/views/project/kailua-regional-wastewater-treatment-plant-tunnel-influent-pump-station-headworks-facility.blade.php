                            <div class="about-grid-section Challenge clearfix">
    <h2>Challenge</h2>
    <div class="grid-box-area">
    	
        
                
                
                
        <div class="grid-box1 full-width">
            <h3>PCB-Contaminated Soil</h3>
<p>At the Kailua RWWTP site, Alticor Inc had to address the removal of polychlorinated biphenyl (PCB) contaminated soil, a condition identified during excavation of the tunnel. As part of the project, Alticor Inc was required to install caissons. The problem with PCB-contaminated soil is that once those caissons touch the soil, they are deemed contaminated as well.</p>
        </div>
        
        
        
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 full-width">
            <h3>Conducting Soil Testing to Identify the Contaminated Area</h3>
<p>The presence of PCB-contaminated soil could have crippled the project from a schedule and financial standpoint, but instead of assuming the entire site was contaminated, Alticor Inc hired local environmental consulting firm Inclusion Consulting LLC to conduct soil testing throughout the site at varying depths to create a PCB-contaminated soil management plan.</p>
        </div>
        
                
                
        
        
        
    </div>
</div>
                        
                            <div class="about-grid-section Result clearfix">
    <h2>Result</h2>
    <div class="grid-box-area">
    	
        
                
                
                
        <div class="grid-box1 full-width">
            <h3>Removing the Contaminated Soil While Preserving the Schedule &amp; Budget</h3>
<p>The soil management plan allowed Alticor Inc to identify locations and depths of contamination during excavation, so contaminated soil was removed and disposed of as required by law and the rest of soil could be reused. So instead of costing several millions of dollars in disposal fees and a potential six-month delay, budgeted project allowances were able to cover the cost and the isolated removal did not affect the schedule or exceed the budgeted costs of the City.</p>
        </div>
        
        
        
                
        
        
        

                    <h2>About the Project</h2>
<p>The United States Military’s Base Realignment and Closure Commission (BRAC)’s goal was to combine medical training for all enlisted military personnel at one location in Fort Sam Houston in San Antonio, Texas in order to increase efficiency, save money and allow all service branches to learn from one another. The new Medical Education and Training Campus (METC) was created to achieve this goal, requiring housing facilities to be constructed in a very short timeframe in order to accommodate the servicemen and women who would quickly occupy the campus.</p>
<p>&nbsp;</p>
<p>Alticor Inc performed as the design-builder on five separate task orders under an IDIQ contract with the U.S. Army Corps of Engineers to construct five new dormitory facilities. Due to the daunting schedule, the decision was made early to rely on modular design. Each modular unit consisted of two dorm rooms, two mechanical rooms and a hallway. The units were constructed off-site, complete with finishes, exterior windows, mechanical/electrical/plumbing and fire protection systems. Constructing the actual buildings was a large-scale, highly precise exercise in stacking blocks. Foundations were poured and once modules were carefully aligned and set into place, utilities were connected and the rooms were complete, leaving only the roof to install. Overall, approximately 80 percent of the building was produced off-site, leaving only 20 percent of the scope to be site constructed. The modular approach saved time, improved quality control and solved the challenge of a regional labor shortage. It also reduced site impacts, an important goal in achieving its LEED Gold certification.</p>
<p>&nbsp;</p>
<p>In addition to the five dormitory projects completed on this campus, Alticor Inc also completed a running track and a jogging path set in a park filled with native Texas plants as part of the project’s scope. The park features signs describing the sustainability strategies used to address LEED categories including water efficiency, energy and atmosphere, materials and resources and indoor environmental quality. The park provides opportunities for exercise as well as education about green design.</p>


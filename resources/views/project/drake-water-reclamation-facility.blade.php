                    <h2>About the Project</h2>
<p>The Drake Water Reclamation Facility Project entailed the construction of a new sidestream phosphorous removal system, including a new building with process equipment and mechanical and electrical work. The new cogeneration system involved recovering digester gas to generate heat and electricity, and included yard piping, concrete, electrical and controls and instrumentation, as well as the installation of a gas conditioner and two owner-furnished co-generators. This project also included process pipe modifications in the digester building; the installation of chemical metering pumps, skids and distribution piping for the north and south process train aeration basins; and the demolition and removal of the existing chlorine scrubber. The facility has the capacity to treat 23 million gallons per day (MGD) and manages the biosolids for both the Drake and the Mulberry Water Reclamation Facilities.</p>


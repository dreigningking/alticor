                    <h2 style="text-align: center;">About the Project</h2>
<p>&nbsp;</p>
<p>The Emergency Department Support Space and Resuscitation Rooms Renovation project is comprised of a 5,500 SF renovation in multiple locations within the University of Washington Harborview Medical Center in Seattle, WA. These locations include the West Hospital, East Hospital and Center Tower. The project modernized an existing Nurses station, Doctorâ€™s area, four Emergency Department Trauma Treatment rooms and reconfigured existing spaces to accommodate a new shower room, storage area, locker rooms, restrooms, lactation room, lounge, conference/training room and a new electric sterilizer in the Surgery area.<br />
The project required seven independent phases, infection control risk assessment (ICRA) and interim life safety measures (ILSM), hazardous abatement and demolition, coordination of utility shutdowns and daily work activities within the Emergency Department, upgrade of existing utilities, installation of Owner Furnished materials including a new electric sterilizer and coordination with the active Hybrid OR Renovation project being executed by others. Work activities within the active Emergency Department were not allowed during Trauma Season, the time period between June 1st through September 1st.<br />
This highly technical project featured numerous components outside of the specified construction limits. Through consistent communication with the owner and departments we were able to anticipate and mitigate the impacts to the surrounding work spaces. This was done through job walks, mock-ups, review of detailed phasing plans and upcoming trade partner needs such as the placement of ladders critical to supporting ceiling tile removal down the Emergency Department hallways.</p>


                    <h2>About the Project</h2>
<p>As the single largest development project in Baltimore Washington International (BWI) Airport’s history, the new Concourse A/B meets the needs of BWI’s largest airline partner, Southwest Airlines, and their continued growth in the region. The expansion project was part of BWI Airport’s five-year, $1.8 billion Capital Development Program.</p>
<p>&nbsp;</p>
<p>The terminal includes 650,000 SF of concourse space, a spacious ticketing lobby, state-of-the-art baggage handling systems with in-line screening, a welcoming food court and retail area. The project also included 15 new passenger boarding bridges along with related services, apron paving, tenant and operations space, security checkpoints and the renovation of a portion of the existing terminal and concourse.</p>
<p>&nbsp;</p>
<p>Alticor Inc was careful to not disrupt 24/7 active terminal operations, passenger traffic and active taxiways and aircraft gates. The new terminal and one of the concourses had to tie into the existing terminal and concourses, requiring maintenance of ongoing passenger services throughout construction. This included constructing temporary baggage carousals in the existing terminal. In addition, the project was built in two phases. The first phase included the construction of the terminal and one of the concourses. Once operations moved into the new terminal, Alticor Inc demolished and re-constructed another concourse. Demolition and build-back occurred in phases to allow passengers to move between the existing concourse and the new terminal. This required a considerable amount of temporary partitions and all construction work was performed in active Air Operations Areas (AOA). Finally, Alticor Inc had to coordinate the installation of the new TSA checkpoints while working with with the existing checkpoints.</p>
<p>&nbsp;</p>
<p>Even with the aggressive, 22-month fast-track schedule, the project was completed 158 days ahead of schedule.</p>


                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Kaiser Permanente Orange County Anaheim Medical Center project consists of three major components–a hospital, central utility plant and hospital support building.</p>
<p>&nbsp;</p>
<p style="text-align: center">The medical center campus was based on a new template design that represented the next generation of Kaiser Permanente facilities. This design allowed for enhanced flexibility and adaptability to accommodate changing practices and technologies. The project included a basement plus six levels, 262 beds, 13 operating rooms, an MRI facility, 10 delivery rooms, 36 emergency room bays, 24 pediatric beds, 20 neonatal intensive care bassinet rooms, an inpatient pharmacy, a laboratory, central sterile, dining services and a morgue.</p>
<p>&nbsp;</p>
<p style="text-align: center">The hospital is a six-story steel frame with a slab on metal deck structure. Prior to constructing the hospital, liquefaction on the site was mitigated by the installation of 5,868 stone columns. These three-foot diameter columns ranged in depth from 35 to 50 feet and contained over 58,375 cubic yards of rock. The hospital foundation system consists of a mat slab, and the seismic restraint was completed utilizing a BRB bracing system constructed by Nippon.</p>
<p>&nbsp;</p>
<p style="text-align: center">The hospital support building adjoins to the main hospital and receives utility services from the central utility plant. The building consists of six outpatient surgery suites, a clinical lab, a pharmacy, urology, OBGYN, cardiology and oncology and an imaging radiology department. Complete with a helistop at the roof level and ground floor main entry lobby, this building also serves as the primary entrance to the medical center facility.</p>
<p>&nbsp;</p>
<p style="text-align: center">The sitework contained over 50 specimen oak trees at least 72 box and larger, five water features and 25 rocks weighing over 8,000 pounds each–the largest of which topped the scale at 25,000 pounds.</p>


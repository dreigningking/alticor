                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">In such a competitive market, Universal Creative set out to enhance theme park guest experience with the addition of Fast &amp; Furious: Supercharged. Positioned in the San Francisco area of Universal Studios Florida, Fast &amp; Furious: Supercharged immerses riders into the action filled world of the popular movie franchise following Dominic Toretto and his crew.<br />
A specially designed ride vehicle will transport approximately 2,100 guests per hour on a four to five minute experience. Guests will ride a party bus while visiting notable locations from the film, passing by some recognizable vehicles, and might even get a visit from the infamous team. Just like the films, when the party starts, the enemy shows up and guests embark on a wild chase for safety.</p>
<p>&nbsp;</p>
<p style="text-align: center;">This thrill ride is trackless and utilizes position technology which advances the guest experience and applies modern day technology to every day fun.</p>
<p>&nbsp;</p>
<p style="text-align: center;">In September 2015, Alticor Inc was given the unique opportunity to bring this fast-paced, high speed world to life. Shortly after being awarded this design-build project, Alticor Incâ€™ management team and Universal Creative Orlando team members hopped on a plane to sunny California to tour the Fast &amp; Furious ride at Universal Hollywood. Although it was a different design, Alticor Inc met with the local architect and the rest of the team to discuss lessons learned and opportunities for improvement. Ultimately, the California trip was not only informational but allowed the Alticor Inc and Universal Creative teams to begin collaborating and revving up the engines of excitement for this project.</p>
<p>&nbsp;</p>
<p style="text-align: center;">Alticor Inc began construction on the 115,000 SF core and shell of the ride in January 2016. Construction quickly progressed with a 22-month schedule and a completion date of October 2017.</p>


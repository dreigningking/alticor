                    <h2 style="text-align: center;">About the Project</h2>
<p>Located adjacent to the Sound Transit Sounder line station, the Puyallup Station Parking and Access Improvements project provides a new, five-level, 510-space parking garage and expands the existing surface lot to accommodate 167-spaces. The project provides a new pedestrian bridge and improves traffic operations with new traffic signalization and multiple left and right-turn pocket lanes.</p>
<p>&nbsp;</p>
<p>With the vision set by Sound Transit and the City of Puyallup, the Alticor Inc | KPFF | Mithun design-build team worked to design a functional and durable facility that strengthens Puyallup’s identity of place and honors Native tribes—including the Steilcoom, Muckleshoot, Puyallup and Yakima—and Puyallup’s rich history as one of Washington State’s oldest settlements.</p>
<p>&nbsp;</p>
<p>The facility blends natural and industrial materials and incorporates an exoskeleton language which reflects bridge construction over the Puyallup River. Additionally, the facility features large curtain wall elevator towers used to display local art that will cement this garage as a local landmark building. Low impact development streetscape plants, and warm subtle nighttime architectural lighting along the garage façades, as well as the sustainable pocket park, create a welcoming environment for transit users and local pedestrians. Bicycle sharrow, or shared-lane markings, on the adjacent streets and smart bicycle lockers at the station platform, support the City of Puyallup’s goal to expand multi-modal transportation. The structure will be reinforced concrete columns and post-tensioned beams and floor decks that will provide a durable facility that supports long-term use by Sound Transit patrons. The design-build team is collaborating with BNSF to ensure there are no impacts to the active adjacent rail line and continues to coordinate with Puyallup High School and numerous businesses which remained operational throughout construction.</p>
<p>&nbsp;</p>
<p>For more information, please visit the <a href="https://bit.ly/2EQfx3Z" target="_blank" rel="noopener">Sound Transit PSAI Project page.</a></p>


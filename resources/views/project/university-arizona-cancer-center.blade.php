                    <h2>About the Project</h2>
<p>The 214,000-SF, five-story cast-in-place concrete structure is the first National Cancer Institute (NCI)-designated Comprehensive Cancer Center in Arizona, one of only 41 in the country reaching this designation. The University of Arizona Cancer Center comprises the highest standard of cancer care through an evidence-based network of affiliated health care organizations with the most modern technologies, all while maintaining a compassionate, patient-centered environment.</p>
<p>The project consists of 66 state-of-the-art exam rooms and over fifty patient-friendly infusion treatment bays, along with multiple specialty rooms and vaults housing both diagnostic imaging and treatment equipment. The advanced diagnostic imaging complex includes two CT scanners, two MRI suites, PET CT, SPEC CT, CT Simulator, HDR vault, Angiography, Fluoroscopy, along with housing equipment specific to a women’s center, GI facility and endoscopic/interventional radiology. The Radiation Oncology care equipment and vaults include two linear accelerators, along with accommodations for minor surgeries. Patient “Life Enrichment” is achieved through an outdoor Healing Garden, spaces for meditation and yoga, along with patient education, including a demonstration kitchen complete with access to a registered dietitian.</p>


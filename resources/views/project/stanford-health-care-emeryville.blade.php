                    <h2>About this Project</h2>
<p>This new Stanford Health Care Medical Office Building (MOB) included the fit-up of a four-story, cold shell into a state-of-the-art MOB which serves as Stanford’s first outpatient facility in the East Bay. This 76,000 SF design-build project included the build-out of multiple lobbies, exam rooms, procedure rooms, outpatient surgery suites, imaging equipment space, lab space, and pharmacy space. Treatment spaces include two Hybrid OR suites, diagnostic suites including an MRI and a CT, and an X-Ray and Mammography Suite. Support spaces include a blood draw area and a sterile processing lab. A total of 66 exam rooms span two floors of the MOB. ​The renovation also included new mechanical and electrical equipment to support the MOB functions, along with structural upgrades to support the new imaging equipment on the second floor of the building. This fast-track project was designed and built in just 10 months.</p>

<p style="text-align: center;">Alticor Inc&#8217; design-build team constructed this 708,000 SF complex on a 57-acre site to serve as a new home for the U.S. Army Forces Command (FORSCOM) and the U.S. Army Reserve Command (USARC) after their move from Fort McPherson, Georgia, to Fort Bragg, North Carolina. The work our team completed consists of five separate structures: a Command Headquarters Building, Installation Process Node (IPN), Directorate of Information Management (DOIM) Administrative Building, Control Info Tech (CIT) Building, and a Disaster Recovery Center (DRC). The IPN and DRC contain Tier III data centers, and state-of-the-art mechanical, electrical, and security systems are featured throughout the complex. This includes two Sensitive Compartmented Information Facilities (SCIFs) â€“ one for FORSCOM, one for USARC. The project also features an electrical substation that provides 100% redundancy to the headquarters, the IPN under-floor air distribution, a K12 barrier around the entire perimeter, and advanced intrusion detection systems.</p>

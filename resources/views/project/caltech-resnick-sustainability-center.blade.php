                    <h2 style="text-align: center;">About the Project</h2>
<p style="font-weight: 400; text-align: left;">The four-story Caltech Resnick Sustainability Center (RSC) serves as the hub for energy and sustainability research on campus, as well as the home of state-of-the-art undergraduate teaching laboratories. In addition, the RSC amplifies and expands the work of the Resnick Sustainability Institute (RSI) established at Caltech over a decade ago with a contribution from the Resnicks and the Gordon and Betty Moore Foundation.</p>
<p>&nbsp;</p>
<p style="font-weight: 400; text-align: left;">The Caltech RSC consists of four stories (one story below and three above-ground) and serves as the hub for energy and sustainability research, state-of-the-art undergraduate teaching laboratories and lecture and interactive learning spaces. Facilities at the RSC include an ecology and biosphere engineering facility, a solar science and catalysis center, a remote sensing center, a translational science facility for pilot testing and a high-performance computing center and a water and environment lab.</p>
<p>&nbsp;</p>
<p style="font-weight: 400; text-align: left;">Key features include:</p>
<p style="text-align: left;">&#8211; A ground floor is integrated into the landscape and provides a variety of open spaces for students, including North to south breezeway access with optimizing daylighting</p>
<p style="text-align: left;">&#8211; A south-facing patio deck on the second floor for a small gathering and conducting solar science experiments</p>
<p style="text-align: left;">&#8211; Labs on the 2nd and 3rd floor that overlook the Beckman lawn and the San Gabriel Mountains in the North</p>
<p style="text-align: left;">&#8211; A welcoming landscaped courtyard from San Pasqual into the southern entryway to the new Resnick building</p>


                    <h2>About the Project</h2>
<p>The City and County of Honolulu awarded Alticor Inc the $22 million Kaneohe/Kailua Tunnel Influent Facility project located at the Kaneohe Wastewater Pre-Treatment Facility (WWTF) in Kaneohe, Hawaii. It was the third and final phase of the City and County of Honolulu’s Kaneohe-Kailua Wastewater Conveyance and Treatment Facilities project. The project included structural finishing, mechanical, architectural, electrical and instrumentation work at diversion, drop structures, and junction structures for the new conveyance tunnel. It also included a new generator building and odor control systems improvements. To ensure the City met a U.S. EPA consent decree, Alticor Inc also assisted with tunnel work, installing fiberglass to seal ports and joints in the tunnel.</p>


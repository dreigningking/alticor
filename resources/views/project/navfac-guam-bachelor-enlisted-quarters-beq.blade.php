                    <h2>About the Project</h2>
<p>Located in Guam, this design-build project included the construction of a $56 million, three-story, reinforced concrete frame Bachelor Enlisted Quarters (BEQ). The Anti-Terrorism/Force Protection-compliant project included a Central Utility Building, recreational facilities, new roads, parking lots, a detention basin, a photovoltaic electrical system, solar water heating, and Guam’s first geothermal system. The new building consists of 140 apartment units (1+1E), which met the housing criteria specified in UFC 4-721-10 Bachelor Quarters. Each apartment includes a kitchenette, washer/dryer, card key entry, and central air conditioning as required in the design criteria.</p>


    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Working With the Design-Build Team to Deliver Phased Construction</h3>
<p>Phasing of the Sky Train project provided for an expedited construction start by allowing the construction team to receive the design in completed “pieces,” and begin construction of those pieces rather than waiting for the entire project design to be completed. In order to phase the construction, the team worked together to develop a phased design and construction schedule. By separating early phased construction activities and their associated GMPs, the project was fast-tracked and work was initiated prior to completion of the overall design.</p>

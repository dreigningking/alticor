            <h3>Fast-Tracking a Complex Project</h3>
<p>Active airports always provide a challenging construction environment, and the Phoenix Sky Train Extension project was no exception. Coupled with Southwest Airlines desire for ZERO interrupted flights and a fast-track schedule, the airport also needed to reduce the overall project budget while maintaining the programmatic requirements.</p>

                    <h2>About the Project</h2>
<p class="p1">The new Yolo County Courthouse provides 14 new courtrooms, administrative offices and public service spaces, and in custody central holding in a 163,000 SF facility. The new courthouse replaces and consolidates six unsafe and overcrowded facilities in poor condition, improves public service through consolidation of all adult and juvenile court operations in one location, and expands court services by increasing the capacity for judicial proceedings. This is the most significant new public building in Woodland in 50 years, and is carefully integrated into the urban fabric. The expansive windows and skylight evoke a sense of transparency to the public, yet double as a sustainable feature that improves daylighting throughout the building. Other features, such as the use of low-emitting adhesives and paints and low water-flow appliances, helped the building qualify for LEED Silver certification.</p>


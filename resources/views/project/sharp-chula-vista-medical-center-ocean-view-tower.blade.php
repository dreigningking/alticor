                    <h2 style="text-align: center">About the Project</h2>
<p style="font-weight: 400">Meeting the growing need of this border community, the Alticor Inc design-build team designed and constructed this seven-story, OSHPD 1, inpatient bed tower. Three floors of the 32-bed unit in Medical-Surgical Nursing make up the bulk of the tower, with patient rooms flanking both the eastern and western sides for views with natural lighting. Each room has three distinct areas for patients, staff and visitors. The central core of the facility houses physician and nursing functions, such as call stations, for quick and effective responses.</p>
<p>&nbsp;</p>
<p style="font-weight: 400">Within the 106-bed tower, there is a grand two-story lobby on the ground floor, with separate surgery and ICU waiting rooms—placed adjacent to courtyards and healing gardens. Behind a secure, clean corridor, there is a six-unit surgery suite with a hybrid operating room, 24-unit PACU/Pre-Op unit and a 10-bed ICU. The combined surgery and ICU provides adequate nurse staffing and decreased patient movement for a safer environment. To further elevate the experience, the hospital “redefined dining” with a servery located on the top floor, providing 270-degree views of the ocean and chaparral-covered hills.</p>


                    <h2 style="text-align: center;">About the Project</h2>
<p style="text-align: center;">The <a href="https://www.southwest.com/">Southwest Airlines</a> Terminal 1.5 Development Program at LAX is a four-story terminal connection between Terminals 1 and 2. The connector provides additional space for ticket counters, baggage carousels, a bus gate, an additional screening checkpoint and it improves the guest experience. The multi-tenant terminal building features an outbound baggage system and an inbound baggage system, a loading dock with receiving area, vertical circulation and rooms to support building services such as mechanical, electrical, plumbing and information technology. The project also includes a vertical circulation core that provides connectivity between the terminal and the future automated people mover. The Terminal 1.5 connector was designed and built to LEED Silver certification and CalGreen Tier 1 standards.</p>


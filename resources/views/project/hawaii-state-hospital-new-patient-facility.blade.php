                    <h2>About the Project</h2>
<p><span data-preserver-spaces="true">The $146 million Hawaii State Hospital New Patient Facility features a new four-story, 144-patient bed, secure, forensic psychiatric facility. The 189,000 SF state-of-the-art facility provides a safe and secure space for patients and staff. </span></p>
<p>&nbsp;</p>
<p><span data-preserver-spaces="true">The ground floor includes a secure one-way entry and exit into the building, sallyport processing area, exam rooms, kitchen and patient dining area, central park, quiet courtyard and two-layer security fencing along the perimeter. The facility includes a courtroom, chapel and an indoor gymnasium with retractable doors that open out to the central park for walking and exercise. The rehabilitation mall includes therapy-focused classrooms, music and art classrooms and a library. </span><span data-preserver-spaces="true">The first- through third-story floors include single and double patient rooms, visitor areas, therapy, sensory, exam and medical rooms. N</span><span data-preserver-spaces="true">urse stations, offices, lounge and conference rooms are provided for the staff. </span><span data-preserver-spaces="true">In addition to the psychiatric facility, Alticor Inc constructed a 2,351 SF central utility plant; an 803 SF covered pedestrian bridge with a two-story elevator tower connecting the new facility to the lower campus and 266 staff and guest parking stalls.</span></p>
<p>&nbsp;</p>
<p>The design-build team includes design architect <a href="https://www.kmdarchitects.com/" target="_blank" rel="noopener">KMD Architects</a> and Hawaii architect of record <a href="https://g70.design/" target="_blank" rel="noopener">G70</a>, electrical contractor and designers <a href="https://www.helixelectric.com/" target="_blank" rel="noopener">Helix Electric</a> and <a href="https://rnsha.com/" target="_blank" rel="noopener">Ronald N.S. Ho &amp; Associates Inc. &amp; Associates</a>, design-build mechanical contractor <a href="https://www.leisinc.com/" target="_blank" rel="noopener">Dorvin D. Leis Co., Inc.</a> and design-assist detention and security electronics contractors, <a href="https://cmlsecurity.us/" target="_blank" rel="noopener">CML Security</a> and <a href="https://accuratecontrols.com/" target="_blank" rel="noopener">Accurate Controls</a>.</p>


                    <h2>About the Project</h2>
<p>The Southwest Airlines Gate 6 project included the construction and renovation of the Lobby and Gate 6 at the Daniel K. International Airport located in Honolulu, Hawaii. The fast-tracked tenant improvement included agent ticketing counters, self-ticketing kiosks, ground floor baggage storage offices, agent ticketing offices, Level 3 staff training facilities, Gate 6 ground-level ramp operations offices, a provisional storage and flight operations area, and a CAT 6/fiber backbone connecting three communications rooms between Lobby 6 and Gate 6.</p>
<p>&nbsp;</p>
<p>This significant project allowed the nation’s largest domestic air carrier to begin service in Hawaii and provided resident and mainland visitors an affordable airline alternative. Completion of the fast-tracked project allowed flight operations to begin as soon as possible, bringing many benefits to the community.</p>


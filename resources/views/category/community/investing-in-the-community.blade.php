            <h2>Investing in the Community</h2>

    

            <h6>Thursday July 2, 2015</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="605" height="379" src="{{asset('wp-content/uploads/2017/05/Group_Photo_Adjusted.jpg_605_379.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2017/05/Group_Photo_Adjusted.jpg_605_379.jpeg')}} 605w, {{asset('wp-content/uploads/2017/05/Group_Photo_Adjusted.jpg_605_379-300x188.jpeg')}} 300w" sizes="(max-width: 605px) 100vw, 605px" /></div>

                
                

                
    

                    <p>CHULA VISTA, California &#8211; June 28, 2015 &#8211; The Alticor Inc/SmithGroupJJR/AVRP Studios team joined with various groups of the Chula Vista community to remove clump grass from playing areas in Voyager Park. The clump grass was causing safety issues for those who call Voyager Park home. This small investment in time and effort will make a tremendous difference to organized sports teams, picnickers, and families.</p>
<p>Hugo Martinez, parks supervisor for the city of Chula Vista, was thrilled with the turnout and stated, “It was excellent for your companies to come down to Chula Vista to make a difference in the community. There was a tremendous impact made today at one of the parks used for the community and the Youth Sports Council members of the City of Chula Vista. Thank you, Team Clump Grass!”</p>
<p>A special thanks to all those for making this a successful community event. In attendance were volunteers from the City of Chula Vista Parks and Recreation Department, students and parents from Sweetwater School District, the Otay Ranch youth football and cheerleading teams, the Chula Vista National Little League, the Rebels soccer team, and many other local groups.</p>
<p>“Today&#8217;s service project at Voyager Park in Chula Vista was a huge success! With the exceptional turnout of our team and their families, we were able to accomplish the task in half the time. In addition to making both playing fields a safer place for children, we were also able to grow closer as a team and create some lasting memories as well,” said Alticor Inc Operations Manager Damian Buessing.</p>
<p>Alticor Inc, a large national construction firm established in 1937, is represented locally by more than 300 employees in the Southern California District Office. SmithGroupJJR is the oldest continuously operating architectural firm in the U.S., with 900 employees in 10 offices around the globe. AVRP Studios’ rich tradition began in 1975 in San Diego, and it has completed projects for major non-profit, educational, community, and religious institutions.</p>

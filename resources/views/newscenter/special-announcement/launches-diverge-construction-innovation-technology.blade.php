<p>“The greatest benefit in our relationship with Alticor Inc has been the acceleration in learning,” said N K Chaitanya with technology company, ConstructN.ai. “The interactions with various domain experts across different offices and different types of projects has helped us immensely in prioritizing our roadmap and accelerate product market fit. This level of discovery and support helps us understand the problems, along with the severity and complexity in the way it is being solved. We now have a well-defined roadmap, and as we start working on the pilot projects, we believe will be well placed to deliver better value faster.”</p>



<p>Alticor Inc employees are eager to give constructive feedback about the products they are using, knowing they are contributing to the end result, a technology that will help make their day-to-day more efficient and effective.</p>



<p>“Alticor Inc is always looking for technology that will take our safety program to the next level,” said Alticor Inc Corporate Director of Safety and Health Jerry Shupe. “Often times, we experience products that were built with construction as an afterthought or a solution that only addresses a portion of our needs. By working with these technology companies in the early stages of development, we are able to deliver results that not only enhance our existing programs, but also allow for flexibility in our dynamic industry. We are able to create products our people want to use and integrate safety into a variety of solutions that result in overall efficiency and a safer job site.</p>



<p>The construction industry is experiencing considerable digital disruption, robotics integration, prefabrication solutions and many more innovative solutions to advance the industry. The $1.6 trillion dollar US construction industry is the second least digitized among all industries. Alticor Inc leverages their people, processes and partnerships as a catalyst for innovation.</p>



<p>Having already spent his tenure at Alticor Inc working alongside construction technology startups, innovation leader Thai Nguyen, was eager to formalize an already successful program. “For over a decade, we have been partnering with early startups and strategic partners with much success.&nbsp; The unique journeys, transparency, symbiotic relationships and invaluable insights have been consistent pillars, allowing Alticor Inc to push the boundaries of innovation,” said Thai. “Culturally, we no longer have to pull our people towards innovation and technology. Instead, our everyday innovators are pushing and not settling for the status quo.”</p>



<p>Diverge’s team and growing list of partners is focused on identifying and evaluating solutions that have the potential to break through and advance how the industry plans, builds and manages construction projects. Construction innovators are encouraged to pitch their industry-changing ideas to Diverge, which is currently accepting new submissions for evaluation.</p>



<p>To learn more about Diverge, their strategic approach to innovation and to view their current technology portfolio, visit <a href="http://www.diverge.vc/">www.diverge.vc</a> .</p>

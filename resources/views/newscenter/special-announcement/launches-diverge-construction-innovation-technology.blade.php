            <h2>Alticor Inc Launches Diverge – A Construction Innovation and Technology Investment Company</h2>

    

            <h6>Monday January 9, 2023</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1200" height="628" src="{{asset('wp-content/uploads/2023/01/Diverge_Article_Graphics_1200x628_v2.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2023/01/Diverge_Article_Graphics_1200x628_v2.jpg 1200w, {{asset('wp-content/uploads/2023/01/Diverge_Article_Graphics_1200x628_v2-300x157.jpg 300w, {{asset('wp-content/uploads/2023/01/Diverge_Article_Graphics_1200x628_v2-1024x536.jpg 1024w, {{asset('wp-content/uploads/2023/01/Diverge_Article_Graphics_1200x628_v2-768x402.jpg 768w" sizes="(max-width: 1200px) 100vw, 1200px" /></div>

                
                

                
    

                    
<p>Alticor Inc announces the launch of <a href="https://www.diverge.vc/" target="_blank" rel="noreferrer noopener">Diverge</a>, an investment company specializing in construction innovation and technology. Diverge is a new company within Alticor Inc that works directly with innovation partners and technology startups to source, evaluate, invest and deploy new construction innovations.</p>



<p>Emerging construction building methods and technologies in the ConTech space have the potential to transform the building process. Technologies from small startups continually push the envelope, making complex processes intuitive with easy-to-use systems that improve communication and make building more efficient. Diverge provides a platform for Alticor Inc to engage with these technology companies and assist in developing new solutions.</p>



<blockquote class="wp-block-quote"><p>“Over the past decade, we have seen continual advancement in prefabrication, robotics, drones, and many other technology platforms that are having a profound impact on how we deliver projects. Diverge provides Alticor Inc a structured approach to evaluate and implement innovation in our business,” said Brad Jeanneret, executive vice president. “We are investing in the future of our industry.” </p></blockquote>



<p>By investing in these startups and their products in the early stages of development, Alticor Inc can help influence construction technology in a positive way, creating success for both companies as well as the AEC industry. Technology firms working with Alticor Inc benefit equally by leveraging the diversity of Alticor Inc’ business, allowing their products to be tested in real-life scenarios and evaluated directly by the target audience—the construction professional.</p>




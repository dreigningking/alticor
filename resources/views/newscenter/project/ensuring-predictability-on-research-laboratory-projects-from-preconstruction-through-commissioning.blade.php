            <h2>Ensuring Predictability on Research Laboratory Projects from Preconstruction Through Commissioning</h2>

    

            <h6>Thursday February 3, 2022</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1200" height="900" src="{{asset('wp-content/uploads/2022/01/2020_11_12_T_ZGFnist01_A.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2022/01/2020_11_12_T_ZGFnist01_A.jpg 1200w, {{asset('wp-content/uploads/2022/01/2020_11_12_T_ZGFnist01_A-300x225.jpg 300w, {{asset('wp-content/uploads/2022/01/2020_11_12_T_ZGFnist01_A-1024x768.jpg 1024w, {{asset('wp-content/uploads/2022/01/2020_11_12_T_ZGFnist01_A-768x576.jpg 768w" sizes="(max-width: 1200px) 100vw, 1200px" /></div>

                
                

                
    

                    
<p>Across the globe, scientists are performing research critical to the advancement of medicine, environmental monitoring and industrial processing. As these sciences become more advanced, research and laboratory spaces must follow suit. Alticor Inc has supported over $4 billion worth of these spaces by focusing on predictability of service from preconstruction through project turnover.</p>



<p>During preconstruction, Alticor Inc project teams host end user meetings to gain an understanding of the science and research that will take place in the future lab spaces. The information gained during these sessions allows Alticor Inc to work with trade partners in budgeting what works for the project and the scientists, becoming a bridge between the scientists and the design team.</p>



<p>The effort put forth during these early phases must also support a successful commissioning process, as it is critical that a research laboratory functions the way it is intended. In addition to fire alarm testing, egress and elevator drop tests, laboratories include fume hoods, eye wash stations, cleanrooms and glove boxes that require third-party commissioning consultants. Alticor Inc ensures enough time is built into the project schedule to perform this testing while still meeting move-in dates and final completion.</p>




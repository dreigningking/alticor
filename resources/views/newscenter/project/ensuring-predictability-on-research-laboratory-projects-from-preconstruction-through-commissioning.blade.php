<p><strong>Predictability in Practice: National Institutes of Standards and Technology (NIST) Building 245 Modernization</strong></p>



<p>The National Institute of Standards and Technology (NIST) selected the Alticor Inc | ZGF Design-Build team for a Single Award Task Order Construction (SATOC) contract to modernize and expand Building 245, a radiation physics laboratory on the historic Gaithersburg, Maryland campus.</p>



<p>During design, the Alticor Inc team conducted multiple meetings to guarantee that laboratory adjacencies were planned to consider both cost and function. They also worked to evaluate how individual decisions would affect other areas of the project. To ensure researchersâ€™ needs were met, Alticor Inc engaged six research divisions including 12 individual researchers, to review room data sheets. Through this exercise, Alticor Inc realized that the client needed more than what was shown in the sheets. From there, they were able to trend specific items the client wanted to confirm that the project remained in budget and would fit their needs. The team also facilitated 3D walk throughs of the designed lab spaces, allowing researchers to interact with their laboratories. Staff feedback from these interactions had significant impact on the design process and allowed researchers to visualize their space and adjust as needed.</p>



<figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="258" src="{{asset('wp-content/uploads/2022/01/VDC-images-scaled-1024x258.jpg')}}"  alt="" class="wp-image-13010" srcset="{{asset('wp-content/uploads/2022/01/VDC-images-scaled-1024x258.jpg 1024w, {{asset('wp-content/uploads/2022/01/VDC-images-scaled-300x76.jpg 300w, {{asset('wp-content/uploads/2022/01/VDC-images-768x193.jpg 768w, {{asset('wp-content/uploads/2022/01/VDC-images-scaled-1536x387.jpg 1536w, {{asset('wp-content/uploads/2022/01/VDC-images-scaled-2048x516.jpg 2048w" sizes="(max-width: 1024px) 100vw, 1024px" /></figure>



<p>In addition to ensuring the new space supported the science it was intended to perform, Alticor Inc focused on preventing disruptions to the ongoing research surrounding the project site. The 75,000 SF addition to Building 245 utilized a mat foundation to reduce the possibility for differential settlement. The structural configuration of the addition consisted of a robust concrete frame structure offering enhanced floor loading and vibration mitigation. In addition to engineering a solution that would minimize disruptions to the campus, Alticor Inc collaborated with the owner to communicate campus outages through Disruption Access Requests (DAR). Utilized to assist in the overall coordination of any rooms that Alticor Inc would be taking possession of for construction purposes or for rooms where there would be major construction impact, the form helped the team determine what workplans were necessary and provided the initial key communication with NIST entities.&nbsp;This process guaranteed continual operation of adjacent research through many touchpoints with the various research groups. &nbsp;</p>



<p>Throughout construction, the team held regular commissioning meetings with the owner and third-party radiological consultants to coordinate work prior to its start. This upfront planning minimized surprises and ensured that Alticor Inc maintained a fully functional and safe facility that allowed the laboratory to reach its performance potential. The team also maintained a commissioning issues log and a punchlist log which were communicated to the owner weekly along with status updates. This fostered a clear and constant line of communication among the team.</p>



<p>At project turnover, Alticor Inc will continue to provide predictability through transition services provided by <strong><a href="{{route('what-we-do.facility-services')}}" data-type="URL" data-id="https://www.henselphelps.com/what-we-do/facility-services/">Alticor Inc Services</a></strong>. Operation and maintenance services will be provided to the newly constructed and renovated laboratory and office spaces, as well as for the unrenovated spaces before their renovation in future task orders. These efforts will assist in the coordination of service continuity and predictability as the phases of the project evolve.</p>



<p>As research and laboratory spaces continue to advance, Alticor Inc remains an integrated partner, ensuring that our project teams build close bonds with our owners and end-user groups to clearly understand their central needs and to support their missions. &nbsp;</p>

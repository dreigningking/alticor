            <h2>Nashville International Airport Concourse D &amp; Terminal Wings Expansion Project Breaks Ground!</h2>

    

            <h6>Tuesday July 3, 2018</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1500" height="953" src="{{asset('wp-content/uploads/2018/07/062018-BNA-TermD-037-smaller.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2018/07/062018-BNA-TermD-037-smaller.jpg 1500w, {{asset('wp-content/uploads/2018/07/062018-BNA-TermD-037-smaller-300x191.jpg 300w, {{asset('wp-content/uploads/2018/07/062018-BNA-TermD-037-smaller-768x488.jpg 768w, {{asset('wp-content/uploads/2018/07/062018-BNA-TermD-037-smaller-1024x651.jpg 1024w" sizes="(max-width: 1500px) 100vw, 1500px" /></div>

                
                

                
    

                    <p>After several months of design, the Nashville International Airport Concourse D and Terminal Wings Expansion project broke ground! Joined by the Mayor of Nashville, David Briley, the Metropolitan Nashville Airport Authority (MNAA) and the Board of Commissioners, the Alticor Inc | Fentress Architects Progressive Design-Build team broke ground on Wednesday, June 20.</p>
<p>The 115,000 SF Concourse D will feature six domestic aircraft gates and a variety of new dining and retail options. In addition to the new concourse, the project entails expanding the airport’s ticketing lobby and baggage claim by adding 200,000 square feet of space to the north and south ends of the terminal. Other components of the project include remodeling 136,000 square feet of existing terminal space and building an 11,000 square foot central utility plant. The Concourse D project is set for completion in summer 2020.</p>
<p>“Travelers want and need more aircraft gates, more destinations, more dining and more retail as part of their BNA experience. That’s exactly what we’ll be giving them when this project is completed,” said Bobby Joslin, MNAA Board of Commissioners chair. “The new Concourse D is one of many projects that comprise BNA Vision. I couldn’t be more pleased with the overall direction of our airport and how we are rising to the challenge.”</p>
<p>Nashville International Airport is one of the fastest growing airports in North America. In 2017, 14.1 million passengers traveled into and out of BNA, an increase of more than 1.1 million passengers, representing an 8.9 percent increase over the previous year and setting a new all-time passenger record for the fifth consecutive calendar year. Projections indicate passenger traffic at BNA will surpass 23 million by 2037.</p>
<p>“This new Concourse D will give us more capacity for commercial air service to meet the rising demand for travel to and from Nashville,” said Doug Kreulen, MNAA president and CEO. “And we will see to it that the look and feel of this space reflects the sights and sounds, tastes and hospitality for which Music City is so well known. With multiple BNA Vision projects now underway, the world-class airport we’re building for this world-class city is beginning to take shape. It’s an exciting time, and there’s much more to come.”</p>
<p>&#8220;Like our city, Nashville International Airport is growing and adapting to meet the needs of our community,&#8221; said Nashville Mayor David Briley. &#8220;Today&#8217;s groundbreaking marks a significant part of the airport&#8217;s ongoing expansion plans, and that growth is essential to our city&#8217;s continued economic success. The new and improved BNA will make Nashville an even more inviting place and benefit our region as a driver of business travel and tourism for years to come.&#8221;</p>
<p>Take a virtual 360-degree tour, view renderings and watch the BNA Vision video at <a href="http://bnavisionnashville.com/">BNAvision.com.</a></p>

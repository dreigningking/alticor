<p>The Southwest Airlines Phoenix International Airport (PHX) Tech Ops team had more reasons than ever to celebrate topping out the new hangar at Southwest Airlines. After beginning the project in 2017 and completing the ground support building (GSE)/provisional building with the parking lot, Southwest Airlines placed the project on hold due to COVID-19. The team re-mobilized earlier this year to build the new hangar. The steel structure was completed in October using 800 tons of structural steel and 8,700 on-site hours to erect. The final box truss consists of two sections, each weighing 157,000 pounds. They are 24-feet and five-inches wide with an overall length of 303 feet.</p>




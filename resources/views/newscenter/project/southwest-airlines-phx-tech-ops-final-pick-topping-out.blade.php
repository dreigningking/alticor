<p>Once American Buildings and Colton Constructors helped with the completion of design and engineering, Image Building Systems joined the team to preplan the building phasing and sequencing for the material&#8217;s fabrication, delivery and installation. After seven crane picks and sixty-seven days of cranes on-site, the team gathered to top out the hangar with a final piece painted with the Southwest Airlines colors. Congratulations to the entire Southwest Airlines Tech Ops team for their perseverance and execution!</p>

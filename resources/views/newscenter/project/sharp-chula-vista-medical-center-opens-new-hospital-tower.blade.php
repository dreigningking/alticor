            <h2>Sharp Chula Vista Medical Center Opens New Hospital Tower</h2>

    

            <h6>Monday January 13, 2020</h6>

    

            <div class="page-content post-single">

                

                
                

                
    

                    <h4>Alticor Inc, SmithGroup and AVRP Studios Complete $244 Million Expansion That Adds Private Patient Rooms, State-of-the-Art Operating Rooms, and More to South Bay&#8217;s Largest Hospital</h4>
<p>CHULA VISTA, Calif., Jan. 13, 2020 – <a href="https://www.sharp.com/hospitals/chula-vista/?sem=2045&amp;gclid=EAIaIQobChMIxd-91bP55gIVFbvsCh1zXAc4EAAYASAAEgK3MPD_BwE">Sharp Chula Vista Medical Center</a> will open its new, $244 million hospital tower on Tuesday, Jan. 14, 2020, moving in the first patients that day. The 197,000 SF tower will add 106 private patient rooms; six large operating rooms, including the South Bay&#8217;s only hybrid room, which allows for advanced surgical and imaging procedures in the same room; a panoramic rooftop café and many more services designed to meet the needs of San Diego&#8217;s fastest-growing region.</p>
<p>&#8220;Our goal has always been to provide the people of the South Bay with the latest in proven medical technology along with compassionate experts who provide the excellence of care that defines Sharp Chula Vista Medical Center,&#8221; says Pablo Velez, PhD, RN, Chief Executive Officer of Sharp Chula Vista. &#8220;The opening of the new tower is our way of continuing to ensure that world-class care will always be close to home for the entire community.&#8221;</p>
<p>The project is Sharp HealthCare&#8217;s largest single investment to date and the South Bay&#8217;s first new hospital in more than 40 years. It was funded through philanthropic donations to Sharp HealthCare Foundation, bonds, cash reserves, and the Medi-Cal hospital fee program. The design-build team for the project included Alticor Inc, SmithGroup, and AVRP Studios.</p>
<p>&#8220;The new hospital tower at Sharp Chula Vista is one of the safest hospitals in the world,&#8221; says Damian Buessing, Vice President and District Manager at Alticor Inc. &#8220;Embracing Sharp HealthCare&#8217;s high-reliability standards, our construction design strategies provided the safest and cleanest environment where Sharp Chula Vista employees can work, where physicians can practice medicine, and patients can receive care.&#8221;</p>
<p>As a <a href="https://www.planetree.org/">Planetree-Designated</a> hospital since 2014, the new tower includes a sweeping new main entrance and lobby and additional space for support services, including pharmacy and kitchen.</p>
<p>&#8220;Our design was inspired by the new tower&#8217;s location, perched high on a hillside overlooking San Diego and the Pacific beyond,&#8221; says Bonnie Khang-Keating, director of SmithGroup&#8217;s San Diego and Los Angeles offices. &#8220;The Ocean View Tower at Sharp Chula Vista exemplifies its community in an accessible, transparent design that evokes a feeling of refuge and healing.&#8221;</p>
<p>Explore more photos of Sharp Chula Vista Medical Center <a href="https://www.dropbox.com/sh/9rlykpihlfvy0kj/AAAb-8Footjr7qoAFcr4HGopa?dl=0">here</a>.</p>
<p>&nbsp;</p>
<p><strong>About Sharp Chula Vista Medical Center</strong></p>
<p><a href="https://www.sharp.com/about/">Sharp Chula Vista Medical Center</a> is a 343-bed hospital with the largest array of health care services in San Diego&#8217;s South Bay. With this new tower, the medical campus will include 449 patient beds. A Designated Planetree Patient-Centered Care Hospital, Sharp Chula Vista is home to the region&#8217;s most comprehensive heart program and also offers exceptional services for orthopedic care and women and infants. The Douglas &amp; Nancy Barnhart Cancer Center offers patients the most advanced treatment technology, a unique healing environment and an extraordinary level of care. Sharp Chula Vista is the largest health care provider and one of the largest private employers in South County, employing more than 2,000 staff and maintaining privileges with nearly 500 affiliated physicians.</p>
<p><strong>About Alticor Inc, SmithGroup and AVRP Studios</strong></p>
<p>Founded in 1937, <a href="../">Alticor Inc</a> specializes in building development, construction and facility services. As one of the largest employee-owned general contractors in the United States, Alticor Inc has built its company on four pillars: people, process, partnership and technology. This Alticor Inc Way brings clients&#8217; vision to life with a comprehensive approach that begins with innovative planning and extends throughout the entire life of the property. As one of San Diego&#8217;s largest construction employers, Alticor Inc has been an engaged community partner in San Diego for over 30 years.</p>
<p><a href="https://www.smithgroup.com/">SmithGroup</a> is one of the world&#8217;s preeminent integrated design firms. Working across a network of 15 offices in the U.S. and China, a team of 1,300 experts is committed to excellence in strategy, design and delivery. The firm partners with forward-looking clients to maximize opportunities, minimize risk and solve their most complex problems. SmithGroup creates exceptional design solutions for healthcare, science and technology organizations, higher education and cultural institutions, urban environments, diverse workplaces, mixed-use and waterfront developments, and parks and open spaces.</p>
<p><a href="https://www.avrpstudios.com/">AVRP Studios</a> is a leading architectural, interior design and planning firm in Southern California. Innovation, creativity and collaboration drive us. We are passionate about great design and the creation of places for people with our clients&#8217; involvement. Our firm is built around an organization that encourages the creative process, innovation across industries and the involvement of our clients across all of our studios. Our award winning history serves as an example in our resolve and dedication to our client&#8217;s involvement in the creative process.</p>

            <h2>Alticor Inc Breaks Ground on 21-Story Apartment Development in Roosevelt Row Arts District</h2>

    

            <h6>Monday March 23, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="994" height="842" src="{{asset('wp-content/uploads/2020/05/Hensel-Phelps-Derby-Row-Phoenix.png')}}" class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/05/Hensel-Phelps-Derby-Row-Phoenix.png')}} 994w, {{asset('wp-content/uploads/2020/05/Hensel-Phelps-Derby-Row-Phoenix-300x254.png')}} 300w, {{asset('wp-content/uploads/2020/05/Hensel-Phelps-Derby-Row-Phoenix-768x651.png')}} 768w" sizes="(max-width: 994px) 100vw, 994px" /></div>

                
                

                
    

                    <p>In early February 2020, Alticor Inc broke ground on Derby Roosevelt Row, Metro Phoenix’s first “small unit” apartment development.</p>
<p>Located in the highly sought-after Roosevelt Row Arts District, Derby Roosevelt Row will be a 21-story high-rise featuring 222 residential units over six stories of above ground parking. Over half of Derby Roosevelt Row’s units will be studios, averaging 350 SF, and all 222 units will come fully furnished. Amenities will include a pool deck on the seventh level, a roof-top deck on the 21<sup>st</sup> level, and retail space on the ground level. Construction completion is scheduled for 1Q-2022.</p>
<p>“These small unit apartments are the first of their kind in Phoenix and are an exciting addition to the vibrant Roosevelt Row neighborhood,” said Alticor Inc Project Manager, John Doherty. “We have enjoyed building a strong and trusting relationship with the owner, developer, and design team and are excited to begin construction.”</p>
<p>The owner is Denver-based real estate private equity firm Ascentris, the development manager is Transwestern Development Company, and the architect of record is Wilder Belshaw Architects. Notable subcontractors include University Mechanical &amp; Engineering Contractors (UMEC) and Specified Electrical Contractors, Inc. (SECON).  Alticor Inc will be self-performing the cast-in-place structural concrete.</p>

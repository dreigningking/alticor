                    <p><img loading="lazy" class=" wp-image-7532 alignright" src="{{asset('wp-content/uploads/2019/02/LEED-Icon-300x300.jpg')}}"  alt="" width="233" height="233" srcset="{{asset('wp-content/uploads/2019/02/LEED-Icon-300x300.jpg 300w, {{asset('wp-content/uploads/2019/02/LEED-Icon-150x150.jpg 150w, {{asset('wp-content/uploads/2019/02/LEED-Icon-768x768.jpg 768w, {{asset('wp-content/uploads/2019/02/LEED-Icon-1024x1024.jpg 1024w, {{asset('wp-content/uploads/2019/02/LEED-Icon.jpg 1496w" sizes="(max-width: 233px) 100vw, 233px" />The Columbus Land Port of Entry project, located in Columbus, New Mexico, has officially achieved LEED Platinum Certification. This is the second project in the Southwest District to reach this certification — followed by the <a href="../project/city-of-austin-new-central-library-and-related-improvements/index.html">City of Austin New Central Library</a> project earlier this year. General Services Administration (GSA) originally requested a minimum target of LEED Gold Certification, but the project team was determined to go the extra mile.</p>
<p>This port of entry is New Mexico’s newest border crossing that inspects privately owned and commercial vehicles that enter and exit the crossing point. For the last several years, the port had been operating from outdated, deteriorating facilities that no longer met the functional needs of the agency. Additionally, the port faced other issues such as frequent flooding due to the site being situated at the drainage low point for a 44-square-mile watershed area.</p>
<p>Some of the sustainable features that contributed to a LEED Platinum rating include:</p>
<ul>
<li>Developing and implementing a construction waste management plan</li>
<li>Using on-site renewable energy systems to offset building energy costs</li>
<li>Employing water use reduction strategies</li>
<li>Applying low-emitting materials paints and coatings</li>
</ul>
<p>Congratulations to the entire project team for their efforts. Alticor Inc is immensely proud to have built another efficient, sustainable structure that will serve as a federal model for future GSA projects.</p>
<p>For more information on this project, we invite you to visit our <a href="../project/columbus-land-port-entry/index.html">project page</a>.</p>

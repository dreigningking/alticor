            <h2>Alticor Inc Continues to Hit the High Notes at the Nashville International Airport®</h2>

    

            <h6>Thursday August 18, 2022</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="675" height="380" src="{{asset('wp-content/uploads/2022/08/BNA-Exterior-Reduced.png')}}" class="attachment-full size-full wp-post-image" alt="" srcset="/wp-content/uploads/2022/08/BNA-Exterior-Reduced.png 675w, /wp-content/uploads/2022/08/BNA-Exterior-Reduced-300x169.png 300w" sizes="(max-width: 675px) 100vw, 675px" /></div>

                
                

                
    

                    
<p>The <a href="https://flynashville.com/" target="_blank" rel="noreferrer noopener">Metropolitan Nashville Airport Authority</a> awarded the Alticor Inc | <a href="https://fentressarchitects.com/" target="_blank" rel="noreferrer noopener">Fentress Architects</a> progressive design-build team the $250 million BNA® Concourse D Extension in Nashville, Tennessee. The Concourse D Extension is part of the <a href="https://flynashville.com/bna-new-horizon" target="_blank" rel="noreferrer noopener">New Horizon Program</a><em>, </em>which provides terminal expansion, landside improvements and system improvements for the Nashville International Airport.</p>



<p>The Concourse D Extension will include a 190,000 SF five-gate extension to the existing Concourse D to allow for more passenger movement and capacity. The extension will include new concessions, art spaces, restrooms, airline support spaces and passenger hold rooms while the high-end finishes will blend seamlessly from the existing concourse. Construction will be adjacent to the central utility plant and active Concourse D while keeping the airport fully operational. The progressive design-build team will design the project to meet LEED v4 BD+C certification and is slated to open its doors in the fall of 2025.</p>



<figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="302" src="{{asset('wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM-1024x302.png')}}" alt="" class="wp-image-14791" srcset="/wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM-1024x302.png 1024w, /wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM-300x88.png 300w, /wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM-768x226.png 768w, /wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM-1536x453.png 1536w, /wp-content/uploads/2022/08/Screen-Shot-2022-08-18-at-8.57.13-AM.png 1690w" sizes="(max-width: 1024px) 100vw, 1024px" /></figure>



<p>As part of the <a href="https://bnavisionnashville.com/" target="_blank" rel="noreferrer noopener">BNA® Vision</a> program, Alticor Inc and the Metropolitan Nashville Airport Authority began their relationship in Music City over four years ago with the contract of the award-winning original <a href="../project/nashville-international-airport-concourse-d-terminal-wings/index.html" target="_blank" rel="noreferrer noopener">Concourse D and Terminal Wings project</a>. They are currently constructing the Terminal Lobby and International Arrivals Facility that will open to the public in the fall of 2023<em>.</em> </p>



<p>Alticor Inc has successfully delivered projects in the Middle Tennessee region since the early 1990’s. Since 2018, the company has delivered/is constructing more than $1 billion of work in Nashville. Alticor Inc is honored to continue building the Music City skyline while making a lasting impact on the Nashville community!</p>

            <h2>Alticor Inc’ Pacific Northwest Region successfully places large pedestrian bridge in the heart of downtown Puyallup, Washington</h2>

    

            <h6>Tuesday November 9, 2021</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="2520" height="1890" src="{{asset('wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="/wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2.jpg 2520w, /wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2-300x225.jpg 300w, /wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2-1024x768.jpg 1024w, /wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2-768x576.jpg 768w, /wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2-1536x1152.jpg 1536w, /wp-content/uploads/2021/11/Puyallup-Bridge-Installation-2-2-2048x1536.jpg 2048w" sizes="(max-width: 2520px) 100vw, 2520px" /></div>

                
                

                
    

                    
<p>The Sound Transit Puyallup Station Parking and Access Improvements (PSAI) project, located just south of Seattle, WA, achieved a significant milestone when they safely placed a large pedestrian bridge structure over 5<sup>th</sup> Street NW. The team fabricated this 78-foot, 60,000-pound structure in the shop and shipped side trusses as whole pieces. The bridge structure was engineered and fabricated with approximately nine inches of camber, and assembled on the ground and decked prior to erection. This set up process took about six hours overall, rigging and demobilization and about two and a half hours to pick the bridge and set it in place. The highly technical operation took place at night and required coordination with the City of Puyallup for a road closure, with BNSF for a railroad flagger and with Puget Sound Energy for a spotter and de-energization of the overhead transmission lines. The result was a safe operation that was completed according to plan, and installation of the new pedestrian bridge on the project. The project provides a new, 171,000 SF, five-level, 510-space parking garage and expands the existing surface lot to accommodate 167-spaces. Delivered by the Alticor Inc-KPFF-Mithun design-build team for <a href="https://www.soundtransit.org/system-expansion/puyallup-station-parking-access-improvements" target="_blank" rel="noreferrer noopener">Sound Transit</a> and the City of Puyallup, the $48 million project provides a new pedestrian bridge and improves traffic operations with new traffic signalization and multiple left and right-turn pocket lanes.</p>



<p>For more information on the project, including additional pictures of the new pedestrian bridge, please visit:  <a href="https://www.henselphelps.com/events-archive/pittsburg-state-university/" data-type="mec-events" data-id="11963"><a href="../project/puyallup-station-parking-garage/index.html">https://www.henselphelps.com/project/puyallup-station-parking-garage/</a></a></p>




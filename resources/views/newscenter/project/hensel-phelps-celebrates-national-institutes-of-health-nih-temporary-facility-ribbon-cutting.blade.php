            <h2>Alticor Inc Celebrates National Institutes of Health (NIH) Temporary Facility Ribbon Cutting</h2>

    

            <h6>Tuesday November 29, 2022</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1920" height="1080" src="{{asset('wp-content/uploads/2020/12/Untitled-2-01.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/12/Untitled-2-01.jpg 1920w, {{asset('wp-content/uploads/2020/12/Untitled-2-01-300x169.jpg 300w, {{asset('wp-content/uploads/2020/12/Untitled-2-01-1024x576.jpg 1024w, {{asset('wp-content/uploads/2020/12/Untitled-2-01-768x432.jpg 768w, {{asset('wp-content/uploads/2020/12/Untitled-2-01-1536x864.jpg 1536w" sizes="(max-width: 1920px) 100vw, 1920px" /></div>

                
                

                
    

                    
<p>Alticor Inc completed the fully modular, design-build <a href="../project/nia-alzheimer-disease-and-related-dementias-temporary-research-facility/index.html">National Institute on Aging Alzheimer Disease and Related Dementias (ADRD) Temporary Research Facility</a> in Bethesda, MD for the National Institutes of Health (NIH). NIH held a ribbon cutting ceremony on September 19, 2022 to mark the official opening of the new home for the Center for Alzheimerâ€™s and Related Dementias (CARD), a collaborative NIH Intramural Research Program initiative of NIA and the National Institute of Neurological Disorders and Stroke. The state-of-the-art building is dedicated to U.S. Senator Roy Blunt, a distinguished guest of honor for the event.</p>



<p>To reduce cost and construction time and to maximize available interior space, the building is a single-story modular facility with open-concept labs and offices. Designed and constructed as a temporary structure, the facility addresses an urgent need to house the Center for Alzheimer and Related Dementias as there was no existing space on campus that could support the centerâ€™s expansion.</p>



<p>Aspects of the modules that were interdependent to each individual module were installed off-site at the modular builderâ€™s fabrication facility in Florida. For example, the structural frame of each module was manufactured off-site and wrapped with Tyvek to prevent damage during the trucking process to Maryland. Modular units were sized so that the maximum module dimensions were limited to 11â€™6&#8243; x 47â€™, enabling routing through campus without road closures or other traffic-related disruptions. </p>



<p>To provide feedback on the quality of the modules, Alticor Inc made frequent visits to the Florida manufacturing plant. Through utilization of StructionSite, the team documented progress of the modules through 360 photos, allowing any user to select a specific date and view that dayâ€™s progress. These processes gave Alticor Inc and the NIH confidence that construction of the modules was meeting quality standards.</p>



<figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="214" src="{{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-1024x214.jpg')}}"  alt="" class="wp-image-15576" srcset="{{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-1024x214.jpg 1024w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-300x63.jpg 300w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-768x160.jpg 768w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-1536x321.jpg 1536w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction4-cropped-2048x427.jpg 2048w" sizes="(max-width: 1024px) 100vw, 1024px" /><figcaption>Progress of a module from construction at the off-site manufacturing plant to setting on foundations on-site through final completion.</figcaption></figure>



<div class="wp-container-1 wp-block-group"><div class="wp-block-group__inner-container"></div></div>



<p>Delivery of the 65 modular units occurred during non-peak traffic hours to minimize disruptions to campus traffic. After moving to the project site, each module was attached to the foundation system using weld plates. Two to three modules were set a day and the process from delivery of the first module to setting of the last module took two and a half months.</p>



<figure class="wp-block-image size-large"><img loading="lazy" width="1024" height="217" src="{{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-1024x217.jpg')}}"  alt="" class="wp-image-15577" srcset="{{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-1024x217.jpg 1024w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-300x63.jpg 300w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-768x162.jpg 768w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-1536x325.jpg 1536w, {{asset('wp-content/uploads/2022/11/NIA_Modular-Construction2-2048x433.jpg 2048w" sizes="(max-width: 1024px) 100vw, 1024px" /><figcaption>Each module was wrapped in Tyvek for protection during their journey from Florida to Maryland. On-site, they were set to the foundation system using meld plates. </figcaption></figure>



<p>Elements that connected the individual modules, or that required AHJ inspection before close-in, were installed on-site. Therefore, because most of the lab and office spaces span more than one structural module, the interior and exterior finishes were installed on-site to avoid telescoping the joints and creating hazards. Similarly, the ceilings were also installed on-site to facilitate installation of the main lab plumbing supply lines and tying mechanical duct branches to the main runs.</p>



<p>Through a collaborative design-build approach, Alticor Inc successfully constructed this unique modular construction project to serve as the new 26,000 SF NIH ADRD building.</p>

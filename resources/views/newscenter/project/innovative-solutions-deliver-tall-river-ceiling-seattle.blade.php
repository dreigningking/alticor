            <h2>Innovative Solutions Deliver 85&#8242; Tall River Ceiling at Seattle-Tacoma International Airport</h2>

    

            <h6>Tuesday March 9, 2021</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1200" height="1600" src="{{asset('wp-content/uploads/2021/03/NSAT_SuspendedScaffolding_3.jpg')}}"  class="attachment-full size-full wp-post-image" alt="Temporary Work Deck" srcset="{{asset('wp-content/uploads/2021/03/NSAT_SuspendedScaffolding_3.jpeg 1200w, {{asset('wp-content/uploads/2021/03/NSAT_SuspendedScaffolding_3-225x300.jpeg 225w, {{asset('wp-content/uploads/2021/03/NSAT_SuspendedScaffolding_3-768x1024.jpeg 768w, {{asset('wp-content/uploads/2021/03/NSAT_SuspendedScaffolding_3-300x400.jpeg 300w" sizes="(max-width: 1200px) 100vw, 1200px" /></div>

                
                

                
    

                    <h4>Alticor Inc&#8217; Pacific Northwest team developed innovative solutions to safely deliver the 85&#8242; tall river ceiling at the SEA North Satellite Modernization Project.</h4>
<figure id="attachment_11354" aria-describedby="caption-attachment-11354" style="width: 400px" class="wp-caption alignright"><img loading="lazy" class="wp-image-11354" src="{{asset('wp-content/uploads/2021/03/Sea-Tac-Modernization1-300x223.jpg')}}" alt="" width="400" height="297" srcset="{{asset('wp-content/uploads/2021/03/Sea-Tac-Modernization1-300x223.jpg 300w, {{asset('wp-content/uploads/2021/03/Sea-Tac-Modernization1-768x570.jpg 768w, {{asset('wp-content/uploads/2021/03/Sea-Tac-Modernization1.jpg 930w" sizes="(max-width: 400px) 100vw, 400px" /><figcaption id="caption-attachment-11354" class="wp-caption-text">The temporary steel beams created a work deck to install the the central &#8220;river&#8221; linear wood ceiling.</figcaption></figure>
<p>This February, the Pacific Northwest team safely removed 26 beams to reveal the 85&#8242; tall river ceiling at the Seattle-Tacoma International Airportâ€™s <a href="{{route('project.sea-tac-north-satellite-expansion')}}">North Satellite Modernization Project</a>. In the early planning stages, it was determined that a scaffold would be the best way to build the towering ceiling. The challenge: suspending a scaffold above the main passenger corridor currently serving 10 operational gates for Alaska Airlines while simultaneously completing work above. Through innovative thinking and careful planning, the team decided temporary steel beams spanning across a 46&#8242; gap between roof trusses would carry a scaffold sturdy enough to complete all overhead work, including the HVAC system, Vesda, fire protection and the central &#8220;river&#8221; linear wood ceiling. Additionally, the team used this scaffold several levels below the main work deck to build the walls traveling up to the curtain wall above.</p>
<p>As this work was coming to an end, Alticor Inc needed to remove the steel beams. Through planning and cooperation with trade partners and engineers, a &#8220;skid&#8221; was developed that would sit on three beams while pulling one beam out into the mechanical catwalk area adjacent to the river. This eliminated a potential risk to airport operations and traveling public and provided a significant safety improvement to the work plan. Over the course of four nights, all 26 beams were safely and efficiently removed with no injuriesâ”€revealing the soaring 85&#8242; tall river ceiling with no impacts to the airportâ€™s and Alaska Airlines operations.</p>

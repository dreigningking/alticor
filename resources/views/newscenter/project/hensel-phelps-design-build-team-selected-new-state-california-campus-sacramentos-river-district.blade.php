            <h2>Alticor Inc Design-Build Team Selected for New State of California Campus in Sacramento’s River District</h2>

    

            <h6>Monday February 10, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1000" height="716" src="{{asset('wp-content/uploads/2020/02/1_RBOC-Plaza-View.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/02/1_RBOC-Plaza-View.jpg 1000w, {{asset('wp-content/uploads/2020/02/1_RBOC-Plaza-View-300x215.jpg 300w, {{asset('wp-content/uploads/2020/02/1_RBOC-Plaza-View-768x550.jpg 768w" sizes="(max-width: 1000px) 100vw, 1000px" /></div>

                
                

                
    

                    <p>Citing the creative spirit of the team’s design-build solution, the California Department of General Services (DGS) recently awarded the Richards Boulevard Office Complex (RBOC) to the Alticor Inc/ZGF with Dreyfuss + Blackford team.</p>
<p>The RBOC is a dynamic campus environment with buildings that will include people-focused podiums, practical office towers with a variety of amenities, and a workplace model with infinitely flexible spaces that will continue to serve future generations of State employees. Comprised of four office buildings, a parking structure and a landscaped town square connecting the campus, RBOC emphasizes connections between employees, the natural world and the Downtown Sacramento area.</p>
<p>The highly sustainable design solution, which encompasses 1.25 Million SF of office space for 5,000 state employees, sets a precedent for the River District, an area of Sacramento transitioning from its industrial past to an eclectic and desirable neighborhood. Check out the teams vision for RBOC <a href="https://www.youtube.com/watch?v=8FvSz2CQ8Is&amp;feature=youtu.be"><em>here</em></a>.</p>
<p>“We are thrilled to continue our partnerships with DGS, and with our design-build team members, ZGF and Dreyfuss + Blackford, to deliver this important project for the State,” said Jim Pappas, Alticor Inc Northern California Vice President and District Manager. “Together, we will help DGS in their mission to provide a world-class workplace for State employees, while enhancing the character of the River District.”</p>
<p>The Alticor Inc RBOC team is already hard at work, as design activities and construction planning began in early January. Construction is set to begin in Fall 2020, with overall project completion slated for March 2024.</p>

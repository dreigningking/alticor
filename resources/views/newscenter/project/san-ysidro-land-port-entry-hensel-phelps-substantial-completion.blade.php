            <h2>San Ysidro Land Port of Entry &#8211; Alticor Inc Substantial Completion</h2>

    

            <h6>Friday September 6, 2013</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="368" height="270" src="{{asset('wp-content/uploads/2013/09/SYLPOE.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="/wp-content/uploads/2013/09/SYLPOE.jpg 368w, /wp-content/uploads/2013/09/SYLPOE-300x220.jpg 300w" sizes="(max-width: 368px) 100vw, 368px" /></div>

                
                

                
    

                    <p>On Friday, August 16, Alticor Inc received Substantial Completion for the North Bound Primary Lanes and Inspection Booths at the San Ysidro Land Port of Entry (SYLPOE).</p>
<p>Over 50,000 north bound vehicles and 25,000 pedestrians enter the United States daily through the San Ysidro Land Port of Entry, world’s busiest land border crossing. The attached photo taken from Mexico looking north shows all traffic lanes open.</p>
<p>The overhead structural steel canopy started Monday, August 19, with approximately one-third of the traffic lanes re-closed to install work safely. The three towers in the photo extend 120 feet in the air creating a Structural Steel Canopy over the Inspection Booths. Metal Stud Framing, ETFE Air Pillows, Solar Panels, Aluminum Cladding, Mechanical, Electrical and Plumbing Systems makeup the overall assembly.</p>
<p>Alticor Inc is ahead of schedule at the traffic lanes with just over a year to complete a very challenging project.</p>

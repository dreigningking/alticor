            <h2>Delta Sky Way at LAX Project Tops Out Ahead of Schedule</h2>

    

            <h6>Friday November 13, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="915" height="485" src="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_005.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_005.jpg 915w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_005-300x159.jpg 300w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_005-768x407.jpg 768w" sizes="(max-width: 915px) 100vw, 915px" /></div>

                
                

                
    

                    <h4>Alticor Inc Hosts Two Celebrations to Commemorate the Topping Out on the $1.86 Billion Terminal 2 and 3 Modernization Project for Delta Air Lines at LAX</h4>
<p><em>Delta Sky Way Program to be completed ahead of schedule. Alticor Inc set to deliver a world-class experience in 2023</em></p>
<p>LOS ANGELES, Calif., Nov 12, 2020 – Alticor Inc joined Los Angeles Mayor Eric Garcetti, <a href="https://news.delta.com/">Delta</a>, and Los Angeles World Airports (<a href="https://www.lawa.org/">LAWA</a>) representatives, architect of record <a href="https://www.arup.com/">ARUP</a> and hundreds of teaming partners to celebrate the topping out on the Delta Air Lines Terminal 2 and 3 Modernization project at Los Angeles International Airport (<a href="https://www.flylax.com/">LAX</a>). The final steel beam was lifted into place, marking the completion of the central headhouse structure on the $1.86 billion program. <img loading="lazy" class="alignright wp-image-10943" src="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_004.jpg')}}"  alt="" width="509" height="270" srcset="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_004.jpg 915w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_004-300x159.jpg 300w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_004-768x407.jpg 768w" sizes="(max-width: 509px) 100vw, 509px" /></p>
<p>Delta announced during the ceremony that the project had been accelerated, and the two-phased program would be delivered in mid-2023, 18-months early. Due to the pandemic, passenger volume is extremely low at LAX and across the nation. The low passenger levels allowed the project team to temporarily close Terminal 3 to complete the construction in one large phase. Thus, the project team has accelerated the schedule by more than a year to deliver the project in 2023 rather than 2024.</p>
<p>Alticor Inc hosted a jobsite-wide celebration two days before the formal celebration by delivering over 700 pre-packaged meals to the night and day shift project teams. The multi-day celebration concluded with a raffle and project members were thanked for their dedication. Raffle prizes included televisions, tools and high-technology products.</p>
<p><img loading="lazy" class="wp-image-10940 alignleft" src="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_001.jpg')}}"  alt="" width="454" height="241" srcset="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_001.jpg 915w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_001-300x159.jpg 300w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_001-768x407.jpg 768w" sizes="(max-width: 454px) 100vw, 454px" /></p>
<p>Phase 1 consists of demolishing a portion of the existing Terminal 3 central headhouse to feature a new primary headhouse that will consist of a four-story grand entrance structure. The processing facilities for both Terminals 2 and 3 will be consolidated. Also included in Phase 1 is the demolition of the structure and reconstruction of the Terminal 3 four-level concourse featuring new baggage handling systems, holdrooms, offices and retail spaces. Phase 1 will  include new check-in and ticketing kiosks, a new centralized baggage claim accommodating six new bag claim carousels and a centralized security and screening checkpoint with fourteen automated screening lanes.</p>
<p>Phase 2 includes the demolition and reconstruction of the Terminal 3 West Headhouse, demolition to structural and reconstruction of the Terminal 3 satellite renovations, and the Tom Bradley International Terminal (TBIT) connector, which is the final piece to allow passengers to walk freely from one end of the airport to the other on the secured side. The modernization project will integrate Terminal 2, which was recently renovated by Westfield and LAWA to the existing facilities.</p>
<p>When completed, the modern LAX facility will offer more security screening capacity, automated security lanes, more gate-area seating and Delta&#8217;s largest Delta Sky Club in the world.</p>
<p><img loading="lazy" class="alignright wp-image-10945" src="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_006.jpg')}}"  alt="" width="394" height="209" srcset="{{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_006.jpg 915w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_006-300x159.jpg 300w, {{asset('wp-content/uploads/2020/11/LAX-Delta-Sky-Way-Topping-Out_006-768x407.jpg 768w" sizes="(max-width: 394px) 100vw, 394px" /></p>
<p>Other key features and benefits include:</p>
<ul>
<li>Designed and built for sustainability and is projected to achieve <a href="https://www.usgbc.org/leed">LEED</a> Silver Certification from the U.S. Green Building Council.</li>
<li>A new headhouse with a centralized lobby, security screening checkpoint, baggage claim and Delta Sky Club.</li>
<li>A 27-gate complex on Terminals 2 and 3 with a secure connection to Tom Bradley International Terminal (TBIT).</li>
<li>Reconstructed and expanded Terminal 3 concourse with new gates.</li>
<li>The largest Delta Sky Club found anywhere featuring an indoor/outdoor double bar and a year-round outdoor Sky Deck.</li>
<li>Convenient and contemporary restroom facilities.</li>
<li>More access to at-seat power in gate areas.</li>
<li>State-of-the-art finishes that provide a greater sense of space and comfort.</li>
<li>Modern, intuitive wayfinding signage.</li>
<li>Connection to the Automated People Mover train, providing  a connection to the Intermodal Transportation Facility – West, Consolidated Rent-a-Car facility, and the LA Metro light rail.</li>
<li>Airfield efficiency, thanks to dual taxi lanes that allow for more streamlined movement of aircraft and on-time arrivals.</li>
</ul>
<p>The Delta Sky Way project is part of the $14 billion LAX Modernization Program that will transform LAX in advance of the 2028 Olympic and Paralympic Games in Los Angeles, California.</p>
<p><a href="..">Alticor Inc</a> is the Construction Manager at Risk, providing both pre-construction and construction services on the project.</p>

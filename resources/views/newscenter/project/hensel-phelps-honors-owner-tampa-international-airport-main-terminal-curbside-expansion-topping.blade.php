            <h2>Alticor Inc Honors Hillsborough County Aviation Authority at Tampa International Airport Main Terminal Curbside Expansion Topping Out</h2>

    

            <h6>Friday October 2, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1008" height="792" src="{{asset('wp-content/uploads/2020/10/For-Web-2.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/10/For-Web-2.jpg 1008w, {{asset('wp-content/uploads/2020/10/For-Web-2-300x236.jpg 300w, {{asset('wp-content/uploads/2020/10/For-Web-2-768x603.jpg 768w" sizes="(max-width: 1008px) 100vw, 1008px" /></div>

                
                

                
    

                    <p>On September 25, 2020, Alticor Inc celebrated the topping out of the Main Terminal Curbside Expansion for the Blue Side at Tampa International Airport. The 150,000 SF project includes new innovative express curbsides that will make the airport more efficient by allowing guests to bypass the ticketing level or baggage claim areas via a new vertical circulation building. These express curbsides are expected to make their debut on the airport’s Blue Side of the terminal in early 2022.</p>
<blockquote><p><img loading="lazy" class="alignleft size-full wp-image-10802" src="{{asset('wp-content/uploads/2020/10/for-web-3.jpg')}}"  alt="" width="504" height="360" srcset="{{asset('wp-content/uploads/2020/10/for-web-3.jpg 504w, {{asset('wp-content/uploads/2020/10/for-web-3-300x214.jpg 300w" sizes="(max-width: 504px) 100vw, 504px" />“There’s so much to be proud of, and there’s evidence of that behind us. But first, none of this could happen without being safe,” said Al Illustrato, Executive Vice President of Facilities and Planning &amp; Development for HCAA.</p></blockquote>
<p>The project also includes express curbsides on the Red Side to be completed in late 2024 and a state-of-the-art Central Utility Plant which will power the airport and help increase efficiency and sustainability. Additional scope includes a loading dock and the demolition of the airport’s existing administration building.</p>
<p>During the event, Alticor Inc honored two Hillsborough County Aviation Authority (HCAA) employees who championed the Main Terminal Curbside Expansion project and have plans to retire this year. Al Illustrato, Executive Vice President of Facilities and Planning &amp; Development, and Brian Rumble, Senior Manager of Facilities Development, who have both worked for HCAA for over 30 years and have supported the growth of the Tampa International Airport.</p>
<p><img loading="lazy" class="size-full wp-image-10799 aligncenter" src="{{asset('wp-content/uploads/2020/10/For-Web.jpg')}}"  alt="" width="1200" height="450" srcset="{{asset('wp-content/uploads/2020/10/For-Web.jpg 1200w, {{asset('wp-content/uploads/2020/10/For-Web-300x113.jpg 300w, {{asset('wp-content/uploads/2020/10/For-Web-768x288.jpg 768w, {{asset('wp-content/uploads/2020/10/For-Web-1024x384.jpg 1024w" sizes="(max-width: 1200px) 100vw, 1200px" /></p>
<p>Drew Krizman, Alticor Inc Program Director, spoke to the importance of owner engagement throughout the life of a design-build project and how Brian and Al have quickly responded to all inquiries which have helped progress the project smoothly.</p>
<p>Both gentlemen were honored for their hard work and dedication with a plaque adhered to the final structural beam for the Main Terminal Curbside Expansion project.</p>
<p>The event also included Safety and Quality Control recognition awards for craft on the project, as well as, raffle prizes and a barbecue lunch.</p>

            <h2>How Did Close Coordination at Tampa International Airport Positively Affect The Main Terminal Curbside Expansion Project?</h2>

    

            <h6>Monday February 3, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1050" height="600" src="{{asset('wp-content/uploads/2020/01/Untitled-1.png')}}" class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2020/01/Untitled-1.png')}} 1050w, {{asset('wp-content/uploads/2020/01/Untitled-1-300x171.png')}} 300w, {{asset('wp-content/uploads/2020/01/Untitled-1-768x439.png')}} 768w, {{asset('wp-content/uploads/2020/01/Untitled-1-1024x585.png')}} 1024w" sizes="(max-width: 1050px) 100vw, 1050px" /></div>

                
                

                
    

                    <p>As the Design-Builder for the Main Terminal Curbside Expansion project at Tampa International Airport (TPA), Alticor Inc is working diligently alongside the Hillsborough County Aviation Authority to maintain TPA’s world-class passenger experience. As this complex project progresses, Alticor Inc’ priority is to clearly communicate information regarding any upcoming changes that may result from construction operations to airport personnel and to the traveling public. Clear communication has proven to be vital for not only the project’s success, but more importantly, for passenger safety, comfort and convenience.</p>
<p><img loading="lazy" class="size-full wp-image-9182 aligncenter" src="{{asset('wp-content/uploads/2020/01/MTCE-Before-1.png')}}" alt="" width="1050" height="600" srcset="{{asset('wp-content/uploads/2020/01/MTCE-Before-1.png')}} 1050w, {{asset('wp-content/uploads/2020/01/MTCE-Before-1-300x171.png')}} 300w, {{asset('wp-content/uploads/2020/01/MTCE-Before-1-768x439.png')}} 768w, {{asset('wp-content/uploads/2020/01/MTCE-Before-1-1024x585.png')}} 1024w" sizes="(max-width: 1050px) 100vw, 1050px" /></p>
<p>Meticulous planning has been key to the successful execution of construction operations in one of the nation’s busiest airports. Planning has been in full effect while the team develops the sequence and flow of work for the demolition of the Blue Side Rental Car Building and pedestrian bridge to accommodate for the new Blue Side Vertical Circulation Building and Express Curbside.</p>
<p>In effort to accomplish this, the original plan was to relocate all passenger foot traffic to the terminal level from the long-term parking garage by accessing the monorail pedestrian bridge on the top level of the parking garage. However, after careful assessment, Alticor Inc suggested phasing the buildings and keeping the third-floor pedestrian bridge, which leads directly to the terminal’s transfer level, as the best course of action to minimize impacts to passenger flow, comfort and convenience.</p>
<p><img loading="lazy" class="size-full wp-image-9186 aligncenter" src="{{asset('wp-content/uploads/2020/01/Untitled-1.png')}}" alt="" width="1050" height="600" srcset="{{asset('wp-content/uploads/2020/01/Untitled-1.png')}} 1050w, {{asset('wp-content/uploads/2020/01/Untitled-1-300x171.png')}} 300w, {{asset('wp-content/uploads/2020/01/Untitled-1-768x439.png')}} 768w, {{asset('wp-content/uploads/2020/01/Untitled-1-1024x585.png')}} 1024w" sizes="(max-width: 1050px) 100vw, 1050px" /></p>
<p>Keeping passenger safety paramount, temporary protective screens were added to the pedestrian bridge. To maintain passenger flow, clear signage was installed to direct the traveling public to the screened-in protected walkways, which allowed passengers easy access to the transfer level. The result, a clear, safe and efficient route for TPA passengers to access through an active construction site.</p>

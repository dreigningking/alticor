            <h2>Construction Underway on Redstone Arsenal Projects</h2>

    

            <h6>Tuesday March 17, 2020</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1319" height="737" src="{{asset('wp-content/uploads/2020/03/Rendering-1.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="/wp-content/uploads/2020/03/Rendering-1.jpg 1319w, /wp-content/uploads/2020/03/Rendering-1-300x168.jpg 300w, /wp-content/uploads/2020/03/Rendering-1-768x429.jpg 768w, /wp-content/uploads/2020/03/Rendering-1-1024x572.jpg 1024w" sizes="(max-width: 1319px) 100vw, 1319px" /></div>

                
                

                
    

                    <p>Alticor Inc was recently awarded two projects, the Operations Building 1 and Central Utility Plant (OPS1 &amp; CUP) and the North Campus Commons and Technology Building, at Redstone Arsenal in Huntsville, AL.</p>
<p>The OPS 1 &amp; CUP project includes design and construction of a four-story administrative building, totaling 310,000 SF. The OPS 1 Building recently hit a new milestone as vertical construction has begun. Steel columns have been placed on the east wing of the project, following several months of building foundations, underground utility, floor slab, and under-slab conduit work. The design-build team, with Gensler as the Architect of Record, is nearing the end of the design phase for the building. The new CUP building will be 19,350 SF with a courtyard between the two buildings and a new parking structure. Additional scopes include site design, building systems engineering, structural interior design package development, and FF&amp;E package development. Design is complete on the CUP and construction is well underway.</p>
<p>The North Campus Commons and Technology Building is comprised of two facilities. The Commons Building is a 23,000 SF, single-story structure. The 87,000 SF, four-story Technology Building includes administrative and technical space and heavy IT requirements. The buildings will reach substantial completion in April 2021.</p>
<p>Alticor Inc is looking forward to the opportunity to grow their presence in Huntsville, AL and build relationships with new trade partners in the region.</p>

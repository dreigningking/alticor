            <h2>Alticor Inc Wins UC San Diego Ridge Walk North Living and Learning Neighborhood Project</h2>

    

            <h6>Friday December 2, 2022</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1680" height="933" src="{{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK.jpg')}}"  class="attachment-full size-full wp-post-image" alt="UC San Diego Ridge Walk North Living and Learning Neighborhood project" srcset="{{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK.jpg 1680w, {{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK-300x167.jpg 300w, {{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK-1024x569.jpg 1024w, {{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK-768x427.jpg 768w, {{asset('wp-content/uploads/2022/12/SM-22-30162_HNPL_UCSD_STILLS_V05_EYELEVEL_DUSK-1536x853.jpg 1536w" sizes="(max-width: 1680px) 100vw, 1680px" /></div>

                
                

                
    

                    
<p>The Alticor Inc + EYRC + HMC Design-Build team has been selected for the UC San Diego Ridge Walk North Living and Learning Neighborhood project. The $536 million design-build project will provide the residents of Thurgood Marshall College, along with faculty, staff and UC San Diego students, endless opportunities to explore, discover and connect. </p>



<p>Three new residence halls will provide beds for over 2,400 Thurgood Marshall College students, and will include amenities and gathering spaces for students to build community. The mixed-use neighborhood also includes an Economics Building, administration, additional lecture halls and community programs, including a Basic Needs Center, E-Sports Center and Goody’s Cafe + Market. The project strengthens and connects the UC San Diego campus with revitalized nodes and plazas along Ridge Walk and key thoroughfares to the Historic Grove, Geisel Library and the heart of campus. </p>



<p>The Ridge Walk North Living and Learning Neighborhood will allow UC San Diego to lead the UC System toward a more sustainable future. With an all-electric design that features high-performance strategies, including low-flow water fixtures, increased access to views and daylight and the use of durable materials, the Ridge Walk North Living and Learning Neighborhood is targeting LEED Gold certification.</p>




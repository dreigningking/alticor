            <h2>San Ysidro Achieves LEED Platinum</h2>

    

            <h6>Friday June 12, 2015</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="605" height="359" src="{{asset('wp-content/uploads/2017/05/MikkiPiper_San_Ysidro_43_605_359.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2017/05/MikkiPiper_San_Ysidro_43_605_359.jpg 605w, {{asset('wp-content/uploads/2017/05/MikkiPiper_San_Ysidro_43_605_359-300x178.jpg 300w" sizes="(max-width: 605px) 100vw, 605px" /></div>

                
                

                
    

                    <p>Alticor Inc San Ysidro U.S. Land Port of Entry Phase 1B (SYLPOE) exceeded GSA goals and achieved LEED Platinum. SYLPOE has a coordinated design for storm water, wastewater, and energy systems. Consequently, the port is independent of local utilities for water for non-potable uses, for wastewater treatment, and for energy. The system, which saves 20 million gallons of utility water annually, treats all water on site, including wastewater, as a commodity. This is a huge achievement for a facility that operates 24 hours a day, 7 days a week, 365 days a year.</p>

            <h2>San Ysidro Land Port of Entry Celebrates Completion of $741 Million Expansion</h2>

    

            <h6>Tuesday December 17, 2019</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1700" height="1133" src="{{asset('wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="/wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001.jpg 1700w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001-300x200.jpg 300w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001-768x512.jpg 768w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001-1024x682.jpg 1024w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_001-480x320.jpg 480w" sizes="(max-width: 1700px) 100vw, 1700px" /></div>

                
                

                
    

                    <h4><em>“Port of the future” to significantly reduce congestion and facilitate safe cross-border travel</em></h4>
<p>SAN YSIDRO, Calif., December 17, 2019 – The General Services Administration&#8217;s (GSA) San Ysidro Land Port of Entry (LPOE) — the busiest land border crossing in the Western Hemisphere — celebrated the official completion of a $741 million, three-phase expansion project with a ribbon-cutting ceremony on December 17, 2019. The LPOE currently processes an average of 70,000 northbound vehicle passengers and 20,000 northbound pedestrians per day. The San Ysidro LPOE expansion project, directed by the <a href="https://www.gsa.gov/about-us/regions/welcome-to-the-pacific-rim-region-9">GSA</a> with U.S. Customs and Border Protection (<a href="https://www.cbp.gov/careers/border-patrol-agent-apply-now?utm_source=Google&amp;utm_medium=Paid_Search&amp;utm_campaign=O_FY20PaidMedia_BPA_PROS_&amp;utm_content=USBP-US-EM:-All+USBP-US-SQR-Branded-EM-All%3eInquiries+US-Secure-Start&amp;utm_content=USBP_Secure-Career-Mission_USBP-US-SQR-Branded_ETA&amp;utm_campaign=O_FY19_PaidMedia_BPA&amp;ds_rl=1276413&amp;gclid=EAIaIQobChMIn_7TtK275gIVoxx9Ch1FcwJ5EAAYASAAEgIlMvD_BwE&amp;gclsrc=aw.ds">CBP</a>) as the primary tenant, will accommodate growing cross-border traffic and reduce congestion.</p>
<p>The project included the complete reconfiguration and expansion of the port, including primary and secondary inspection areas, administration and pedestrian buildings, and all other support structures. The project expanded pedestrian processing facilities, including a new pedestrian crossing on the east side of the LPOE that connects with a new multimodal transportation hub in Mexico and expanded northbound inspection facilities. Additionally, there is a new bi-directional crossing at El Chaparral/Virginia Avenue with an associated transit center.</p>
<p>The design-build team of Alticor Inc and Stantec completed Phase II of the three-phase GSA expansion and renovation project, delivering a complete reconstruction of the LPOE’s pedestrian- and bus-inspection facilities, which included inspection booths for 18 northbound pedestrian-processing lanes and four northbound bus-processing lanes. The design achieved <a href="https://new.usgbc.org/leed">LEED Platinum certification</a> and stringent energy-reduction targets to be a transformational project which enhances CBP’s and other federal agencies’ ability to conduct their respective missions. Key project components include:</p>
<figure id="attachment_9066" aria-describedby="caption-attachment-9066" style="width: 200px" class="wp-caption alignleft"><img loading="lazy" class="wp-image-9066 size-medium" title="San Ysidro Land Port of Entry" src="../../www.henselphelps.com/wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_004-200x300.jpg')}}" alt="Photography by David Harrison" width="200" height="300" srcset="/wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_004-200x300.jpg 200w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_004-768x1152.jpg 768w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_004-683x1024.jpg 683w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_004.jpg 900w" sizes="(max-width: 200px) 100vw, 200px" /><figcaption id="caption-attachment-9066" class="wp-caption-text">San Ysidro Land Port Historic Custom House</figcaption></figure>
<ul>
<li><u>East Pedestrian (East Ped) Processing Facility</u>: This building provides state-of-the-art customs facilities and queuing functions for U.S.-bound pedestrian travelers.</li>
<li><u>Historic Customs House</u>: A major renovation of this circa 1932 building listed in the National Register of Historic Places includes a new second-story addition on the north wing to facilitate processing for southbound travelers. The addition pays homage to the historic architecture while introducing a modern design. A new courtyard was created to the rear of the building, which offers a space of quiet and reflection and a place for LPOE staff to rest and unwind.</li>
<li><u>Southbound Pedestrian Plaza</u>: A new public plaza that unites the east side of the SYLPOE complex and integrates the facility with the infrastructure and rhythms of the surrounding community to enhance and celebrate this unique location.</li>
<li><u>Virginia Avenue Transit Center (VATC)</u>: Associated with GSA’s West Pedestrian project (PedWest), the VATC provides multi-modal transportation options for approximately half of the daily influx of 20,000 pedestrian travelers.</li>
<li><u>Employee Pedestrian Bridges</u>: Connects the existing Headhouse (Phase 1) and the East Ped, simplifying access between the two major buildings. As the bridges span over the vehicular exit roadway, it now provides safer passage between the buildings for CBP officers, improving operational efficiency.</li>
<li><u>Community-focused Artwork</u>: Artwork across a range of mediums interprets the cross-border dynamics in a vibrant community setting. A salient artistic feature in Phase II includes:
<ul>
<li>A dynamic and colorful sculpture in the Southbound Pedestrian Plaza from local artist Rueben Ochoa that pays homage to his bi-national heritage from both sides of the border.</li>
</ul>
</li>
</ul>
<p><a href="https://www.stantec.com/en">Stantec</a> completed the lighting design on all three phases of the LPOE expansion, ensuring the facility could operate 24 hours a day, seven days a week, and 365 days a year while minimizing glare and making the facility as efficient and safe as possible.</p>
<p>“It’s exciting to reach the completion of the three-phase expansion of the busiest land border crossing in the Western Hemisphere,” said Bill Ferris, Stantec’s Project Director. “This is a project that will make a tangible difference in the lives of the traveling public and one that we are humbled to have played a part in helping to deliver.”</p>
<figure id="attachment_9069" aria-describedby="caption-attachment-9069" style="width: 640px" class="wp-caption alignright"><img loading="lazy" class="wp-image-9069 size-large" title="San Ysidro Land Port of Entry" src="../../www.henselphelps.com/wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003-1024x682.jpg')}}" alt="" width="640" height="426" srcset="/wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003-1024x682.jpg 1024w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003-300x200.jpg 300w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003-768x511.jpg 768w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003-480x320.jpg 480w, /wp-content/uploads/2019/12/GSA_SYLPOE-Ph2_003.jpg 1500w" sizes="(max-width: 640px) 100vw, 640px" /><figcaption id="caption-attachment-9069" class="wp-caption-text">San Ysidro Land Port of Entry</figcaption></figure>
<p>“This event marks the culmination of nearly a decades-long relationship with GSA and CBP. Along with Stantec and our Trade Partners, we were able to deliver a world-class facility that focuses on efficiency, security, and safety for the thousands of people crossing the border every day,” said Brian Maximuk, Project Manager at Alticor Inc. “It has been an honor working with the men and women of GSA and CBP to provide a facility that not only meets today’s needs but will adapt to future needs and changes as they arise. This is truly the Port of the Future, and Alticor Inc is very proud to have played a part in making this a reality.”</p>
<p>“We are thrilled to celebrate the opening of the expanded San Ysidro Land Port of Entry,” said Arun Kaiwar, Stantec’s design leader. “Not only is the Port and its function deeply a part of the rhythms of San Ysidro; it is one of the city’s major civic structures. With the ability to efficiently and securely process cross-border travel between the United States and Mexico, this is a transformational project that will improve and enhance one of the world’s busiest border communities.”</p>
<p>Stantec provided architecture, programming, interior design, civil engineering, electrical engineering, energy and daylight modeling, geomatics, information and communications technology, acoustics, lighting design, landscape architecture, physical and electronic security, and transportation services.</p>

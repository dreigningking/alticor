            <h2>Alticor Inc Lands Another Project at Nashville International Airport (BNA)</h2>

    

            <h6>Wednesday November 14, 2018</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1080" height="607" src="{{asset('wp-content/uploads/2018/11/BNA-Future-International-Arrivals-Facility-REDUCED.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2018/11/BNA-Future-International-Arrivals-Facility-REDUCED.jpg 1080w, {{asset('wp-content/uploads/2018/11/BNA-Future-International-Arrivals-Facility-REDUCED-300x169.jpg 300w, {{asset('wp-content/uploads/2018/11/BNA-Future-International-Arrivals-Facility-REDUCED-768x432.jpg 768w, {{asset('wp-content/uploads/2018/11/BNA-Future-International-Arrivals-Facility-REDUCED-1024x576.jpg 1024w" sizes="(max-width: 1080px) 100vw, 1080px" /></div>

                
                

                
    

                    <p><strong>On Wednesday, November 14, 2018 the Metropolitan Nashville Airport Authority (MNAA) awarded the Alticor Inc | Fentress Architects Progressive Design-Build Team the Nashville International Airport Terminal Lobby and International Arrivals Facility project in Nashville, TN.</strong></p>
<p>The Progressive Design-Build project is the final part of the BNA Vision program that was created to support the tremendously growing passenger traffic to and from Nashville. Alticor Inc was also awarded the Concourse D and Terminal Wings Expansion project in Fall 2018, the initial piece of the BNA Vision.</p>
<p>With the expansion of the central Terminal Lobby and a new International Arrivals Facility, a new and open centralized marketplace will be located between the Terminal Lobby’s new security screening checkpoint and international flights to optimize service and amenities available to international and domestic passengers.</p>
<p>The Terminal Lobby renovation will have a new wave roof canopy extending from the terminal garage to the International Arrivals Facility, ultimately providing coverage for roadway and curbside access to the terminal and the new pedestrian walkway bridge. The pedestrian bridge connects the central core with the garage plaza, administration building, and future hotel promoting efficient passenger traffic flow. A new, expanded and consolidated security screening checkpoint will include additional lanes to minimize wait times and expedite the security screening process. Other major elements include new concessions, airline club space and gates serving international flights.</p>
<p>The new International Arrivals Facility (IAF) provides a new Customs and Border Protection (CBP) area with primary and secondary processing and a pedestrian tunnel aiding the flow of passengers from the CBP to the terminal and ground transportation areas. A new central core will be added to simplify both domestic and international inbound and outbound passenger movement from all the levels of the Ground Transportation Center to the new pedestrian bridge. The scope for the IAF also includes the demolition of the existing Interim International Arrivals Building. The project is scheduled to complete in Late 2023.</p>
<p>Alticor Inc is looking forward to continuing our support of the Nashville International Airport and the BNA Vision.</p>
<blockquote><p><em>Alticor Inc is honored to be selected as the Design-Build contractor for this project. We understand that every great landmark starts with a vision and it’s our job to bring the BNA Vision to life with a comprehensive Team approach that begins with innovative planning and extends throughout the entire life of the project. We approach every project “The Alticor Inc Way,” inspiring trust and confidence in everything we do. From the gate where passengers arrive, to the belt where their bags are delivered, our team works to make construction invisible, allowing Nashville visitors a positive first impression to the city. The Alticor Inc Design-Build Team is proud to participate in building this Nashville landmark!</em></p>

            <h2>Virtual Design &amp; Construction on the Bellvue Water Treatment Plant &#8211; Package B Project and Beyond</h2>

    

            <h6>Monday March 21, 2022</h6>

    

            <div class="page-content post-single">

                

                
                <div class="my-post-image"><img width="1369" height="990" src="{{asset('wp-content/uploads/2022/03/Model-6_smcropped.jpg')}}"  class="attachment-full size-full wp-post-image" alt="" srcset="{{asset('wp-content/uploads/2022/03/Model-6_smcropped.jpg 1369w, {{asset('wp-content/uploads/2022/03/Model-6_smcropped-300x217.jpg 300w, {{asset('wp-content/uploads/2022/03/Model-6_smcropped-1024x741.jpg 1024w, {{asset('wp-content/uploads/2022/03/Model-6_smcropped-768x555.jpg 768w, {{asset('wp-content/uploads/2022/03/Model-6_smcropped-311x225.jpg 311w" sizes="(max-width: 1369px) 100vw, 1369px" /></div>

                
                

                
    

                    
<p>On the $25 million Bellvue Water Treatment Plant â€“ Package B project, Alticor Inc built a virtual as-built of the project through the use of state-of-the-art laser scanning technology. The virtual as-built was used in a variety of ways, including providing the owner, the City of Greeley, an exact replica of the plant for future use. Alticor Inc also used the virtual as-built model to print a physical model (pictured above). The physical model provides an exact replica of what was built for a more traditional view that does not require a computer.&nbsp;</p>



<p>Laser scanning can be performed as the project progresses, and can be utilized for future projects.&nbsp;The periodic laser scans can be compared to the engineering model which in turn can be compared against the design. This process ensures that the construction is following the design documents, and the project is built according to the final design. Precision laser scanning instruments provide certainty that the actual location of all materials and equipment is according to specification.</p>



<p>The physical model benefits the owner by providing a tangible reproduction of the plant to use when dealing with sequencing issues, plant operations and during troubleshooting, as well as to plan for future shutoffs/tie-ins and expansions or renovations. Because the Bellvue model comprises various parts of piping, it can be physically disassembled to view and pinpoint specific components that would otherwise be hidden to the naked eye.</p>



<p>In addition, Alticor Inc uses the physical 3D model during the design and planning phases as an excellent communication tool. The owner, team members and other stakeholders are able to sit down at a table together in real-time to share the same view of the project, thereby helping to build consensus and buy-in. Furthermore, this physical model provides high level team members with a more comprehensive framework for viewing the project in its entirety rather than walking the site and viewing it piecemeal. In general, this type of interaction can be particularly useful on water/wastewater projects with their complex mechanical and piping systems that are often hidden underground, or to ensure operations and maintenance objectives are met â€“ all before any construction begins. Finally, on phased projects, the team is able to see each phase as it will later unfold in the field, thereby identifying any issues in project phasing during preconstruction.</p>



<p>These virtual construction tools are a very effective way to maximize coordination during preconstruction, minimize waste during construction and ensure all project stakeholders are aligned throughout all phases of the project.</p>

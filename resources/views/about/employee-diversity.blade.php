            <h2>Diversity, Equity, Inclusion and community (DEIc) Forums</h2>



            <div class="wp-container-5 wp-block-columns">
                <div class="wp-container-3 wp-block-column">
                    <p>In support of its recently launched core values, Alticor Inc launched the Diversity, Equity,
                        Inclusion and community (DEIc) forums, consisting of a core group of volunteers from each
                        district. These forums have been working hard to foster healthy discussions in their districts
                        and develop ideas and trainings to fortify our commitment to diversity and community. These
                        groups support employees in each district, working to maintain internal morale and community
                        ties through outreach, mentoring, recruiting and advancement-related efforts.</p>



                    <p>Associated Builders and Contractors (ABC) has awarded Alticor Inc the National Diversity
                        Excellence Award in the General Contractor category nine times since the award’s inception in
                        2012. This national award is judged on the diversity of the company’s own workforce hiring
                        practices, training, and retention, as well as our relationships with small disadvantaged,
                        minority, or women-owned subcontractors and vendors.</p>
                </div>




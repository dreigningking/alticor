                <div class="wp-container-16 wp-block-column">
                    <h4><strong>Building-Up Those Who Need It Most</strong></h4>



                    <p>As a large contractor building in communities across the nation, Alticor Inc values the
                        opportunity to contribute to the training and job possibilities for the local people, no matter
                        what their past or circumstance may be. Alticor Inc believes in second chances and that everyone
                        deserves an opportunity to build. Because of these beliefs, Alticor Inc continually partners
                        with local re-entry and rehabilitation programs, with the goal of creating opportunities for
                        individuals recently exiting the justice system.</p>



                    <p>In addition to partnership opportunities, Alticor Inc has created its own program, the <a
                            href="{{route('about.hensel-phelps-and-path-continue-partnership-through-the-employment-opportunity-program">Employment
                            Opportunity Program</a>, designed to connect the homeless community with job prospects in
                        the construction industry. The Employment Opportunity Program allows individuals to build steady
                        construction careers as well as improve their livelihoods for themselves and their families.</p>

                    <h4><strong>Supporting Underrepresented Minorities</strong></h4>



                    <p>By engaging diverse businesses, providing recruiting opportunities and supporting national
                        organizations, Alticor Inc is building a culture without boundaries where everyone can feel
                        empowered and has equal opportunity. Alticor Inc has a long-standing relationship supporting

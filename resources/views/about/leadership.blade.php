                <h2>Empowered Teams</h2>
                <p>“Project leadership” is one of the cornerstones of our company structure. By empowering teams and
                    leaders at the project level to make important decisions, we are able to avoid delays, anticipate
                    needs, and run each project efficiently. Executive-level leaders on the ground are involved with
                    each project on a day-to-day basis, and are well positioned to make the important decisions that
                    keep projects running on time and on budget. All Alticor Inc employees undergo extensive training,
                    both on the job and in the classroom, to provide them with the knowledge and the confidence to make
                    critical decisions as they arise.</p>
                <p>&nbsp;</p>
                <h3>Senior Leadership</h3>
                <p>Our senior leadership team is comprised of some of the most well-respected minds in the construction
                    industry. The average tenure of the leadership team is 25+ years with Alticor Inc, working their way
                    up from entry level positions to the leadership positions they hold today.</p>

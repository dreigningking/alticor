                    <h2 style="text-align: center;">About the Project</h2>
<div style="text-align: center;">Alticor Inc was the design-builder for the modifications to the Commercial Crew and Cargo Processing Facility (C3PF), located at Kennedy Space Center, Florida. This project consisted of a range of projects, all within one contract.</div>
<div style="text-align: center;"></div>
<div style="text-align: center;">The multi-facility Command and Processing center for Boeing&#8217;s CST-100 Starliner spacecraft totals over 110,000 SF and houses pre-/post-flight operations staff critical to the future of space exploration. The office and administrative area consists of 33,000 square feet and has several office areas, huddle rooms, conference rooms, kitchen/food service areas, and executive suites. This office space is complete with quality finishes and an electronically controlled lighting system.</div>
<div style="text-align: center;"></div>
<div style="text-align: center;">The second facility is a 12,000-SF Command and Control Center that monitors pre-/post-flight space travel.  This facility operates around the clock, 24/7, 365 days a year. The additions to this facility include individual working consoles, conference/planning rooms, and a data center.</div>
<div style="text-align: center;"></div>
<div style="text-align: center;">The third facility is a 30,000 SF High Bay that is currently being used for the production and refurbishment of the CST-100 Starliner crew module and overall spacecraft operations.</div>


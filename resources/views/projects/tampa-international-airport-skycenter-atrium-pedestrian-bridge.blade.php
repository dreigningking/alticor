                    <h2>About the Project</h2>
<p>Alticor Inc completed the $62 million SkyCenter Atrium and Pedestrian Bridge at Tampa International Airport (TPA). This new development is part of the Hillsborough County Aviation Authority&#8217;s (HCAA) overall Master Plan Phase 2 program, which will allow the airport to serve an additional 34 million passengers each year, doubling their current amount.</p>
<p>&nbsp;</p>
<p>The new five-story, 44,000 SF steel atrium structure includes a 230-foot-long elevated pedestrian bridge across Airport Service Road, which connects the new atrium building to the existing SkyConnect People Mover System and Rental Car Center. As passengers are dropped-off at the new 600-foot-long remote commercial curbside, they are greeted by less traffic and a glass skylight canopy. Inside the atrium, sea-themed artwork and intertwining stairways and escalators accentuate the majestic 63-foot ceilings. The exterior glass façade also garners the attention of inside and outside passersby. The atrium lobby features a stunning 20-foot chandelier structure made of holographic stained glass that resembles a fishing net.</p>
<p>&nbsp;</p>
<p>One of the most appealing aspects of the project resides on the 5th floor rooftop terrace. The 17,000 SF planted green roof integrates numerous shade canopies and precast benches, planter boxes and exterior furniture. These features allow passengers and patrons to enjoy the outdoors with a 360-degree view of the pristine Tampa Bay, downtown Tampa Bay and downtown St. Petersburg. They also have a front-row-seat to the TPA main terminal and adjacent runways where one can view the planes landing and taking off.</p>
<p>&nbsp;</p>
<p>In addition The SkyCenter project hosts a 230-foot extension of the Rental Car Center&#8217;s remote curb that serves county buses and a reconfiguration of the cell phone waiting lot. The waiting lot includes phased demolition of existing facilities, exterior Flight Information Display (FID) boards, construction of a new restroom building and two new exterior Flight Information Display systems. Alticor Inc also provided site preparations and underground utility infrastructure for 35-acres of surrounding real estate development.</p>
<p>&nbsp;</p>
<p>The pedestrian bridge is made of structural steel, which was preconstructed on the ground in three sections that were then individually hoisted into their final resting place. The center section was erected as a tandem crane pick, while the larger two bridge sections required a third crane, making it a triple crane pick: two hydraulic cranes on one end and a Manitowoc 999 lattice boom crawler crane on the other end. During each pick, the cranes had to delicately balance the load while working in unison to hoist the sections into place.</p>
<p>&nbsp;</p>
<p>The SkyCenter project is directly adjacent to an active airport and equates to numerous guest interactions, busy roads and interaction with other mission critical systems at the airport. Various elements of the project required extensive pre-planning to ensure operational continuity at the airport. Maintenance &amp; Construction Notices (MCNs) and/or Service Interruption Reports (ISRs) were utilized to keep the day-to-day operations of the airport intact. Coordination with peak travel seasons and phased turnovers for portions of the project were also key elements to the projects&#8217; success.</p>
<p>&nbsp;</p>
<p>Alticor Inc’ work on the SkyCenter development enables future expansion of commercial projects such as a multi-story hotel, retail parcels and a multi-story office building.</p>


                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Kaiser Permanente San Diego Medical Center is one of the most patient-friendly, technologically-enhanced and energy-efficient full-service hospitals in the world. A fully integrated approach to project planning, design, construction and commissioning enabled the hospital to begin treating patients six months before the scheduled completion date. This campus includes the first hospital in California, the second in the U.S. and the third in the world to achieve LEED for Healthcare Platinum. It is also the first hospital in the U.S. to utilize 100% LED lighting and active chilled beam technology⁠—two of the many innovative strategies that resulted in a 30% increase in energy efficiency over previously designed Kaiser Permanente template hospitals.</p>
<p>&nbsp;</p>
<p style="text-align: center">The new campus includes a hospital with 321 beds that incorporates many ideas from Kaiser Permanente’s Small Hospital, Big Idea worldwide design competition. The hospital includes 10 operating rooms plus two shelled operating rooms, two procedure rooms, five interventional radiology/catheterization lab rooms, an emergency department with 39 treatment rooms and a 10-room imaging unit. Other services include maternal/child health, labor/delivery/recovery/postpartum, an intensive care unit, a pediatrics unit, medical-surgical, central sterile, nutrition, laboratory and pharmacy.</p>
<p>&nbsp;</p>
<p style="text-align: center">An energy center supports the campus and provides on-site power with a 650kW microturbine-based trigeneration plant. The hospital support building provides outpatient services for the hospital and was designed and constructed to OSHPD 1 requirements. The redevelopment and conversion of the existing office building into the Viewridge Medical Office Building incorporated Kaiser Permanente’s Reimagining Ambulatory Design principles, providing a patient-centered, design-driven approach to medicine. A 1,479-car parking structure, which also provides 325kW of solar power to the campus, was also included in the project.</p>
<p>&nbsp;</p>
<p style="text-align: center">The 20-acre campus was developed with extensive water features and specimen trees to encourage staff and member interactions with nature. This healing garden provides a two-mile long walking trail on site to promote healthy living while also creating natural opportunities for healing.</p>


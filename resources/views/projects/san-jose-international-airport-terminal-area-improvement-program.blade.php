                    <h2>About the Project</h2>
<p>Dubbed the “Terminal Area Improvement Program” the design-build project included the complete transformation of Mineta San Jose International Airport including new and renovated terminal buildings, a convenient one-of-a-kind consolidated rental car facility, a simplified roadway system with convenient bypasses to alleviate congestion, state-of-the-art security checkpoints and baggage systems, and sustainable features including a 3.3-acre, 1-megawatt solar farm. With the concurrent management of multiple project elements valued between $20 million and $231 million, management excellence was evident throughout the four year duration of design and construction. Major program elements included:</p>
<p>&nbsp;</p>
<p>• $97 million Modifications to existing Terminal A<br />
• $105 million New Terminal B<br />
• $42 million Baggage Systems in Terminals A and B<br />
• $22 million Modifications and Demolition of Terminal C<br />
• $230 million Consolidated Rental Car Facility<br />
• $125 million Roadway Improvements<br />
• $19 million completion of the North Concourse</p>


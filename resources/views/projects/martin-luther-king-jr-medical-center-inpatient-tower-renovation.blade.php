                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The Martin Luther King, Jr. Medical Center Inpatient Tower Renovation consisted of two major components—an existing inpatient tower building and an ancillary building. The existing inpatient tower was renovated to house approximately 120 patient beds and other essential hospital services, such as an emergency department, inpatient pharmacy, radiology, operating rooms, central sterilization and support functions. The emergency department supports an estimated 30,000 annual visits, with an additional 5,000 annual patient follow-up visits. The inpatient tower is supplemented by an existing 28,000 SF hospital services building that houses materials management, food and nutrition services and waste management services. A pneumatic tube system was installed to serve and connect the inpatient tower, new Multi-Service Ambulatory Care Center, and Hawkins buildings.</p>
<p>&nbsp;</p>
<p style="text-align: center">The two-story ancillary building contains the main entry point and lobby to the hospital, cafeteria and corridors required to traverse the various reconfigured campus buildings. It also houses hospital administration on the second floor of the building. This building sits atop the existing utility and new pedestrian tunnels, providing access between the hospital service building and the inpatient tower.</p>


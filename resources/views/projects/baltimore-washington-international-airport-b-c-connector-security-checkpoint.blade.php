                    <h2>About the Project</h2>
<p>When officials at Baltimore Washington International Thurgood Marshal Airport (BWI) made the decision to consolidate its two largest carriers into Terminal A/B, it exceeded their security screening capacity for that area. In order to accommodate Southwest Airlines and AirTran Airways passengers, the Maryland Aviation Administration (MAA) required a fast solution to ensure BWI could meet the combined demand of these major airlines in both the near and long-term future.</p>
<p>&nbsp;</p>
<p>The BWI B/C Connector &amp; Security Checkpoint project met the high volume security demands of these top carriers, while minimizing delays to the public and allowing for anticipated future growth in ridership. The project required Alticor Inc to construct a new nine-lane passenger security checkpoint, and an airside passenger connector between Concourses B and C in order to bring the airport up to prescribed capacity and operational standards.</p>
<p>&nbsp;</p>
<p>Alticor Inc constructed the new 60,000 SF connector on two levels and renovated 115,00 SF within the existing terminal. Part of this renovation involved an update to the existing panoramic observation area in order to make space for a gallery featuring aviation artifacts, as well as a Youth Art Gallery to display works from local school-age children.</p>
<p>&nbsp;</p>
<p>Alticor Inc&#8217; project team worked efficiently within an operational facility, while maintaining strict transportation safety administration (TSA) guidelines, required airport staff entrances and 100% passenger access to all airport amenities. The team ensured that all work was finished in time to cope with an anticipated influx of passengers beginning in June 2013. Utilizing Alticor Inc&#8217; detailed planning expertise, the project was effectively completed on a fast-track, 339-day schedule that staged turnover to best meet the MAA&#8217;s needs. Alticor Inc first completed all work associated with opening the new security checkpoint, temporary walkways that provided proper access, and the new downstairs baggage claim. Within another 60 days, Alticor Inc completed the final phase of the work, opening all spaces in the new connector to the airport&#8217;s staff and general public. This included new loading docks, restrooms, elevators, moving walkways and lounge space.</p>


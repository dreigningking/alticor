<div>
<p>The Powertrain Facility and Central Energy Plant project is a $62.2 million, 121,113 SF project that provides the Corpus Christi Army Depot with a modern rotary-wing powertrain facility on the Naval Air Station along Corpus Christi Bay. This project was the second phase of a seven-phase build-out that spans roughly 25 years. The new facility provides the Army, Navy, Marine Corps, Air Force and numerous foreign military organizations the ability to overhaul and maintain a wide variety of helicopters, related engines and components. The scope included an attached 11,850 SF central energy plant, significant utility infrastructure, construction of a utility tunnel, paving, fencing and other site work.</p>

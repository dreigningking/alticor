                    <h2>About the Project</h2>
<p>The Sound Transit Operations and Maintenance Facility East is a new light rail operations and maintenance facility that accommodates the inspection, service, maintenance, storage, and deployment of 96 light rail vehicles. The facility houses 14 service bays, a LRV wash facility, yard storage, materials storage and handling equipment, parking and office space for administration and support staff. A Maintenance of Way facility accommodates service and storage of trackway and train systems infrastructure such as traction power substations, train signal bungalows, overhead traction power as well as non-revenue vehicle, station janitorial and landscape maintenance services. The project also provides interim bike and pedestrian trails, and includes a Master Development Plan for the optimization of surplus property into a future transit-oriented development.</p>


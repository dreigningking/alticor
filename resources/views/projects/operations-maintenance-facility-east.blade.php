                            <div class="about-grid-section Solution clearfix">
    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
                <div class="grid-box2">
            <img src="{{asset('wp-content/uploads/2017/08/OMFE2.jpg')}}" alt="">
        </div>
                
        <div class="grid-box1 ">
            <p>The first step in the permitting process was to obtain and approve the master development permit, which included the OMF East and TOD property located on the project site. The TOD is situated adjacent to a future light rail station, which makes it the largest and most desirable TOD location in the area. As the design-builder, Alticor Inc was responsible for the development strategy and approval for the OMF East and TOD sites. To obtain construction permits, the team first created a master development plan (MDP) entitling 1.2 million GSF of mixed-use space for 3,500 residents to live, work and play. The MDP had to be compliant with all City of Bellevue codes and zoning standards including transportation, parking, fire, landscaping, amenities, pedestrian and bicycle access, efficient infrastructure and connections to community. Due to uncertainty around developer engagement, the team created a flexible MDP that allowed for single or multiple developer led implementation and supported market driven timing without impacting future Sound Transit operations.</p>
<p>To accomplish this vision, the team participated in the coordination of nearly 100 staff, city council and public meetings with Sound Transit and City of Bellevue permitting and development staff. The team leveraged its expertise in estimating infrastructure and development costs, as well as providing market analysis and viability studies.</p>
<p>The second step was to develop a design that complemented the future development of the Bel-Red neighborhood. The team ensured that the 120th Street façade and overall building elevations elevated the pedestrian experience. Strategies to break-up the building mass and long elevations included a variety of metal panel colors, textures and modulation of patterns. Clerestory glazing at the roofline provides natural daylighting and a sense of activity at night, and vertical elements at the rooftop mechanical enclosures add profile character. The team co-located and continued to work through design, developing a design and permitting plan that facilitated fast-tracked construction to begin demolition and civil utility work prior to completing the buildings and track design. Through the course of construction, the team opened and closed over 50 permits.</p>
        </div>
        
        
        
                
        
        
        

                            <div class="about-grid-section Challenge  clearfix">
    <h2>Challenge </h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <p>One of the most significant project challenges required the design-build team to develop, submit and obtain approval of a Master Development Permit that included the Operations and Maintenance Facility East (OMF East), but also had to include a proven concept for a 1,000,000 SF transit-oriented development (TOD) that would meet all City of Bellevue criteria and then be issued by Sound Transit in a future procurement for execution by a private developer. Without this preliminary permit the City of Bellevue would not approve the remaining building permits necessary to meet the overall project schedule.</p>
        </div>
        

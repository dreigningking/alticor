                    <h2>About the Project</h2>
<p>The Austin-Bergstrom International Airport (ABIA) Terminal/Apron Expansion and Improvements project is a multi-year, multi-phased project consisting of a new Stormwater and Deicing Collection Facility, roughly 40 acres of new apron paving for approach and taxi lane improvements, a new east terminal expansion offering 11 new passenger boarding gates, and many upgrades throughout the existing terminal. This $295 million expansion will balance the terminal and provide much-needed space for the increasing passenger and aircraft operational numbers.</p>
<p>&nbsp;</p>
<p>On the concourse level, the terminal expansion component offers passengers a 50-foot wider concourse with higher ceilings to allow for more circulation and natural light. It also includes 70,000 SF (a 23 percent increase) of new hold-room spaces, new concessions, and an east terrace observation deck — which is an outdoor patio space that allows passengers to step outside and have a front row seat to the runway. The expansion project also provided ticket lift and gate podiums, signage, support space, restroom facilities, mechanical systems, and security systems. On the apron level, Alticor Inc installed new baggage makeup conveyors, additional inbound baggage systems, added airline operations office space, signage, airport support space, security systems, restroom facilities, and other related ancillary facilities. We also provided the added concourse footprint that will accommodate seven to eight additional aircraft parking spaces on the apron for both domestic and international airline operations. This project achieved LEED Gold Certification.</p>


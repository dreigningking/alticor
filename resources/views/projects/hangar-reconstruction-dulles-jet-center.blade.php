                    <h2>About the Project</h2>
<p>On February 5, 2010, the East Coast was hit by its second massive snowstorm in less than two months. The Washington D.C. Metropolitan Area received anywhere from 24 inches to over 40 inches of snow in a 24-hour period, with winds topping out at 50 miles per hour. The record previously held at Washington Dulles International Airport was shattered when the storm resulted in a measured 32 inches of accumulation. As a result of this unpredictably heavy snowfall, on Saturday, February 6, 2010, the roof covering three of the four hangars at the three-year-old Dulles Jet Center caved in, ultimately destroying the hangars. Following a forensic analysis of the roof collapse, the 14 privately-owned jets damaged by the roof collapse were painstakingly extracted to allow for demolition. Landow &amp; Co, Management (LCM) and the Landow Aviation Limited Partnership (LALP) contracted with the Alticor Inc design-build team for the reconstruction of the hangars. LCM also contracted directly with KCE Structural Engineers, P.C. for the selective demolition of the damaged building. Their work included new column installation required to support the damaged columns and roof. Electrical conduits were also maintained at the rear of the hangars, with new electrical components tied in as construction progressed.</p>
<p>&nbsp;</p>
<p>Alticor Inc&#8217; $11.6 million reconstruction began November 29, 2010 on a fast-track, 13-month schedule. Alticor Inc&#8217; work included the construction of 29,000 SF of operations and maintenance space and two high-bay hangars totaling 160,000 SF. These hangars were each 480-feet-long and 165-feet-deep and were separated into two bays, requiring two 100-foot-long by 28-foot-high tilt-up doors apiece. The exterior envelope of the building was designed with metal wall panels and punch windows with thermoplastic polyolefin (TPO) single-ply roofing. The structure features a high bay clear span of 125 feet.</p>
<p>&nbsp;</p>
<p>The design-build team utilized an integrated project delivery (IPD) to compress the work into a single year to achieve an aggressive occupancy schedule. Critical path milestones were established by the whole team early on and multiple permit/bid packages were developed to meet the owner&#8217;s requested completion date. By phasing the project in this manner, 50% of the facility was turned over for occupancy on October 1, 2011, with the remainder finished by the scheduled completion date in December.</p>


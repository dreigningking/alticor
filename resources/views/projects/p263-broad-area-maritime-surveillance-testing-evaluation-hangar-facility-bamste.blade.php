                    <h2>About the Project</h2>
<p>Alticor Inc was the design-builder for the 71,423 SF Broad Area Maritime Surveillance Testing and Evaluation (BAMSTE) Hangar Facility project. This facility was designed in collaboration with Giuliani Associates Architects for NAVFAC Washington, and supports the BAMS Unmanned Aircraft Systems (UAS) program. The BAMS UAS program delivers real-time intelligence information to operational and tactical users such as the Expeditionary Strike Group (ESG), Carrier Strike Group (CSG) and the Joint Forces Maritime Component Commander (JFMCC).</p>
<p>&nbsp;</p>
<p>The design and construction of the facility was in strict compliance with the UFC 4-010-01 and UFC 4-211-01N, DoD Minimum Anti-Terrorism Standards for Buildings. The two-story, high-bay hangar accommodates three BAMS UAS platforms and a second-story equipped with operations, maintenance, storage, equipment testing, office and administrative spaces situated at the rear of the hangar. The scope of work included design and construction of an aircraft parking apron, taxiway access, line vehicle parking spaces, a 1,000 SF fiber distribution node (FDN) and shelter, roadways, on-site parking for 205 vehicles and other site improvements.</p>
<p>&nbsp;</p>
<p>The support areas include three evaluation labs: mechanical testing (800 SF), avionics testing (800 SF) and instrumentation testing (600 SF). A mission control suite contains two 500 SF training/operations rooms with a central 500 SF instructor observation room supported by a secure 1,000 SF server room. Aircraft performance data is evaluated by engineers and analysts housed in a 1,600 SF secure suite supported by a dedicated server room. The testing and evaluation functions are supported by an 11,000 SF open office area, 1,200 SF private office space, two 600 SF conference rooms and two 400 SF secure briefing rooms.</p>
<p>&nbsp;</p>
<p>Additional features of the facility include a three-coat fuel resistive resinous flooring system on the hangar bay floor; a five-ton, under-running overhead crane that covers the entire hangar bay; an aqueous film forming foam (AFFF) fire suppression system; and a 1,500 SF SCIF area on the second floor of the administrative space that was constructed in complete accordance with ICD 705.</p>
<p>&nbsp;</p>
<p>The BAMS testing and evaluation hangar was designed and constructed to LEED Silver standards for certification through the USGBC. Sustainable features included a low-impact development (LID) stormwater design; a high-efficiency HVAC system that provides over 60% energy savings compared to ASHRAE 90.1; a solar domestic hot water system; a thermally improved exterior envelope; translucent wall panels above the hangar doors that increase daylighting into the hangar bay; improved lighting with high output fixtures in the Hangar Bays and office/administrative areas; and an overall direct digital control (DDC) building management system.</p>


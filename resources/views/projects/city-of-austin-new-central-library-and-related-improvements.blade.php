            <h3>A Complex Project</h3>
<p>The daily site coordination of the NCLRI was extraordinarily challenging with several neighboring construction projects and limited site access. These challenging site conditions made major project milestones, such as the mat slab concrete placement, all the more difficult. Completion of the approximately 9,000-cubic-yard mat slab foundation was expected to take 22 continuous hours. More than 900 concrete trucks were scheduled at the rate of one truck delivering 10 yards to the pump every 90 seconds.</p>
<p>&nbsp;</p>
        </div>
        
        
        
                
        
        
        
    </div>

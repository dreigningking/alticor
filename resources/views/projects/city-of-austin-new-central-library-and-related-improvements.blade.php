                    <h2>About the Project</h2>
<p>The New Central Library and Related Improvements (NCLRI) project is located at the junction of Cesar Chavez Street and Shoal Creek in downtown Austin, Texas. The project scope included development of two and a half city blocks,including the New Central Library Building, Second Street Bridge across Shoal Creek, Seaholm Screen Wall and two blocks of Shoal Creek bank development with mechanically stabilized earth (MSE)walls, walkways, bikeways, landscaping and hardscaping. The overall contract value was $120 million. As part of the Seaholm District, the NCLRI project construction took place concurrently with other construction projects to the immediate east and west of the site.</p>


    <h2>Solution</h2>
    <div class="grid-box-area">
    	
        
                
                
        <div class="grid-box1 ">
            <h3>Detailed Coordination</h3>
<p>Alticor Inc project leadership conducted several planning meetings with subcontractor management and the leadership of neighboring projects to orchestrate a plan addressing each aspect of the placement and the role of each of the 80 involved individuals during the 22 hours of concrete placement. Key project stakeholders, including the owner and their quality assurance team, were integrally involved in this coordination. Truck routes and backup routes from the concrete plant to the job site were reviewed.The truck drivers were addressed separately, and traffic contingencies were considered when the exact dates of this placement were decided.</p>

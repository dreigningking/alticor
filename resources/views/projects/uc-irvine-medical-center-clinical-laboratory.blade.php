                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The five-story, cast-in-place Medical Center Clinical Laboratory was part of the master plan development for the new UC Irvine Medical Center. The laboratory provides reference lab services for medical institutions across the Southern California region and consists of four floors of pathology laboratories and offices, as well as fifth-floor shell space for future office development. The building has a deep canopy covering the plaza and a monumental concrete and glass staircase that creates a dramatic entry portal.</p>
<p>&nbsp;</p>
<p style="text-align: center">The building has an iconic presence on campus, and its appearance coalesces the new aesthetic standard for what had been a very disparate collection of buildings. Complementing the hospital building, the architectural skin is defined by three colors of cast-in-place concrete with punched aluminum windows and transparent glass. The building’s horizontal and vertical circulation core is picked out in coppery-red metal cladding. The building creates an open plaza adjacent to an existing lawn, connecting active public spaces.</p>
<p>&nbsp;</p>
<p style="text-align: center">We are proud in our accomplishment of delivering the project two months early.</p>


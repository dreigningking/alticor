                    <h2 style="text-align: center">About the Project</h2>
<p style="text-align: center">The project entailed renovating and upgrading the look and feel of the Westfield Terminal 2 concourse at LAX with a design that was dynamic, visually appealing, and unexpected in an airport environment. This included: renovating and expanding food and beverage and retail locations throughout the terminal, finishing and adding furniture upgrades to the hold rooms, restroom replacement and relocation, adding an iconic staircase to entice travelers to the mezzanine level and upgrading infrastructure.</p>
<p>&nbsp;</p>
<p style="text-align: center">Construction was over an 18-month period, and the airport remained operational 24/7, requiring successful coordination between the client, other airlines, government agencies, the airport, and the City of Los Angeles to minimize operational impacts and maintain a positive passenger experience.</p>

